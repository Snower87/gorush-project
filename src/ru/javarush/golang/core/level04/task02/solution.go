package main

/*
= Задача №2. Чат с подписью =
Вы настраиваете тестовый чат-бот для стрима: он должен несколько раз повторить одно и то же сообщение, но так, чтобы
модераторы могли легко сослаться на конкретную строку по номеру.
Программа читает целое число repeatCount и строку message (одно слово без пробелов), а затем выводит repeatCount строк
вида k: message, где k — номер строки от 1 до repeatCount. Если repeatCount равно 0 или меньше, бот молчит и ничего не выводит.
Требования:
• Программа должна считать из stdin целое число repeatCount и строку message (одно слово без пробелов) с помощью fmt.Scan.
• Если repeatCount равно 0 или меньше, программа не должна выводить ничего.
• Для повторения вывода программа должна использовать классический цикл for с тремя частями (инициализация; условие; пост-действие), без использования for-range.
• Счётчик цикла должен начинаться с 1, а условие должно включать repeatCount (например, i <= repeatCount), чтобы выводились строки с номерами 1..repeatCount.
• Каждая выводимая строка должна иметь точный формат: k, затем двоеточие, затем пробел, затем message (пример: 3: hello), без дополнительных символов.
*/

import "fmt"

func main() {
	var repeatCount int
	var message string

	fmt.Scan(&repeatCount, &message)

	if repeatCount <= 0 {
		return
	}

	// TODO: Выведите repeatCount строк в строгом формате "k: message", где k нумеруется от 1 до repeatCount включительно.
	// TODO: Используйте классический цикл for (init; cond; post) и печать построчно через fmt.Println.
	// TODO: При выводе не должно быть лишних пробелов/символов (пример строки: "3: hello").
	for i := 1; i <= repeatCount; i++ {
	    fmt.Printf("%d: %s\n",i, message)
	}
}