/*
= Задача №1. Паспорт чисел =
Представьте, что вы настраиваете маленький “инспектор данных” для программы: ему важно видеть не только само число, но и в каком именно “контейнере” (типе) оно лежит.
Вы кладёте одно и то же значение 17 в три разные коробочки — int, int64 и uint — и просите программу распечатать “паспорт” каждой коробочки: значение и тип.
Программа ничего не читает: все числа задаются прямо в коде. На экране должны появиться три строки ровно в таком виде, чтобы глазами было видно: значение одно и то же, а типы разные.
Требования:
• Программа должна быть оформлена как исполняемая: содержать package main и функцию func main().
• Программа должна импортировать пакет fmt для форматированного вывода.
• Внутри main должна быть создана переменная со значением 17 типа int с использованием короткой декларации :=.
• Внутри main должна быть создана переменная со значением 17 типа int64 через явное приведение int64(17).
• Внутри main должна быть создана переменная со значением 17 типа uint через явное приведение uint(17).
*/

import "fmt"

func main() {
	// Одно значение в трёх "контейнерах" разных целочисленных типов.
	i := 17

	// TODO: Создай переменную i64 со значением 17 типа int64 через явное приведение int64(17).
	i64 := int64(17)

	// TODO: Создай переменную u со значением 17 типа uint через явное приведение uint(17).
	u := uint(17)

	fmt.Printf("%v (%T)\n", i, i)
	fmt.Printf("%v (%T)\n", i64, i64)
	fmt.Printf("%v (%T)\n", u, u)
}