package main

/*
= Задача №8. Поиск честного чека =
Вы подключаете кассовый терминал к потоку чисел: иногда там встречаются мусорные значения (слишком большие или неположительные), а вам нужен первый “честный чек” по правилам. Как только подходящий чек найден — дальше читать поток уже не нужно, можно сразу остановиться и показать результат.
Вводится последовательность целых чисел. Остановка чтения происходит при вводе 0 или при завершении ввода (ошибка чтения). Программа должна искать первое “валидное” число, которое одновременно: положительное, не больше 10000 и делится на 13 без остатка. Все числа, не попадающие в диапазон 1..10000, должны игнорироваться с помощью continue и не участвовать в нумерации.
Как только найдено подходящее число, программа должна немедленно завершить цикл через break и вывести строку FOUND <value> <index>, где index — порядковый номер среди валидных чисел (в диапазоне 1..10000), начиная с 1. Если подходящее число не найдено, программа должна вывести NOT FOUND.
Требования:
• Программа должна использовать бесконечный цикл for { ... } для последовательного чтения чисел из stdin.
• Каждую итерацию программа должна читать одно целое число через fmt.Scan; цикл должен завершаться, если введено 0 или если fmt.Scan вернул ошибку чтения (например, EOF).
• Если число не попадает в диапазон 1..10000 (то есть <= 0 или > 10000), программа должна пропускать его с помощью continue, не выполняя дальнейшие проверки и не меняя счётчик нумерации.
• Программа должна вести счётчик index, который увеличивается только для чисел в диапазоне 1..10000; нумерация начинается с 1 и отражает порядковый номер среди таких “валидных по диапазону” чисел.
• При обработке очередного числа из диапазона 1..10000 программа должна проверить делимость на 13 без остатка; при нахождении первого подходящего числа цикл должен немедленно завершиться через break, а результат должен быть сохранён для вывода.
*/

import "fmt"

func main() {
	var (
		index    int
		found    bool
		foundVal int
		foundIdx int
	)

	for {
		var x int
		if _, err := fmt.Scan(&x); err != nil {
			break
		}
		if x == 0 {
			break
		}

		if x <= 0 || x > 10000 {
			// TODO: Числа вне диапазона 1..10000 нужно игнорировать через continue и НЕ учитывать в нумерации.
			continue
		}

		index++ // TODO: Индекс должен увеличиваться только для чисел из диапазона 1..10000.

		// TODO: Проверьте, подходит ли число по условию (делится на 13 без остатка).
		// TODO: Если подходит — сохраните found/foundVal/foundIdx и немедленно завершите цикл через break.
		if x % 13 == 0 {
		    found = true
		    foundVal = x
		    foundIdx = index
		    break
		}
	}

	if found {
		fmt.Printf("FOUND %d %d\n", foundVal, foundIdx)
		return
	}

	fmt.Println("NOT FOUND")
}