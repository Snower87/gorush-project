package main

/*
= Задача №9. Декодер подчёркиваний =
Вы разбираете строку, пришедшую из старого протокола: пробелы там запрещены, поэтому их заменяют на подчёркивания '_'. Перед тем как показать текст пользователю, нужно восстановить пробелы обратно — быстро и честно, байт за байтом.
Программа читает из stdin одну строку без пробелов. В новой строке каждый байт '_' должен превратиться в байт пробела ' ', а остальные байты остаться как есть. Для решения нужно преобразовать строку в []byte, заменить нужные элементы по индексам и собрать обратно string(b).
Программа выводит итоговую строку и перевод строки.
Требования:
• Программа должна прочитать из stdin одну строку как один токен (без пробелов).
• Для обработки программа должна создать байтовый слайс через b := []byte(s).
• Программа должна пройти циклом по индексам i от 0 до len(b)-1 и заменить каждый байт '_' на байт пробела ' ' через присваивание b[i] = ' ' (остальные байты не изменять).
• После завершения замен программа должна создать новую строку через string(b).
• Программа должна вывести итоговую строку в stdout, обязательно с переводом строки в конце.
*/

import (
	"bufio"
	"fmt"
	"os"
)

func main() {
	in := bufio.NewReader(os.Stdin)

	// Читаем одну строку как один токен (без пробелов).
	var s string
	fmt.Fscan(in, &s)

	// Работаем байт за байтом: строка -> []byte.
	b := []byte(s)

	// TODO: реализовать побайтовую замену всех '_' на пробел ' ' через присваивание b[i] = ' '
	for i := 0; i < len(b); i++ {
		// TODO: дописать условие и замену, остальные байты оставлять без изменений
		if b[i] == '_' {
		    b[i] = ' '
		}
	}

	// Собираем обратно в строку и печатаем с переводом строки.
	fmt.Println(string(b))
}