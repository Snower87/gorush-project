package main

/*
= Задача №18. Лента показаний =
Вы собираете показания с приборов в одну ленту: важно вывести ровно те числа, которые ввели, без лишних нулей, которые могли бы появиться, если случайно создать слайс неправильной длины. Заодно вы хотите сразу знать сумму, чтобы быстро сверить отчёт.
На вход подаётся целое число n, затем n целых чисел. Соберите их в слайс values типа []int, используя append в цикле и предвыделение ёмкости. Слайс должен создаваться как make([]int, 0, n).
После ввода программа выводит три строки:
- в первой — все числа через пробел в исходном порядке,
- во второй — len и cap слайса в формате len=<число> cap=<число>,
- в третьей — сумму всех введённых чисел.
Если n равно 0, первая строка пустая, а сумма равна 0.
Требования:
*/

import (
	"bufio"
	"fmt"
	"os"
)

func main() {
	in := bufio.NewReader(os.Stdin)
	out := bufio.NewWriter(os.Stdout)
	defer out.Flush()

	var n int
	fmt.Fscan(in, &n)

	// TODO: Создайте слайс values строго через make([]int, 0, n), чтобы len был 0, а cap был n.
	values := make([]int, 0, n)

	sum := 0
	for i := 0; i < n; i++ {
		var x int
		fmt.Fscan(in, &x)

		// TODO: Добавьте x в values только через append (без индексной записи) и посчитайте сумму.
		values = append(values, x)
	}

	// TODO: Выведите в первой строке все числа из values в исходном порядке через пробел.
	fmt.Fprintln(out, values...)
	fmt.Fprintf(out, "len=%d cap=%d\n", len(values), cap(values))
	fmt.Fprintln(out, sum)
}