package main

/*
= Задача №17. Планировщик задач =
Вы делаете консольный планировщик: пользователь говорит, сколько задач он собирается записать, а потом диктует названия задач по одному слову. Чтобы программа работала бодро, вы заранее резервируете место под все задачи, но начинаете с пустой длины — чтобы в списке не появлялись «пустые строки-призраки».
На вход подаётся целое число n, затем n слов (без пробелов). Соберите эти слова в слайс строк tasks, добавляя элементы через append в цикле. Слайс нужно создать как пустой по длине, но с заранее зарезервированной ёмкостью под n элементов: make([]string, 0, n).
После чтения выведите две строки: в первой — текущие len и cap получившегося слайса в формате len=<число> cap=<число>, во второй — нумерованный список задач в формате 1) word. Если n равно 0, вторая строка должна быть пустой.
Требования:
• Программа должна читать из stdin целое число n, затем n слов (строк без пробелов) с использованием fmt.Fscan (допускается чтение через bufio.NewReader).
• Слайс задач tasks должен быть создан как make([]string, 0, n), то есть с длиной 0 и ёмкостью n (нельзя создавать через make([]string, n)).
• Программа должна в цикле прочитать ровно n слов и добавлять каждое в tasks только операцией tasks = append(tasks, value).
• После чтения программа должна вывести первую строку строго в формате len=<число> cap=<число>, где числа соответствуют текущим len(tasks) и cap(tasks).
• Программа должна вывести вторую строку (или последовательность строк, начиная со второй) с задачами в формате 1) word, 2) word, ...; нумерация должна начинаться с 1, вывод должен быть реализован через for или for range.
*/

import (
	"bufio"
	"fmt"
	"os"
)

func main() {
	in := bufio.NewReader(os.Stdin)

	var n int
	fmt.Fscan(in, &n)

    // TODO: Инициализируйте tasks как пустой по длине слайс с заранее зарезервированной ёмкостью под n.
	tasks := make([]string, 0, n)

	for i := 0; i < n; i++ {
		var word string
		fmt.Fscan(in, &word)

		// TODO: Добавьте считанное слово word в слайс tasks, увеличивая длину только через append.
		tasks = append(tasks, word)
	}

	fmt.Printf("len=%d cap=%d\n", len(tasks), cap(tasks))

	if n == 0 {
		fmt.Print("\n")
		return
	}

	for i, task := range tasks {
		// TODO: Выведите задачи построчно с нумерацией с 1 в формате "<номер>) <слово>".
		fmt.Printf("%d) %s\n", i + 1, task)
	}
}