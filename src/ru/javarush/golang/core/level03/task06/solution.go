package main

/*
= Задача №6. Касса на входе =
Вы делаете маленькую кассу для события: вам дают цену билета и сумму, которую внесли. Кассиру нужно сразу увидеть —
не хватает денег или пора выдавать сдачу, причём сумма в ответе должна быть положительной и понятной.
Программа читает два целых числа: price и paid.
Если paid меньше price, программа выводит строку вида: NEED X где X = price - paid.
Иначе программа выводит строку вида: CHANGE X где X = paid - price.
Требования:
• Программа должна прочитать два целых числа через fmt.Scan в переменные с именами price и paid.
• Программа должна использовать конструкцию if/else (без switch), при этом должна выполниться ровно одна из веток в зависимости от сравнения paid и price.
• Фигурные скобки у обеих веток обязательны, а else должен быть расположен на одной строке с закрывающей скобкой if-ветки в виде `} else {`.
• В каждой ветке if/else должна вычисляться разница в отдельную переменную (например, diff): при paid < price diff = price - paid, иначе diff = paid - price.
*/

import "fmt"

func main() {
	var price, paid int
	fmt.Scan(&price, &paid)

	if paid < price {
	    // TODO: Вычислите, сколько денег не хватает (неотрицательная разница) и сохраните в diff.
		diff := price - paid
		fmt.Printf("NEED %d", diff)
	} else {
	    // TODO: Вычислите, какую сдачу нужно выдать (неотрицательная разница) и сохраните в diff.
		diff := paid - price
		fmt.Printf("CHANGE %d", diff)
	}
}