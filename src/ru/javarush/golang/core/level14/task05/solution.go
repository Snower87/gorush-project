package main

/*
= Задача №5. Двойная длина =
Вы пишете "паспорт никнейма" для чата: один и тот же ник может выглядеть коротким, но занимать много места в UTF‑8. Для отчёта вам нужно показать две длины: сколько байт реально ушло в сеть и сколько Unicode‑символов видит пользователь.
Программа читает из stdin одну строку nickname (одним токеном, без пробелов). Затем выводит две длины через пробел: сначала количество байт (len(nickname)), затем количество рун (Unicode‑символов), посчитанное обходом for range по строке.
Требования:
• Программа должна прочитать из stdin одну строку nickname без пробелов, используя fmt.Scan (один токен).
• Количество байт в nickname должно вычисляться только выражением len(nickname), без альтернативных способов.
• Количество Unicode-символов (рун) должно считаться только обходом строки циклом for range по nickname (например, увеличением счётчика на каждой итерации).
• В решении не должно использоваться преобразование строки в []rune (ни напрямую, ни через промежуточные переменные).
• В решении не должны использоваться пакеты unicode/utf8, strings, bytes (и их функции) для подсчёта длины или обработки строки.
*/

import "fmt"

func main() {
	var nickname string
	fmt.Scan(&nickname) // один токен, без пробелов

	bytesLen := len(nickname) // длина в байтах (как уйдёт по сети)

	runesLen := 0
	for range nickname { // range по строке идёт по рунам (Unicode-символам)
		// TODO: Увеличьте счётчик, чтобы посчитать количество рун (Unicode-символов) обходом строки через for range.
		runesLen++
	}

	fmt.Printf("%d %d\n", bytesLen, runesLen)
}