package main

/*
= Задача №1. Витрина задач =
Представьте, что у вас есть маленькая витрина с уже подготовленным набором чисел — как будто это “заготовленные карточки” для тренировки: s := []int{3, 1, 4, 1, 5, 9}. Пользователь просит показать первые n карточек, чтобы быстро взглянуть на начало набора.
Программа должна считать из stdin одно целое число n и вывести первые n элементов слайса, используя нарезку s[:n]. В Go границы среза можно опускать, поэтому важно именно аккуратно воспользоваться срезом, а не перебирать элементы вручную.
Если пользователь попросил невозможное (например, n < 0 или n > len(s)), программа должна спокойно вывести пустой слайс [] и завершиться без паники — витрина просто “пустая”, ничего страшного.
Требования:
• В программе должен быть использован ровно один заранее заданный слайс s со значением []int{3, 1, 4, 1, 5, 9}.
• Программа должна считать одно целое число n из stdin, используя fmt.Scan.
• Программа должна проверять, что n находится в диапазоне от 0 до len(s) включительно; при любом вводе (включая n < 0 и n > len(s)) программа должна завершаться без panic.
• Если n корректное, программа должна получать первые n элементов строго с помощью нарезки s[:n] (без циклов и без обращений к элементам через s[i]).
• Если n некорректное, программа должна вывести пустой слайс как [] с помощью fmt.Println([]int{}).
*/

import "fmt"

func main() {
	// Ровно один заданный исходный слайс по условию.
	s := []int{3, 1, 4, 1, 5, 9}

	var n int
	fmt.Scan(&n)

	// TODO: Проверьте, что n находится в диапазоне [0..len(s)] и программа не падает при любом вводе.
	// TODO: Если n некорректное (n < 0 или n > len(s)) — выведите пустой слайс [] через fmt.Println([]int{}).
	// TODO: Если n корректное — выведите первые n элементов строго через нарезку s[:n] (без циклов и без s[i]).
	if n < 0 || n > len(s) {
	    fmt.Println([]int{})
	    return
	}
    fmt.Println(s[:n])
}