package main

/*
= Задача №19. Чемпион и место =
Вы ведёте таблицу результатов забега: участники финишируют, и вы получаете их очки по одному. Нужно найти максимальный результат и запомнить, на какой позиции он встретился впервые — чтобы честно определить чемпиона, если у кого-то одинаковый максимум.
Программа читает целое число participantsCount и затем participantsCount целых чисел. Нужно найти максимальное значение и позицию (номер по порядку) первого вхождения этого максимума. Позиция нумеруется с 1: первое введённое число имеет позицию 1, второе — 2 и т.д.
Программа должна вывести два числа, каждое на новой строке: сначала максимальное значение, затем позицию первого вхождения максимума. Если participantsCount меньше либо равно 0, программа должна вывести 0 на первой строке и -1 на второй строке.
Требования:
• Программа должна считать с помощью fmt.Scan целое число participantsCount и затем ровно participantsCount целых чисел (если participantsCount > 0).
• Если participantsCount меньше либо равно 0, программа должна вывести 0 на первой строке и -1 на второй строке и завершиться через return, не пытаясь читать результаты участников.
• При participantsCount > 0 максимум должен инициализироваться первым считанным числом (а не 0 или другим “значением по умолчанию”), и начальная позиция максимума должна быть установлена в 1.
• Программа должна находить максимальное значение среди введённых чисел и позицию (нумерация с 1) первого появления этого максимума; при равенстве текущему максимуму позиция не должна обновляться.
• Когда обнаруживается новое значение строго больше текущего максимума, программа должна обновлять и максимум, и переменную позиции максимума (например, maxPosition) на текущий номер элемента.
*/

import "fmt"

func main() {
	var participantsCount int
	fmt.Scan(&participantsCount)

	// Ранний выход: если участников нет, не читаем результаты.
	if participantsCount <= 0 {
		fmt.Println(0)
		fmt.Println(-1)
		return
	}

	// Считываем первое значение и используем его как стартовое.
	var maxValue int
	fmt.Scan(&maxValue)
	maxPos := 1

	// Читаем оставшиеся значения в цикле, без хранения всей последовательности.
	for i := 2; i <= participantsCount; i++ {
		var v int
		fmt.Scan(&v)

		if v > maxValue {
			// TODO: Исправьте обновление максимума и позиции так, чтобы позиция соответствовала первому вхождению максимального значения.
			maxValue = v
			maxPos = i
		}
	}

	fmt.Println(maxValue)
	fmt.Println(maxPos)
}