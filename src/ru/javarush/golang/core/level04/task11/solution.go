	var slug string
	fmt.Scan(&slug)

	pos := -1

	// TODO: Найдите индекс первого символа '-' в строке slug.
	// TODO: Обход должен быть через: for i, v := range slug
	// TODO: При первом совпадении (v == '-') сохраните индекс i в pos и сразу выйдите из цикла через break.
