package main

/*
= Задача №18. Новый массив =
Иногда append ведёт себя “бережно” к исходному слайсу — но не из доброты, а потому что у под‑слайса больше нет свободной ёмкости. Тогда Go вынужден выделить новый backing array, и изменения уходят в новый буфер, не затрагивая старый.
Программа должна создать целочисленный слайс base длины 2 и ёмкости 2 через make, записать в него значения 10 и 20, затем сделать под‑слайс sub как base[:] (полный срез). После этого программа должна выполнить sub = append(sub, 30).
Программа должна вывести 3 строки:
- base(before): len=2 cap=2 [10 20]
- base(after): len=2 cap=2 [10 20]
- sub(after): [10 20 30]
Требования:
*/

import "fmt"

func main() {
	// Базовый слайс: len=2 cap=2 — места для роста нет.
	base := make([]int, 2, 2)

	// TODO: Запишите в base значения 10 и 20 по индексам 0 и 1.
	base[0] = 10
	base[1] = 20

	// TODO: Создайте sub как полный срез base[:] до append.
	// Сейчас срез намеренно сделан не полным, чтобы поведение append отличалось от требуемого.
	sub := base[:]

	// TODO: Выведите строку base(before) в точном формате из условия.
	fmt.Printf("base(before): len=%d cap=%d %v\n", len(base), cap(base), base)

	// TODO: Выполните расширение под-слайса строго операцией sub = append(sub, 30).
	sub = append(sub, 30)

	// TODO: Выведите строку base(after) в точном формате из условия.
	fmt.Printf("base(after): len=%d cap=%d %v\n", len(base), cap(base), base)

	// TODO: Выведите строку sub(after) в точном формате из условия (без len/cap для sub).
	fmt.Printf("sub(after): %v\n", sub)
}