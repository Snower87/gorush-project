package main

/*
= Задача №18. Правильный диапазон =
Вы делаете настройку “коридора значений” для фильтра: пользователь вводит две границы — from и to. Но люди часто путают порядок и вводят сначала верхнюю, потом нижнюю. Ваш фильтр должен быть дружелюбным: привести границы в порядок так, чтобы всегда было from <= to.
Программа должна считать из stdin два целых числа from и to. Если from больше to, поменяйте их местами (через множественное присваивание). Затем выведите одну строку: range: <from>..<to>
Например, для входа 10 3 программа должна вывести range: 3..10.
Требования:
• Программа должна прочитать из stdin два целых числа в переменные from и to с помощью fmt.Scan(&from, &to).
• Программа должна гарантировать, что после обработки выполняется from <= to; если from <= to, значения должны остаться без изменений.
• Если from > to, программа должна поменять значения from и to местами с использованием множественного присваивания (from, to = to, from).
• Внутри if при обмене значений должен использоваться оператор присваивания =, а не короткая декларация :=, чтобы обновлялись уже существующие переменные from и to.
• Программа должна вывести ровно одну строку в stdout в формате range: %d..%d\n (без дополнительных сообщений и пояснений).
*/

import "fmt"

func main() {
	var from, to int
	fmt.Scan(&from, &to)

	if from > to {
		// TODO: Если границы введены в обратном порядке, поменяйте значения from и to местами
		// TODO: Используйте множественное присваивание и оператор "=", чтобы не создать новые переменные (без shadowing)
		from, to = to, from
	}

	fmt.Printf("range: %d..%d\n", from, to)
}