package main

/*
= Задача №18. Чаевые в кафе =
Вы пишете мини‑программу для кафе: кассир вводит сумму счёта и процент чаевых, а программа печатает две строки — сколько оставить чаевых и сколько получится всего. Важно, чтобы результат выглядел “по‑денежному”: всегда два знака после точки.
Программа читает billAmount (тип float64) и tipPercent (тип int). Затем считает tip = billAmount * float64(tipPercent) / 100 и total = billAmount + tip. После этого печатает две строки: Tip: и Total: с форматированием %.2f.
Требования:
• Программа должна считать из stdin два значения с помощью fmt.Scan: billAmount и tipPercent.
• billAmount должен храниться в переменной типа float64, а tipPercent — в переменной типа int.
• Программа должна вычислить tip = billAmount * float64(tipPercent) / 100 и total = billAmount + tip, явно приводя tipPercent к float64 перед вычислениями.
• Программа должна печатать значения tip и total с форматированием ровно до двух знаков после десятичной точки, используя fmt.Printf и формат %.2f.
• Программа должна вывести ровно две строки, первая начинается с Tip:, вторая — с Total:, и каждая строка должна завершаться символом новой строки.
• Программа не должна использовать преобразование к int (например, int(x)) как способ округления или форматирования результата.
*/

import "fmt"

func main() {
	// Входные данные: сумма счёта и процент чаевых (типы фиксированы требованием)
	var billAmount float64
	var tipPercent int
	fmt.Scan(&billAmount, &tipPercent)

	// TODO: Рассчитайте сумму чаевых (tip) на основе суммы счёта и процента чаевых.
	tip := billAmount * float64(tipPercent) / 100

	// TODO: Рассчитайте итоговую сумму (total) с учётом чаевых.
	total := billAmount + tip

	// Денежный вывод: всегда ровно 2 знака после точки
	fmt.Printf("Tip: %.2f\n", tip)
	fmt.Printf("Total: %.2f\n", total)
}