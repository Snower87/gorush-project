/*
= Задача №19. Рекорд и индекс =
Вы пишете обработчик результатов забега: на вход приходит число n (сколько замеров было), затем идут n целых значений. Нужно найти рекорд (максимум) и позицию, на которой рекорд впервые появился — чтобы награду получил “первый установивший рекорд”, даже если потом кто-то повторил результат.
Позиция считается с нуля: первый замер имеет индекс 0. Если максимум встречается несколько раз, выводится индекс первого появления максимума. Если n <= 0, программа должна вывести 0 -1 в одной строке.
Формат вывода: два числа через пробел — максимальное значение и индекс.
Требования:
• Программа должна быть оформлена как package main и содержать func main(), используя fmt.Scan для чтения входных данных.
• Если введённое n меньше либо равно 0, программа должна вывести в одной строке "0 -1" и не пытаться читать последующие значения.
• При n > 0 программа должна считать первый замер, установить его как текущий максимум и установить индекс максимума в 0.
• Оставшиеся n-1 значений должны считываться в цикле, начиная со второго элемента; максимум и индекс обновляются только если новое значение строго больше текущего максимума (x > currentMax), чтобы при равных максимумах сохранялся индекс первого появления.
• Программа должна вывести ровно одну строку, содержащую два целых числа через пробел: максимальное значение и индекс его первого появления (индексация с нуля).
*/

import "fmt"

func main() {
	var n int
	fmt.Scan(&n)

	// Если n <= 0, сразу печатаем ответ и не читаем замеры.
	if n <= 0 {
		fmt.Print("0 -1")
		return
	}

	var max int
	fmt.Scan(&max)
	maxIdx := 0

	// Читаем оставшиеся значения "на лету", без слайсов/массивов.
	for i := 1; i < n; i++ {
		var x int
		fmt.Scan(&x)

		// TODO: Реализуйте обновление рекорда и индекса его первого появления.
		// TODO: Индекс обновляйте только если найдено значение СТРОГО больше текущего максимума.
		// TODO: Хранить все замеры в слайсе/массиве нельзя — обрабатывайте значения при чтении.
		if x > max { // строго больше, чтобы при равных максимумах сохранялся только первый!!!
		    max = x
		    maxIdx = i
		}
	}

	fmt.Printf("%d %d\n", max, maxIdx)
}