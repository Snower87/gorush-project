package main

/*
= Задача №1. Снимок списка =
Вы пишете маленькую утилиту для кассира: список src — это товары в текущем чеке (просто числа), а “снимок” нужен, чтобы попробовать применить тестовую акцию и не испортить исходные данные.
Программа читает N, затем N целых чисел в слайс src. После этого создайте новый слайс snapshot той же длины, скопируйте туда все элементы и (если список не пустой) поменяйте только первый элемент снимка: snapshot[0] должен стать -1. Важно: src после этого обязан остаться таким же, как был прочитан.
В конце программа печатает две строки: сначала исходный src, затем snapshot. Числа выводятся через пробел. Если N = 0, обе строки должны быть пустыми.
Требования:
• Программа должна прочитать целое N, затем прочитать N целых чисел и сохранить их в слайс src типа []int в исходном порядке.
• Программа должна создать слайс snapshot длины len(src) с помощью make([]int, len(src)), чтобы snapshot имел собственный backing array и не разделял память с src.
• Программа должна скопировать все элементы из src в snapshot с помощью встроенной функции copy(snapshot, src).
• Программа не должна использовать присваивание вида snapshot := src (или эквивалентное), которое создаёт только новый заголовок слайса и приводит к общему underlying array вместо независимой копии.
• Если N > 0, программа должна изменить только первый элемент snapshot (snapshot[0] = -1), при этом src должен остаться полностью неизменным (включая src[0]).
*/

import (
	"bufio"
	"fmt"
	"os"
)

func printSlice(w *bufio.Writer, s []int) {
	for i, v := range s {
		if i > 0 {
			fmt.Fprint(w, " ")
		}
		fmt.Fprint(w, v)
	}
	fmt.Fprintln(w)
}

func main() {
	in := bufio.NewReader(os.Stdin)
	out := bufio.NewWriter(os.Stdout)
	defer out.Flush()

	var n int
	fmt.Fscan(in, &n)

	src := make([]int, n)
	for i := 0; i < n; i++ {
		fmt.Fscan(in, &src[i])
	}

	snapshot := make([]int, len(src))

	// TODO: Скопируйте все элементы из src в snapshot так, чтобы snapshot был независимой копией (без общего backing array с src).
	// TODO: Не используйте присваивание вида snapshot := src (или эквивалент), потому что это создаёт aliasing.
	copy(snapshot, src)

	if len(snapshot) > 0 {
		snapshot[0] = -1
	}

	printSlice(out, src)
	printSlice(out, snapshot)
}