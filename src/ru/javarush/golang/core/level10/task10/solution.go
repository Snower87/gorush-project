package main

/*
= Задача №10. Бирка задачи =
Вы делаете маленький генератор "бирок" для задач в трекере. Пользователь вводит заголовок (одно слово), а программа печатает две вещи: псевдо‑ID (это длина заголовка) и сам заголовок, но предварительно нормализованный через strings.TrimSpace (да, даже у одного слова иногда бывают пробелы по краям — такое случается при копипасте).
ID должен стать строкой через strconv.Itoa, а итоговый вывод должен быть одной строкой в формате id: <ID> title: <TITLE>.
Требования:
• Программа должна импортировать ровно пакеты fmt, strconv и strings, и каждый из них должен быть реально использован в коде.
• Импорты должны быть оформлены в виде import ( ... ), при этом строки импортов внутри блока должны быть отсортированы в алфавитном порядке.
• Программа должна считать из stdin заголовок задачи (одно слово) как строку.
• Перед вычислениями и выводом заголовок должен быть нормализован строго через strings.TrimSpace, чтобы удалить пробелы по краям.
• Программа должна сформировать ID как строку строго через strconv.Itoa(len(<нормализованный_заголовок>)) и вывести одну строку в точном формате: id: <ID> title: <TITLE>.
*/

import (
	"fmt"
	"strconv"
	"strings"
)

func main() {
	var rawTitle string

	// TODO: Считайте заголовок задачи из stdin так, чтобы не потерять пробелы по краям (нужно прочитать всю строку).
	var first byte
	var rest string
	fmt.Scanf("%c%[^\n]", &first, &rest)
	rawTitle := string(first) + rest

	title := strings.TrimSpace(rawTitle)

	// TODO: ID должен быть строкой и вычисляться от длины нормализованного заголовка.
	id := strconv.Itoa(len(title))

	// TODO: Выведите результат в точном формате: id: <ID> title: <TITLE> (без лишних символов).
	fmt.Printf("id: %s title: %s\n", id, title)
}