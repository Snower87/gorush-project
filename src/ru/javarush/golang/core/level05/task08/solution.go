package main

/*
= Задача №8. Командный терминал =
Требования:
• Программа должна читать из stdin последовательность троек целых чисел (cmd, a, b) и обрабатывать их по мере чтения.
• Обработка должна выполняться внутри бесконечного цикла for { ... }, при cmd == 0 программа должна выйти из цикла через break и больше ничего не печатать.
• Если чтение очередной тройки завершается ошибкой, программа должна корректно прекратить цикл и завершить работу без паники и без дополнительных сообщений.
• Если cmd не входит в диапазон 0..4, соответствующая тройка должна быть пропущена без вывода результата (через continue).
• Для cmd = 1..4 программа должна вычислять результат над int с использованием операций +, -, *, / (для cmd=4 — целочисленное деление) и печатать результат, каждый — в отдельной строке.
• Если cmd == 4 и b == 0, операция деления должна быть пропущена без вывода результата (через continue).
*/

import (
	"bufio"
	"fmt"
	"os"
)

func main() {
	in := bufio.NewReader(os.Stdin)
	out := bufio.NewWriter(os.Stdout)
	defer out.Flush()

	for { // по условию: обработка в бесконечном цикле
		var cmd, a, b int
		if _, err := fmt.Fscan(in, &cmd, &a, &b); err != nil {
			break // EOF/ошибка ввода — корректно завершаемся
		}

		if cmd == 0 {
			break // команда завершения
		}

		// TODO: реализуйте игнорирование неверных команд (cmd вне диапазона 0..4) через continue
		if cmd < 0 || cmd > 4 {
		    continue
		}
		// TODO: реализуйте пропуск деления на ноль (cmd == 4 и b == 0) через continue без вывода
		if cmd == 4 && b == 0 {
		    continue
		}
		// TODO: реализуйте выполнение арифметических команд 1..4 и построчный вывод результата в stdout
		switch cmd {
		    case 1:
		        fmt.Fprintln(out, a + b)
		    case 2:
		        fmt.Fprintln(out, a - b)
		    case 3:
		        fmt.Fprintln(out, a * b)
            case 4:
                fmt.Fprintln(out, a / b)
		}
		// Временное поведение: корректно читаем и завершаемся по cmd==0/EOF,
		// но команды пока не обрабатываем.
		if cmd == 0 {
		    break
		}
		//continue
	}
}