package main

/*
= Задача №18. Математика без шума =
Вы делаете маленький модуль вычислений для проекта: одна часть команды отвечает за ввод/вывод, другая — за “чистую математику”. Чтобы не тащить зависимости куда не нужно, файл с вычислениями должен быть максимально лёгким: без fmt и вообще без секции import.
Соберите программу из двух файлов в одном package main: main.go и ops.go. В ops.go реализуйте две функции Sum(a, b int) int и Product(a, b int) int, которые просто считают и возвращают результат. В main.go прочитайте два целых числа и выведите две строки: sum=<...> и product=<...>.
Требования:
• Проект должен состоять ровно из двух файлов: main.go и ops.go, и оба файла должны объявлять package main.
• В файле ops.go не должно быть секции import (никаких импортов вообще).
• В ops.go должны быть реализованы функции Sum(a, b int) int и Product(a, b int) int, которые только вычисляют результат и возвращают его; функции не должны выполнять ввод/вывод (не использовать fmt.Print* и не читать stdin).
• В main.go программа должна прочитать два целых числа из stdin с помощью fmt.Scan.
• В main.go программа должна вывести две строки (по одной на строку) в точном формате: sum=<значение> и product=<значение>, используя fmt.Printf или fmt.Println.
*/

import "fmt"

func main() {
	var a, b int
	fmt.Scan(&a, &b)

	// Вывод строго в заданном формате: две строки.
	fmt.Printf("sum=%d\n", Sum(a, b))
	fmt.Printf("product=%d\n", Product(a, b))
}