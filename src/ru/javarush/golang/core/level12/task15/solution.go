package main

/*
= Задача №15. Окно и снимок =
Вы анализируете большой список чисел s и хотите выделить диапазон [a, b) — как “окно просмотра”. Но параллельно вам нужен “снимок” этого окна, чтобы можно было его менять, не трогая оригинал. В реальной жизни это похоже на: “посмотрели кусок таблицы” vs “сделали копию этого куска в новый документ”.
Программа должна прочитать N, затем N целых чисел (слайс s), затем два индекса a и b (гарантируется 0 <= a <= b <= N).
Нужно создать view := s[a:b] (это окно на исходные данные) и snapshot — независимую копию элементов view через make + copy.
Далее, если len(view) > 0, нужно увеличить view[0] на 1. Если len(snapshot) > 0, установить snapshot[0] в -1000. Затем вывести три строки: s, view, snapshot (каждый слайс через fmt.Println).
Требования:
• Программа должна прочитать N, затем N целых чисел в слайс s, затем индексы a и b, причём предполагается, что 0 <= a <= b <= N.
• Программа должна создать слайс view строго как view := s[a:b], то есть как под-слайс, разделяющий underlying array с исходным s.
• Программа должна создать snapshot как новый слайс длины len(view) через make, а затем скопировать в него элементы view с помощью copy, чтобы snapshot не разделял память с s/view.
• Если len(view) > 0, программа должна увеличить view[0] на 1; если len(snapshot) > 0, программа должна установить snapshot[0] в -1000; при нулевой длине соответствующего слайса доступ к индексу 0 выполняться не должен.
• Изменение view[0] (если оно выполнялось) должно отражаться в исходном слайсе s, а изменение snapshot[0] (если оно выполнялось) не должно отражаться в s.
*/

import (
	"bufio"
	"fmt"
	"os"
)

func main() {
	in := bufio.NewReader(os.Stdin)

	var n int
	fmt.Fscan(in, &n)

	s := make([]int, n)
	for i := 0; i < n; i++ {
		fmt.Fscan(in, &s[i])
	}

	var a, b int
	fmt.Fscan(in, &a, &b)

	var view []int
	var snapshot []int

	// TODO: Создайте view как под-слайс s[a:b] без копирования (общий underlying array).
	// TODO: Создайте snapshot как независимую копию элементов view через make+copy.
	view = s[a:b]
	snapshot = make([]int, len(view))
	copy(snapshot, view)

	// TODO: Если len(view) > 0 — увеличьте view[0] на 1 (не обращайтесь к [0], если view пустой).
	// TODO: Если len(snapshot) > 0 — установите snapshot[0] в -1000 (не обращайтесь к [0], если snapshot пустой).
	if len(view) > 0 {
	    view[0] = view[0] + 1
	}
    if len(snapshot) > 0 {
        snapshot[0] = -1000
    }

	fmt.Println(s)
	fmt.Println(view)
	fmt.Println(snapshot)
}