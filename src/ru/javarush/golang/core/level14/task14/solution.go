package main

/*
= Задача №14. Палитра цветов =
Вы пишете мини‑настройку темы интерфейса: пользователь вводит палитру одним словом, где цвета перечислены через двоеточие, например red:green:blue. Вам нужно разложить эту строку на части и вывести их аккуратно и с нумерацией — как в настоящем меню настроек.
Программа читает из stdin одну строку без пробелов, разбивает её через strings.Split по разделителю ":", затем выводит количество частей, а после — каждую часть с новой строки в формате "i) значение", где i начинается с 1. Если разделитель не встречается, это всё равно один элемент — исходная строка.
Требования:
• Программа должна прочитать из stdin одну строку без пробелов с помощью fmt.Scan.
• Программа должна разбить введённую строку на части вызовом strings.Split с разделителем ":" (двоеточие), не выполняя дополнительной обработки, чтобы пустые элементы (между двумя ":" или на краях) сохранялись.
• Программа должна вывести в stdout количество получившихся частей (len результата strings.Split) перед выводом самих элементов.
• Программа должна вывести каждую часть с новой строки в формате "i) значение", где i начинается с 1.
• Для вывода списка программа должна использовать цикл for по индексам (например, for i := 0; i < len(parts); i++), а не range по значениям.
*/

import (
	"fmt"
	"strings"
)

func main() {
	var palette string
	fmt.Scan(&palette)

	// TODO: Разбейте строку palette на части через strings.Split с разделителем ":" (пустые элементы должны сохраняться).
	parts := strings.Split(palette, ":")

	// TODO: Выведите количество элементов (len(parts)) перед выводом самих элементов.
	fmt.Println(len(parts))

	// По требованию — цикл именно по индексам
	for i := 0; i < len(parts); i++ {
		// TODO: Выведите каждый элемент с новой строки в формате "i) значение", где i начинается с 1.
		fmt.Printf("%d) %s\n", i+1, parts[i])
	}
}