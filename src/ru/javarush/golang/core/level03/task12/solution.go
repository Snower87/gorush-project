package main

/*
= Задача №12. Проверка деления =
Вы пишете маленький “санитайзер” для расчётов: система получает a, делитель denom и порог limit. Нужно проверить сразу
два условия: делитель не ноль, и результат целочисленного деления меньше порога. Но главное — сделать это безопасно:
деление должно выполняться только тогда, когда делить можно.
Программа читает три целых числа: a, denom и limit. Затем формирует единое условие так, чтобы a/denom вычислялось
только при denom != 0, используя короткое вычисление через &&:
denom != 0 && (a/denom < limit)
Если условие истинно — вывести OK, иначе — FAIL.
Требования:
• Программа должна считать через fmt.Scan три значения типа int: a, denom и limit.
• Программа должна использовать условие строго в виде: denom != 0 && (a/denom < limit), где скобки вокруг (a/denom < limit) обязательны.
• Программа должна применять оператор && так, чтобы выражение a/denom никогда не вычислялось при denom == 0, и деление на ноль было невозможно при любых входных данных.
• Программа должна использовать конструкцию if/else для вывода результата в зависимости от истинности условия.
*/

import "fmt"

func main() {
	var a, denom, limit int
	fmt.Scan(&a, &denom, &limit)

	// TODO: Сформируйте единое логическое условие с && так, чтобы деление выполнялось только при denom != 0,
	// TODO: а результат целочисленного деления сравнивался с limit (с обязательными скобками вокруг сравнения).
	if denom != 0 && (a/denom < limit) {
		fmt.Print("OK")
	} else {
		fmt.Print("FAIL")
	}
}