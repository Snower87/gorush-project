package main

/*
= Задача №4. Охрана простоты =
Вы пишете «охранника» для учебного сервиса: пользователи отправляют числа, а система должна быстро отвечать, "простое" число или нет. Это маленькая проверка, но она важна: по ней будут гонять тесты, и формат ответа должен быть строго YES или NO.
Программа читает одно целое число candidateNumber. Реализуйте функцию-предикат isPrime(n int) bool: она возвращает true, если число простое, и false иначе. Числа n <= 1 (и отрицательные тоже) считаются не простыми. Делители проверяйте циклом for, а границу берите как i*i <= n (без math.Sqrt). В зависимости от результата печатайте ровно одно слово: YES или NO.
Требования:
• В программе должна быть реализована функция isPrime(n int) bool, которая возвращает true для простых чисел и false иначе, при этом сама функция ничего не печатает.
• Функция isPrime должна возвращать false для любых n <= 1, включая 0 и отрицательные числа.
• В isPrime проверка делителей должна выполняться циклом for, а верхняя граница должна быть реализована условием i*i <= n (без использования math.Sqrt и без подключения math).
• Функция main должна читать одно целое число n (candidateNumber) через fmt.Scan.
• После вычисления результата через isPrime программа должна вывести ровно одно слово: YES (если число простое) или NO (если не простое), без лишних пробелов, пустых строк и отладочного вывода.
*/

import "fmt"

// isPrime возвращает true, если n — простое число.
func isPrime(n int) bool {
	// По условию все n <= 1 (включая отрицательные) — не простые.
	if n <= 1 {
		return false
	}

	// TODO: Реализуйте проверку простоты числа по условию задачи (поиск делителей циклом for до i*i <= n).
	for i := 2; i * i <= n; i++ {
		if n % i == 0 {
			return false
		}
	}

	// Временная реализация: считает все числа > 1 простыми (это намеренно неверно).
	return true
}

func main() {
	var candidateNumber int
	fmt.Scan(&candidateNumber)

	if isPrime(candidateNumber) {
		fmt.Print("YES")
	} else {
		fmt.Print("NO")
	}
}