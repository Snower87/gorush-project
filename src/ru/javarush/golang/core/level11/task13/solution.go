package main

/*
= Задача №13. Лента датчиков =
Представьте, что у вас есть лента показаний датчика, которая идёт потоком: вы читаете числа одно за другим, пока не встретите 0 — это специальный сигнал «конец передачи». Всё, что было до нуля, нужно аккуратно сохранить, чтобы потом вывести отчёт.
Программа читает из stdin целые числа до тех пор, пока не встретит 0. Все числа, кроме завершающего нуля, сохраняются в слайс readings типа []int. Важно начать именно с nil-слайса и увеличивать его только через append, каждый раз сохраняя результат.
После окончания ввода программа выводит все сохранённые числа в исходном порядке в одну строку через пробел. Если первым же числом был 0, выводится пустая строка.
Требования:
• Программа должна считывать из stdin целые числа с помощью fmt.Scan в цикле и завершать ввод при встрече значения 0.
• Слайс для накопления данных должен быть объявлен именно как var readings []int (то есть nil-слайс), без make и без заранее заданной длины.
• Каждое считанное число (кроме завершающего 0) должно добавляться в readings только выражением вида readings = append(readings, x); нельзя забывать присваивание результата append обратно в readings.
• Значение 0, которое сигнализирует окончание ввода, не должно добавляться в readings.
• После завершения ввода программа должна вывести все элементы readings в порядке добавления, в одну строку, разделяя числа пробелами; если readings пустой (первое число было 0), должен выводиться пустой вывод (пустая строка).
*/

import "fmt"

func main() {
	var readings []int // важно: это nil-слайс, не используем make

	for {
		var x int
		fmt.Scan(&x) // читаем поток целых чисел до завершающего 0

		if x == 0 {
			break // 0 — сигнал конца, его не сохраняем
		}

		// TODO: Добавьте считанное число x в слайс readings через append с сохранением результата.
		readings = append(readings, x)
	}

	// TODO: Выведите все значения из readings в исходном порядке в одну строку через пробел.
	// TODO: Убедитесь, что при пустом readings (например, если первым числом был 0) выводится пустая строка.

    if len(readings) == 0 {
        fmt.Println()
    } else {
        fmt.Println(readings...)
    }
}