package main

/*
= Задача №5. Адрес и имя =
Вы объясняете новичку разницу между “адресом пакета” и “именем пакета в коде”. В Go пакет может импортироваться по пути math/rand, но обращаться к нему вы всё равно будете как к rand. Это похоже на ситуацию, когда у вас длинный адрес склада, а внутри команды его называют просто “Ранд”.
Ваша программа ничего не читает, а просто вычисляет rand.Intn(1) и печатает ровно одну строку — так удобно проверять, что всё собрано правильно: при Intn(1) результат всегда 0.
Требования:
• Программа должна быть в пакете main и содержать функцию func main() как единственную точку входа.
• Программа должна импортировать только пакеты fmt и math/rand, без любых других импортов.
• Внутри main() должен быть выполнен вызов rand.Intn(1) (именно через имя пакета rand), чтобы вычислить значение (которое при Intn(1) всегда равно 0).
• Программа должна вывести ровно одну строку, и эта строка должна посимвольно совпадать с ожидаемым образцом (включая все пробелы и перевод строки).
*/

import (
	"fmt"
	"math/rand"
)

func main() {
	// Импортируется по пути math/rand, но в коде обращаемся по имени пакета: rand
	n := rand.Intn(1)

	// TODO: Выведите значение n одной строкой так, чтобы вывод полностью совпал с ожидаемым (включая перевод строки).
	fmt.Printf("%d\n", n)
}