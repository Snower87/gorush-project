package main

/*
= Задача №14. Команда калькулятора =
Вы делаете консольный калькулятор для внутренних утилит. Пользователь вводит команду-операцию "add", "sub", "mul", "div" и два целых числа. Ваша цель — не построить // лесенку из if, а сразу отбрасывать ошибки ранними выходами, чтобы основной расчёт был прямым и читаемым.
Программа читает: op (строка), a и b (целые числа). op может быть только "add", "sub", "mul" или "div".
Если op не входит в список поддерживаемых команд, программа должна вывести "unknown op" и завершить работу. Если op равен "div" и b равен 0, программа должна вывести "division by zero" и завершить работу.
Для корректных входных данных программа должна вывести результат операции (целое число). Для "div" используется целочисленное деление (как для int).
Требования:
• Программа должна считать из stdin три значения через fmt.Scan(&op, &a, &b), где op — строка, a и b — целые числа.
• Если op не равен ни одной из поддерживаемых команд (add, sub, mul, div), программа должна вывести ровно unknown op и сразу завершить работу (печать + return), без дальнейших вычислений.
• Если op == "div" и b == 0, программа должна вывести ровно division by zero и сразу завершить работу (печать + return), без дальнейших вычислений.
• Для корректного op программа должна вычислить результат: add → a+b, sub → a-b, mul → a*b, div → a/b с целочисленным делением как для int.
• После выполнения всех guard clauses оставшаяся часть программы должна быть прямолинейной (без вложенной “лесенки” if), выполняя только вычисление результата и его вывод.
*/

import "fmt"

func main() {
	var op string
	var a, b int

	fmt.Scan(&op, &a, &b)

	// TODO: проверьте, что op — одна из команд: add, sub, mul, div.
	// TODO: если команда неизвестна, выведите "unknown op" и завершите работу (return) ДО вычислений.
	if op != "add" && op != "sub" && op != "mul" && op != "div" {
	    fmt.Println("unknown op")
	    return
	}

	// TODO: если op == "div" и b == 0, выведите "division by zero" и завершите работу (return) ДО вычислений.
	if op == "div" && b == 0 {
	    fmt.Println("division by zero")
	    return
	}

	var res int

	if op == "add" {
		res = a + b
	}
	// TODO: реализуйте остальные операции (sub, mul, div) через отдельные if (без switch и без циклов).
	if op == "sub" {
	    res = a - b
	}
	if op == "mul" {
	    res = a * b
	}
	if op == "div" {
	    res = a / b
	}

	fmt.Print(res)
}