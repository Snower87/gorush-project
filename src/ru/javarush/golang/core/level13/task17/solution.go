package main

/*
= Задача №17. Клон плейлиста =
Вы делаете консольный “плейлист” из чисел: nums — это оригинал, который нельзя портить, потому что он уже сохранён в профиле пользователя. Но для эксперимента вы хотите сделать копию, подкрутить её и сравнить, что получилось.
Программа читает из stdin целое число N, а затем N целых чисел в слайс nums. После чтения создайте независимую копию этого слайса с помощью slices.Clone и сохраните её в переменную copyNums. Если N > 0, измените первый элемент копии, прибавив к нему 1000. Оригинальный слайс nums при этом должен остаться без изменений.
Программа должна вывести две строки. В первой строке — элементы nums через пробел. Во второй строке — элементы copyNums через пробел. Если слайс пустой, строка должна быть пустой.
Требования:
• Программа должна прочитать из stdin целое число N, затем прочитать N целых чисел и сохранить их в слайс nums (через fmt.Scan или эквивалентно fmt.Fscan с os.Stdin).
• После чтения nums программа должна создать независимую копию слайса в переменную copyNums с использованием функции slices.Clone из пакета slices (без присваивания вида copyNums := nums как “копирования”).
• Если N > 0, программа должна увеличить первый элемент слайса copyNums на 1000; при этом исходный слайс nums должен остаться без изменений.
• При N = 0 программа должна корректно создать пустые nums и copyNums и не пытаться изменять первый элемент.
• Программа должна вывести ровно две строки: в первой — элементы nums через один пробел, во второй — элементы copyNums через один пробел; если слайс пустой, соответствующая строка должна быть пустой.
*/

import (
	"bufio"
	"fmt"
	"os"
	"slices"
)

func main() {
	in := bufio.NewReader(os.Stdin)
	out := bufio.NewWriter(os.Stdout)
	defer out.Flush()

	var n int
	fmt.Fscan(in, &n)

	nums := make([]int, n)
	for i := 0; i < n; i++ {
		fmt.Fscan(in, &nums[i])
	}

	// TODO: Сделайте независимую копию слайса nums (не alias).
	// TODO: Используйте slices.Clone (для этого нужно добавить импорт "slices").
	copyNums := slices.Clone(nums)

	// TODO: Убедитесь, что меняется только копия, а оригинальный nums остаётся без изменений.
	if n > 0 {
		copyNums[0] += 1000
	}

	writeSlice(out, nums)
	fmt.Fprintln(out)
	writeSlice(out, copyNums)
	fmt.Fprintln(out)
}

func writeSlice(w *bufio.Writer, s []int) {
	for i, v := range s {
		if i > 0 {
			fmt.Fprint(w, " ")
		}
		fmt.Fprint(w, v)
	}
}