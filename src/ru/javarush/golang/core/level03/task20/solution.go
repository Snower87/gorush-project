package main

/*
= Задача №20. Счёт с чаевыми =
Вы делаете консольную “быструю оплату” для кафе: кассир вводит сумму счёта и процент чаевых, но ввод приходит строками (иногда люди случайно вставляют лишние символы). Вам нужно различать три типа проблем:
не прочитали два значения,
не смогли превратить строку в число,
число получилось, но смысл неверный (например, отрицательная сумма или процент 150).
Программа читает из stdin две строки: billStr и tipStr. Первая — сумма счёта в целых единицах, вторая — процент чаевых в целых единицах.
Если ввод не прочитан (fmt.Scan вернул err != nil), программа должна вывести: ошибка ввода: нужно 2 значения и завершиться.
Далее программа должна преобразовать billStr в int. При ошибке вывести: сумма должна быть целым числом и завершиться. Аналогично для tipStr: при ошибке вывести: чаевые должны быть целым числом и завершиться.
После успешного парсинга должна быть логическая валидация:
bill не может быть отрицательной, иначе вывод: сумма не может быть отрицательной
tipPercent должен быть в диапазоне от 0 до 100 включительно, иначе вывод: процент чаевых должен быть от 0 до 100
Если все проверки пройдены, программа должна вычислить: total = bill + bill*tipPercent/100 и вывести: итого: <total>
Требования:
• Программа должна прочитать из stdin ровно два значения (billStr и tipStr) через fmt.Scan и, если Scan вернул err != nil, вывести "ошибка ввода: нужно 2 значения" и завершиться (return).
• Программа должна преобразовать billStr в int через strconv.Atoi и, если получена ошибка, вывести "сумма должна быть целым числом" и завершиться (return), используя паттерн "вызвал → проверил err → используй результат".
• Программа должна преобразовать tipStr в int через strconv.Atoi и, если получена ошибка, вывести "чаевые должны быть целым числом" и завершиться (return), используя паттерн "вызвал → проверил err → используй результат".
• После успешного парсинга программа должна отдельной проверкой (не через err) отклонять bill < 0, печатая "сумма не может быть отрицательной", и завершаться (return).
• После успешного парсинга программа должна отдельной проверкой (не через err) отклонять tipPercent вне диапазона 0..100 включительно, печатая "процент чаевых должен быть от 0 до 100", и завершаться (return).
*/

import (
	"fmt"
	"strconv"
)

func main() {
	var billStr, tipStr string

	// По условию: читаем ровно 2 значения через fmt.Scan и различаем ошибку чтения.
	if _, err := fmt.Scan(&billStr, &tipStr); err != nil {
		fmt.Println("ошибка ввода: нужно 2 значения")
		return
	}

	// TODO: Реализуйте парсинг billStr в int через strconv.Atoi с корректной обработкой ошибки:
	// TODO: при ошибке выведите "сумма должна быть целым числом" и завершите программу (return).
	bill, err := strconv.Atoi(billStr)
	if err != nil {
		// Временное поведение, чтобы программа продолжала работать (но не соответствовала требованиям).
		fmt.Println("сумма должна быть целым числом")
		return
		bill = 0
	}

	// TODO: Реализуйте парсинг tipStr в int через strconv.Atoi с корректной обработкой ошибки:
	// TODO: при ошибке выведите "чаевые должны быть целым числом" и завершите программу (return).
	tipPercent, err := strconv.Atoi(tipStr)
	if err != nil {
		// Временное поведение, чтобы программа продолжала работать (но не соответствовала требованиям).
		fmt.Println("чаевые должны быть целым числом")
		return
		tipPercent = 0
	}

	// TODO: Добавьте логическую валидацию (отдельными if, не через err):
	// TODO: 1) bill не может быть отрицательной (сообщение: "сумма не может быть отрицательной")
	// TODO: 2) tipPercent должен быть в диапазоне 0..100 включительно (сообщение: "процент чаевых должен быть от 0 до 100")
    // TODO: При нарушении — печатайте сообщение и завершайте программу (return).
	if bill < 0 {
	    fmt.Println("сумма не может быть отрицательной")
	    return
	}
	if tipPercent < 0 || tipPercent > 100 {
	    fmt.Println("процент чаевых должен быть от 0 до 100")
	    return
	}

	// TODO: Посчитайте итоговую сумму только целочисленно по формуле из условия.
	// Временное (неверное) вычисление: сейчас считается только сумма чаевых, без суммы счёта.
	total := bill + ((bill * tipPercent) / 100)

	fmt.Printf("итого: %d\n", total)
}