package main

/*
= Задача №19. Шаги пары =
Вы программируете “магический генератор последовательности” для игры: есть два целых параметра x и y, и каждый ход они обновляются по правилу: новое x становится старым y, а новое y становится суммой старых x и y. Так можно моделировать простую динамику вроде “каждый следующий ресурс — сумма двух предыдущих”.
Программа должна считать из stdin три целых числа: x, y и n. Затем выполнить ровно n шагов обновления пары по правилу: на каждом шаге (x, y) = (y, x+y).
Обновление на каждом шаге должно выполняться одной строкой через множественное присваивание. После всех шагов выведите: x=<x> y=<y>
Если n равно 0, выведите исходные x и y без изменений.
Требования:
• Программа должна считать из stdin три целых числа `x`, `y`, `n` с помощью `fmt.Scan(&x, &y, &n)`.
• Программа должна выполнить цикл `for` со счётчиком от 0 до `n-1` (то есть ровно `n` итераций), а при `n == 0` цикл не должен выполниться ни разу.
• На каждой итерации цикла обновление значений должно выполняться одной строкой в виде множественного присваивания по правилу `(x, y) = (y, x+y)` (в Go: `x, y = y, x+y`).
• При обновлении значений в цикле нельзя использовать дополнительные временные переменные для сохранения старого `x` (или старого `y`); обновление должно опираться только на множественное присваивание.
• После выполнения всех шагов программа должна вывести ровно одну строку формата `x=%d y=%d\n`, подставив итоговые значения `x` и `y`.
*/

import "fmt"

func main() {
	var x, y, n int
	fmt.Scan(&x, &y, &n)

	for i := 0; i < n; i++ {
		// TODO: Обновите пару (x, y) по правилу из условия одной строкой (множественное присваивание), без временных переменных.
		x, y = y, x + y
	}

	fmt.Printf("x=%d y=%d\n", x, y)
}