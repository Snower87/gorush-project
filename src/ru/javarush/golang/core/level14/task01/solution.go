package main

/*
= Задача №1. Сканер байтов =
Вы пишете маленькую утилиту для «проверки пароля на терминале». Пользователь вводит одно слово (без пробелов), а вы хотите быстро увидеть, из каких байтов оно состоит — особенно полезно, когда кто-то присылает странный ввод и «на глаз» непонятно, что там за символы.
Программа читает из stdin одну строку inputWord (одно слово). Если слово оказалось пустым, нужно честно сказать, что анализировать нечего, вывести "EMPTY" и завершиться.
Если слово не пустое, выведите пять строк в строгом порядке:
- длину строки в байтах,
- числовое значение первого байта,
- первый байт как символ,
- числовое значение последнего байта,
- и последний байт как символ.
Так вы сразу увидите и размер, и «края» строки.
Требования:
• Программа должна прочитать из stdin одну строку inputWord (одно слово без пробелов), используя fmt.Scan или fmt.Fscan.
• Если введённая строка пустая (len(inputWord) == 0), программа должна вывести ровно одну строку EMPTY и завершиться, не выполняя индексацию строки.
• Если строка не пустая, программа должна вычислить длину в байтах с помощью len(inputWord) и вывести её в первой строке вывода.
• Для получения байтов программа должна использовать индексацию inputWord[i], причём первый байт брать как inputWord[0], а последний — строго по индексу len(inputWord)-1.
*/

import "fmt"

func main() {
	var inputWord string
	fmt.Scan(&inputWord) // читаем одно "слово" (токен)

	// Важно: нельзя индексировать пустую строку.
	if len(inputWord) == 0 {
		fmt.Println("EMPTY")
		return
	}

	n := len(inputWord) // длина именно в байтах
	fmt.Println(n)

	// TODO: Реализуйте вывод оставшихся 4 строк для непустой строки:
	// TODO: (1) числовое значение первого байта, (2) первый байт как символ,
	// TODO: (3) числовое значение последнего байта, (4) последний байт как символ.
	fmt.Printf("%d\n", inputWord[0])
	fmt.Printf("%c\n", inputWord[0])
	fmt.Printf("%d\n", inputWord[n-1])
	fmt.Printf("%c\n", inputWord[n-1])
}