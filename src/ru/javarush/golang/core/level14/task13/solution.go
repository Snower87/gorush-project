package main

/*
= Задача №13. Чистый ввод =
Вы настраиваете консольный приветственный экран, и там внезапно выясняется неприятное: строка вроде выглядит нормально, но на самом деле по краям прячутся табы, пробелы и перевод строки. Чтобы понять, что именно вы «чистите», вы хотите показать сырую строку и результат очистки так, чтобы были видны все управляющие символы.
В программе есть переменная raw со значением строки " \t Go is fun \n ". Создайте вторую строку clean, полученную из raw с помощью strings.TrimSpace, и выведите четыре строки: raw в формате "%q", clean в формате "%q", длину raw в байтах и длину clean в байтах.
Требования:
• В программе должна быть объявлена переменная raw со строковым значением " \t Go is fun \n " (включая ведущие/хвостовые пробелы, таб и перевод строки).
• Должна быть создана переменная clean, полученная из raw вызовом strings.TrimSpace(raw), при этом должны обрезаться только пробельные символы по краям строки.
• raw и clean должны быть выведены через fmt.Printf с форматом %q (каждая строка — отдельный вывод), чтобы табы/переводы строк/пробелы были видны в результате.
• Должны быть выведены длина raw и длина clean в байтах как целые числа (например, через len(raw) и len(clean)), по одному числу на строку.
*/

import (
	"fmt"
	"strings"
)

func main() {
	raw := " \t  Go is fun \n  "

	// TODO: Получите строку clean из raw с помощью strings.TrimSpace(raw).
	// Сейчас обрезаем только пробелы ' ' по краям — этого недостаточно (таб и перевод строки останутся).
	clean := strings.TrimSpace(raw)

	// TODO: Выведите raw и clean так, чтобы были видны управляющие символы (используйте формат %q).
	fmt.Printf("%q\n", raw)
	fmt.Printf("%q\n", clean)

	// TODO: Выведите длины raw и clean в байтах, по одному числу на строку.
	fmt.Printf("%d\n", len(raw))
	fmt.Printf("%d\n", len(clean))
}