package main

/*
= Задача №15. Команды доступа =
Вы делаете мини‑утилиту админа, которая управляет правами доступа в стиле Unix, но в упрощённой форме: всего три флага — R, W, X, и они живут в одном числе raw (от 0 до 7).
Пользователь вводит текущую маску, операцию (set, clear, toggle, check) и имя флага (R, W, X). Для операций изменения вы печатаете новую маску в десятичном и двоичном виде (ширина 3), а для check — просто on или off. Если команда или флаг неизвестны — печатайте error.
Требования:
• Программа должна считать из stdin три значения в указанном порядке: raw (тип uint), op (string) и flag (string), используя fmt.Scan.
• Программа должна объявить три константы-флага R, W, X как разные биты с помощью выражения 1 << iota (например, R=1, W=2, X=4).
• Программа должна сопоставить введённую строку flag с соответствующей маской (R/W/X); если flag неизвестен — вывести ровно error и завершить обработку.
• Программа должна выполнить операцию над текущим значением raw в зависимости от op: - set: установить бит флага (через value | mask) - clear: очистить бит флага (обязательно через value &^ mask) - toggle: инвертировать бит флага (обязательно через value ^ mask) - check: проверить установлен ли бит (обязательно через (value & mask) != 0) Если op неизвестен — вывести ровно error.
• Для операций set/clear/toggle программа должна вывести новую маску (тип uint) в десятичном виде и в двоичном виде шириной 3 бита, используя форматирование %03b для двоичного представления.
• Для операции check программа должна вывести только on, если флаг установлен, иначе off; никаких дополнительных чисел/строк выводить не нужно.
*/

import "fmt"

const (
	// Три флага как разные биты: 001, 010, 100
	R uint = 1 << iota
	W
	X
)

func main() {
	var raw uint
	var op string
	var flag string

	fmt.Scan(&raw, &op, &flag)

	var mask uint
	switch flag {
	case "R":
		mask = R
	case "W":
		mask = W
	case "X":
		mask = X
	default:
		fmt.Print("error")
		return
	}

	// mask используется, чтобы шаблон компилировался до реализации операций.
	_ = mask

	switch op {
	case "set":
		// TODO: Реализуйте операцию set: установить бит флага в raw.
		raw = raw | mask
		fmt.Printf("%d %03b", raw, raw)
	case "clear":
		// TODO: Реализуйте операцию clear: очистить бит флага в raw.
		raw = raw &^ mask
		fmt.Printf("%d %03b", raw, raw)
	case "toggle":
		// TODO: Реализуйте операцию toggle: инвертировать бит флага в raw.
		raw = raw ^ mask
		fmt.Printf("%d %03b", raw, raw)
	case "check":
		// TODO: Реализуйте операцию check: проверить, установлен ли бит флага в raw.
		if raw & mask != 0 {
		    fmt.Print("on")
		} else {
		    fmt.Print("off")
		}
	default:
		fmt.Print("error")
	}
}