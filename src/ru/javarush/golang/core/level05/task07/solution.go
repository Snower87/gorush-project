package main

/*
= Задача №7. Датчик шума =
Вы подключили поток данных с датчика: он выдаёт целые числа. Часть значений — “шум”, который нужно игнорировать: всё, что по модулю больше 1000. Остальные значения считаются “принятыми”: их нужно учитывать в сумме и счётчике. А ещё есть особый сигнал 777: если он пришёл среди принятых значений, нужно завершить обработку сразу (как аварийная остановка). Ещё одна команда остановки — 0, она тоже завершает ввод, но в статистику не входит.
Программа читает числа до тех пор, пока не встретит 0, или пока ввод не закончится, или пока не будет принят 777. Шумовые значения (меньше -1000 или больше 1000) пропускаются. Для принятых значений увеличивается счётчик и накапливается сумма. В конце выводятся две величины в одной строке через пробел: количество_принятых сумма.
Требования:
• Программа должна читать из stdin целые числа по одному до завершения обработки (0, конец ввода или ошибка чтения, или принятое 777).
• Обработка ввода должна быть реализована через бесконечный цикл for { ... } с управлением завершением через break.
• Если очередное чтение числа завершается ошибкой (err != nil, включая EOF), программа должна завершить цикл типичным для Go способом (проверка err и break), без panic.
• Если прочитанное число x больше 1000 или меньше -1000, оно должно пропускаться через continue и не должно влиять ни на сумму, ни на счётчик.
• Если прочитанное число равно 0, цикл должен завершаться через break, при этом 0 не должен учитываться в сумме и счётчике.
• Все значения из диапазона [-1000; 1000] должны увеличивать счётчик и добавляться к сумме; если принятое значение равно 777, оно должно сначала быть учтено (в сумме и счётчике), а затем цикл должен завершиться через break.
*/

import (
	"bufio"
	"fmt"
	"os"
)

func main() {
	in := bufio.NewReader(os.Stdin)

	var (
		count int
		sum   int
	)

	for { // по требованию: бесконечный цикл с управлением через break
		var x int
		_, err := fmt.Fscan(in, &x)
		if err != nil { // конец ввода/ошибка чтения — просто выходим
			break
		}

		// команда остановки: в статистику не входит
		if x == 0 {
			break
		}

		// TODO: реализовать фильтрацию шумовых значений по условию задачи (с пропуском через continue)
		if x > 1000 || x < -1000 {
		    continue
		}

		// TODO: реализовать учёт принятых значений (счётчик и сумма)
		sum += x
		count++

		// TODO: реализовать аварийную остановку по специальному сигналу из условия задачи
		if x == 777 {
		    break
		}
	}

	fmt.Println(count, sum)
}