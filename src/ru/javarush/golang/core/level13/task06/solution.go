package main

/*
= Задача №6. Снять последний =
Представьте, что у вас очередь на печать, и последний документ надо убрать: он “снимается” из списка, но перед этим вы аккуратно очищаете ссылку в backing array (чтобы не держать лишнюю память).
Программа читает n (гарантируется n > 0), затем n строк (без пробелов) в слайс jobs. Удалите последний элемент так: сначала очистите последний слот через clear на диапазоне длины 1, затем уменьшите длину через reslice. После этого в слайсе должны остаться первые n-1 строк в исходном порядке.
Выведите получившийся список в одну строку: строки через пробел. Если после удаления слайс пустой — выведите пустую строку.
Требования:
• Программа должна прочитать целое n (гарантируется n > 0), затем прочитать n строк без пробелов и сохранить их в слайс jobs типа []string в исходном порядке.
• Перед удалением программа должна очистить последний слот backing array, вызвав clear на диапазоне длиной 1, соответствующем последнему элементу слайса при старой длине.
• Программа должна сначала выполнить очистку последнего элемента при старом len(jobs), и только после этого уменьшить длину слайса.
• Уменьшение len(jobs) должно быть выполнено только через присваивание вида jobs = jobs[:newLen], где newLen = n-1.
• Результат должен получаться без создания нового слайса для оставшихся элементов; допускается только работа с существующим jobs (очистка + reslice), и не должен использоваться пакет slices.
*/

import (
	"bufio"
	"fmt"
	"os"
)

func main() {
	in := bufio.NewReader(os.Stdin)

	var n int
	fmt.Fscan(in, &n)

	jobs := make([]string, n)
	for i := 0; i < n; i++ {
		fmt.Fscan(in, &jobs[i])
	}

	// TODO: Удалите последний элемент "на месте":
	// TODO: 1) сначала очистите последний слот backing array через clear на диапазоне длины 1
	// TODO: 2) затем уменьшите длину слайса через reslice (без создания нового слайса)
	clear(jobs[n-1 : n])
	newLen := n - 1
	jobs = jobs[:newLen]

	out := bufio.NewWriter(os.Stdout)
	for i, s := range jobs {
		if i > 0 {
			fmt.Fprint(out, " ")
		}
		fmt.Fprint(out, s)
	}
	out.Flush()
}