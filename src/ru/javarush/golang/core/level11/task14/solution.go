package main

/*
= Задача №14. Пакетное обновление =
Вы храните список чисел как «пакет данных», и в конце передачи нужно обязательно дописать ещё одно контрольное значение. Добавление делается через функцию, и здесь важно помнить контракт: append возвращает обновлённый слайс, его нельзя игнорировать.
Программа читает из stdin число n, затем n целых чисел и собирает их в слайс numbers. Затем программа читает ещё одно целое число extraValue и добавляет его в конец слайса с помощью функции addOne.
Функция addOne принимает слайс и число, добавляет число через append и возвращает обновлённый слайс. В main вы обязаны присвоить результат обратно в переменную, а затем вывести итоговый слайс одной строкой через пробел.
Требования:
• В программе должна быть реализована функция addOne(s []int, x int) []int, принимающая слайс целых чисел и одно целое число и возвращающая обновлённый слайс.
• Внутри addOne добавление должно выполняться строго через присваивание результата append обратно в переменную слайса: s = append(s, x), после чего функция возвращает обновлённый слайс.
• В main результат вызова addOne должен быть присвоен обратно переменной слайса numbers (результат append/функции нельзя игнорировать).
• Программа должна прочитать через fmt.Scan число n, затем n целых чисел в слайс numbers, затем ещё одно целое число extraValue.
*/

import "fmt"

func addOne(s []int, x int) []int {
	// TODO: Добавьте число x в конец слайса s через append и верните обновлённый слайс.
	// TODO: Важно: результат append нельзя игнорировать — его нужно сохранить обратно в переменную слайса.
	s = append(s, x)
	return s
}

func main() {
	var n int
	fmt.Scan(&n)

	numbers := make([]int, n)
	for i := 0; i < n; i++ {
		fmt.Scan(&numbers[i])
	}

	var extraValue int
	fmt.Scan(&extraValue)

	// TODO: Вызовите addOne и сохраните результат обратно в numbers (результат функции нельзя игнорировать).
	numbers = addOne(numbers, extraValue)

	for i, v := range numbers {
		if i > 0 {
			fmt.Print(" ")
		}
		fmt.Print(v)
	}
}