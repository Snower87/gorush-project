package main

/*
= Задача №2. Скидка на кассе =
Представьте, что вы делаете “быструю кассу” для интернет-магазина. На вход приходит одно целое число total — сумма заказа до скидки.
Правило простое, как на табличке у кассира: если total не меньше 1000, скидка discount равна 100, иначе скидка равна 0. После этого нужно вывести в одной строке два числа через пробел: сначала discount, затем finalTotal, где finalTotal = total - discount.
Фишка задания — область видимости: скидка должна быть доступна после if, поэтому объявите discount заранее, а внутри if только меняйте её через =.
Требования:
• Программа должна считать одно целое число total из stdin с помощью fmt.Scan.
• Переменная total должна быть объявлена внутри main с использованием короткого объявления :=.
• Переменная discount должна быть объявлена до конструкции if (через var или через :=) так, чтобы быть доступной после if, и не должна переобъявляться повторно.
• Внутри if должно использоваться обычное присваивание discount = 100 или discount = 0; короткое объявление (:=) для discount внутри if запрещено.
• После выполнения if программа должна вычислить finalTotal как total - discount (отдельной переменной) после if.
*/

import "fmt"

func main() {
	total := 0
	fmt.Scan(&total)

	discount := 0
	// TODO: Реализуйте правило расчёта скидки по условию задачи (discount должен быть доступен после if).
	if total >= 1000 {
		// TODO: Назначьте discount корректное значение через обычное присваивание (=), без переобъявления (:=).
		discount = 100
	} else {
		// TODO: Назначьте discount корректное значение через обычное присваивание (=), без переобъявления (:=).
		discount = 0
	}

	// TODO: Вычислите finalTotal после if как total - discount (отдельной переменной).
	finalTotal := total - discount

	fmt.Printf("%d %d\n", discount, finalTotal)
}