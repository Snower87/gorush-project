package main

/*
= Задача №11. Конфликт имён =
Вы пишете приветствие для консольного приложения, но по требованиям дизайна в main должна быть строковая переменная с именем fmt — она хранит префикс "hello,". Это специально сделано, чтобы вы увидели реальную проблему: переменная перекрывает имя импортированного пакета.
Чтобы всё равно печатать в консоль, пакет fmt придётся импортировать с алиасом и вызывать печать через этот алиас. Имя пользователя нужно предварительно привести в нормальный вид через strings.TrimSpace.
Требования:
• В программе должна быть объявлена строковая переменная с именем fmt, содержащая префикс "hello,"; переименовывать эту переменную запрещено.
• Пакет "fmt" должен быть импортирован с алиасом (например, f "fmt"), чтобы имя пакета не конфликтовало с переменной fmt.
• Все импорты должны быть оформлены в групповой форме import ( ... ).
• Пакет "strings" должен быть импортирован без алиаса и использован для нормализации имени пользователя через strings.TrimSpace перед выводом.
*/

import (
	"bufio"
	f "fmt"
	"os"
	"strings"
)

func main() {
	fmt := "hello," // по условию нельзя переименовывать

	in := bufio.NewReader(os.Stdin)
	name, _ := in.ReadString('\n')

	// TODO: Нормализуйте имя пользователя через strings.TrimSpace (убрать пробелы и перевод строки).
	// TODO: Импортируйте пакет "fmt" с алиасом, чтобы не конфликтовать с переменной fmt, и печатайте через алиас.
	// TODO: Выведите ровно одну строку в формате: hello, <name>.
	f.Println(fmt, strings.TrimSpace(name))
}