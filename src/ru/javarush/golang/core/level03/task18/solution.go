package main

/*
= Задача №18. Промокод плюс =
Вы поддерживаете консольный сервис “быстрого бонуса”: пользователь вводит значение как строку (потому что иногда туда копируют что угодно),
а система должна попытаться распознать целое число. Если число получилось — прибавить 10 и вывести результат.
Если нет — вежливо отказать одним коротким сообщением.
Программа читает из stdin одну строку bonusStr (через fmt.Scan).
Далее программа пытается преобразовать bonusStr в int через strconv.Atoi. Преобразование должно быть оформлено через
if с инициализатором (в стиле: if n, err := ...; err != nil { ... } else { ... }).
Если строка не является целым числом, программа должна вывести:
не число
и завершиться.
Если преобразование успешно, программа должна вывести значение n+10.
Требования:
• Программа должна считать из stdin ровно одно значение через fmt.Scan в переменную типа string (bonusStr), не пытаясь читать число напрямую.
• Программа должна преобразовать bonusStr в int с помощью strconv.Atoi без альтернативных способов парсинга.
• Вызов strconv.Atoi и проверка err должны быть оформлены одним if с инициализатором вида: if n, err := strconv.Atoi(bonusStr); err != nil { ... } else { ... }.
• Если strconv.Atoi возвращает ошибку, программа должна вывести ровно строку: не число (без дополнительных слов/символов) и не выполнять дальнейшие действия “успешного” сценария (через return или корректную ветку else).
• Если преобразование успешно, программа должна вывести только одно целое число — результат n+10.
*/

import (
	"fmt"
	"strconv"
)

func main() {
	var bonusStr string
	fmt.Scan(&bonusStr)

	if n, err := strconv.Atoi(bonusStr); err != nil {
		// TODO: Если строка не является целым числом, выведите ровно "не число" и завершите программу.
		fmt.Print("не число")
		return
	} else {
		// TODO: Если преобразование успешно, выведите результат n+10 (без лишних слов).
		n += 10
		fmt.Print(n)
	}
}