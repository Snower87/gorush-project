package main

/*
= Задача №1. Касса и кешбэк =
Вы пишете маленький модуль для приложения "кошелёк", которое показывает, сколько денег осталось после покупки. Пользователь вводит три целых числа: budget (сколько было денег), purchase (сколько потратили) и cashback (сколько вернулось кешбэком).
Посчитайте остаток по-честному “как в жизни”: сначала считаем, что остаток равен budget, потом уменьшаем его на purchase, а затем увеличиваем на cashback. В конце выведите одно целое число — итоговый остаток.
В этой задаче важно не запутаться в переменных: переменную остатка нужно создать один раз, а дальше только обновлять через оператор = (без повторных объявлений).
Требования:
• Программа должна считать из stdin три целых числа `budget`, `purchase`, `cashback` с помощью `fmt.Scan`.
• Переменные `budget`, `purchase`, `cashback` должны быть объявлены в `main` через `var` с явным типом `int`.
• Переменная `rest` должна быть создана один раз через `:=` на основе `budget` (то есть стартовое значение остатка равно `budget`).
• Дальнейшие изменения `rest` должны выполняться только оператором присваивания `=`: сначала уменьшить на `purchase`, затем увеличить на `cashback` (без использования `:=` для `rest` повторно).
• Программа должна вывести итоговое значение `rest` ровно один раз с переводом строки, используя `fmt.Println`.
*/

import "fmt"

func main() {
	var budget int
	var purchase int
	var cashback int

	fmt.Scan(&budget, &purchase, &cashback)

	rest := budget

	// TODO: Учтите покупку: обновите rest через "=", не используйте ":=" повторно для rest
	// TODO: Учтите кешбэк: снова обновите rest через "=", не используйте ":=" повторно для rest
	rest -= purchase
	rest += cashback

	fmt.Println(rest)
}