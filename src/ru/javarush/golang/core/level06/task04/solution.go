package main

/*
= Задача №4. Рекордный счётчик =
Вы делаете табло для тренировок: спортсмен вводит результаты попыток, а программа должна показать лучший результат и сколько раз по ходу серии “обновлялся рекорд”.
Программа читает целое число n, а затем n целых чисел — результаты попыток. Нужно найти лучший результат best (максимум) и посчитать, сколько раз рекорд обновлялся updates. Рекорд считается обновлённым, когда текущий результат строго больше текущего best. Самая первая попытка всегда устанавливает рекорд, поэтому при n > 0 она должна увеличить updates на 1.
Выведите best и updates в одной строке через пробел. Если n равно 0, выведите 0 0.
Требования:
• Программа должна прочитать целое число n, затем (если n > 0) прочитать ровно n целых чисел с использованием fmt.Scan.
• Если n равно 0, программа должна вывести ровно "0 0" (два целых числа через пробел) и завершить работу без попыток чтения результатов.
• При n > 0 программа должна считать первую попытку установкой рекорда (best становится равен первому результату, updates увеличивается на 1), а для каждой следующей попытки увеличивать updates и обновлять best только если текущий результат строго больше текущего best.
• Переменные best и updates должны быть объявлены до цикла, быть доступны после цикла; текущее значение попытки должно объявляться внутри тела цикла в минимально возможной области видимости; best должен обновляться только через присваивание "=", без повторного объявления через ":="; updates должен увеличиваться через присваивание (например, updates = updates + 1).
*/

import "fmt"

func main() {
	var n int
	fmt.Scan(&n)

	// По условию: при n == 0 ничего больше не читаем.
	if n == 0 {
		fmt.Println("0 0")
		return
	}

	var best int    // лучший результат (максимум), нужен после цикла
	var updates int // сколько раз ставился новый рекорд, нужен после цикла

	for i := 0; i < n; i++ {
		var x int // текущая попытка — область видимости минимальная
		fmt.Scan(&x)

		// Первая попытка всегда ставит рекорд.
		if i == 0 {
			best = x
			updates = updates + 1
			continue
		}

		// TODO: Реализуйте правило обновления рекорда: если текущий результат строго больше best,
		// TODO: то обновите best (через "=") и увеличьте updates (через присваивание).
		if x > best {
			best = x
			updates = updates + 1
		}
	}

	fmt.Println(best, updates)
}