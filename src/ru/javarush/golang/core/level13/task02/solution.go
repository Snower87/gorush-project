package main

/*
= Задача №2. Сколько влезло =
Представьте, что вы готовите витрину: у вас есть “полка” dst длины D, и вы заранее заполняете её одинаковыми заглушками F (чтобы было видно, какие места так и остались пустыми).
Потом приходит поставка src длины S, и вы пытаетесь “переложить” товары на полку с помощью copy.
Программа читает D, S, F, затем S целых чисел в src. Создайте dst как make([]int, D), заполните все элементы значением F циклом for, затем выполните n := copy(dst, src).
После этого выведите, сколько реально переложилось (n), и затем содержимое dst после копирования.
Требования:
• Программа должна прочитать целые числа D, S, F, затем прочитать S целых чисел для заполнения src (если S = 0, чтение элементов src не выполняется).
• Программа должна создать src как make([]int, S) и заполнить его значениями из ввода по индексам 0..S-1.
• Программа должна создать dst как make([]int, D) (именно длины D, а не нулевой длины с capacity), включая корректную работу при D = 0.
• Перед копированием программа должна заполнить все элементы dst значением F с помощью цикла for (если D = 0, цикл не должен приводить к ошибкам).
• Программа должна выполнить n := copy(dst, src) и не должна создавать дополнительные слайсы “для результата” (изменения должны происходить непосредственно в dst).
*/

import (
	"bufio"
	"fmt"
	"os"
)

func main() {
	in := bufio.NewReader(os.Stdin)
	out := bufio.NewWriter(os.Stdout)
	defer out.Flush()

	var D, S, F int
	fmt.Fscan(in, &D, &S, &F)

	// src заданной длины S
	src := make([]int, S)
	for i := 0; i < S; i++ {
		fmt.Fscan(in, &src[i])
	}

	// dst именно длины D (не 0 с capacity)
	dst := make([]int, D)

	// TODO: заполните все элементы dst значением F с помощью цикла for
	for i := 0; i < D; i++ {
	    dst[i] = F
	}

	// TODO: выполните копирование через n := copy(dst, src) без создания дополнительных слайсов
	n := copy(dst, src)

	// TODO: выведите ровно две строки: в первой n, во второй — содержимое dst после копирования
	// TODO: учтите, что при D = 0 вторая строка может быть пустой
	fmt.Fprintln(out, n)
	fmt.Fprintln(out, dst...)
}