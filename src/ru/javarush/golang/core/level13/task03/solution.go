package main

/*
= Задача №3. Закрыть дыру =
Вы делаете прототип списка задач. Иногда задача "удаляется", но пока вы не меняете длину списка — вы просто закрываете дырку: всё справа сдвигается влево на одну позицию, и список остаётся той же длины (последний элемент дублируется, но это нормально для демонстрации).
Программа читает N, затем N целых чисел в слайс tasks, затем индекс holeIndex. Если holeIndex находится в диапазоне 0 ≤ holeIndex < N-1, выполните операцию copy(tasks[holeIndex:], tasks[holeIndex+1:]). Если индекс некорректный — ничего не делайте.
Выведите итоговый tasks в одной строке: ровно N чисел через пробел (длина не меняется).
Требования:
• Программа должна прочитать N, затем создать слайс tasks длины N через make и заполнить его N целыми числами из ввода, после чего прочитать holeIndex.
• Программа должна через if проверить, что holeIndex находится в диапазоне 0 ≤ holeIndex < N-1; при N=0 или N=1 условие должно быть ложным, и сдвиг не выполняется.
• Если holeIndex корректный, программа должна выполнить сдвиг элементов влево ровно одной операцией copy(tasks[holeIndex:], tasks[holeIndex+1:]).
• Программа не должна изменять длину tasks (нельзя делать tasks = tasks[:...] или удалять элементы); после выполнения (или пропуска) операции сдвига в tasks должно оставаться ровно N элементов.
• Программа должна вывести tasks в одной строке: ровно N целых чисел, разделённых одиночными пробелами (без дополнительных пакетов и без лишних символов форматирования).
*/

import "fmt"

func main() {
	var n int
	fmt.Scan(&n)

	// По требованию: создаём слайс длины N через make и заполняем из ввода.
	tasks := make([]int, n)
	for i := 0; i < n; i++ {
		fmt.Scan(&tasks[i])
	}

	var holeIndex int
	fmt.Scan(&holeIndex)

	// TODO: Проверьте, что holeIndex находится в диапазоне 0 ≤ holeIndex < N-1 (при N=0 или N=1 сдвиг не выполняется).
	// TODO: Если индекс корректный, выполните сдвиг элементов влево РОВНО одной операцией copy, не меняя длину tasks.
	if holeIndex >= 0 && holeIndex < n - 1 {
	    copy(tasks[holeIndex: ], tasks[holeIndex + 1: ])
	}

	// Ровно N чисел через пробел.
	for i := 0; i < n; i++ {
		if i > 0 {
			fmt.Print(" ")
		}
		fmt.Print(tasks[i])
	}
	fmt.Println()
}