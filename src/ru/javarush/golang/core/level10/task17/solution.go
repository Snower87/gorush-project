package main

/*
= Задача №17. Командный счётчик =
Вы делаете консольный “счётчик очков” для небольшой игры. Игра присылает команды по одной: что сделать с текущим результатом и какое число использовать. Иногда нужно прибавить очки, иногда вычесть, а иногда — жёстко установить значение (например, при загрузке сохранения). В конце приходит команда остановки, после которой нужно показать финальный результат.
Ввод состоит из пар: операция (строка) и число (int). Поддерживаются команды add x, sub x, set x. Команда stop 0 завершает ввод. Текущий результат начинается с 0, хранится только локально в main и меняется через отдельную “чистую” функцию apply(op string, current int, x int) int, которая ничего не читает и ничего не печатает.
После stop программа должна вывести одно число — итоговый результат.
Требования:
• В программе не должно быть переменных уровня пакета (никаких var вне функций); текущее значение счётчика хранится только в локальной переменной внутри main.
• Программа должна содержать функцию apply(op string, current int, x int) int, которая принимает операцию, текущее значение и число x, и возвращает новое значение.
• Функция apply должна поддерживать операции add/sub/set; при неизвестной операции она должна возвращать current без изменений.
• Программа должна читать пары (операция, число) через fmt.Scan в цикле for и при команде stop 0 немедленно завершать цикл без дополнительных вычислений.
*/

import "fmt"

// apply — чистая функция: не читает и не печатает, только считает новое значение.
func apply(op string, current int, x int) int {
	// TODO: Реализуйте логику операций add/sub/set.
	// TODO: При неизвестной операции верните current без изменений.
	switch op {
        case "add":
            return current + x
        case "sub":
            return current - x
        case "set":
            return x
	}
	return current
}

func main() {
	current := 0 // по требованиям — только локальное состояние, без глобальных переменных

	// TODO: Реализуйте цикл чтения пар (операция, число) через fmt.Scan.
	// TODO: Немедленно завершайте цикл при команде stop 0 (без вызова apply).
	// TODO: Для остальных команд обновляйте current через apply.
	for {
		var op string
    	var x int
	    fmt.Scan(&op, &x)
	    if op == "stop" && x == 0 {
	        break
	    }
        current = apply(op, current, x)
	}

	// Ровно одна строка с итогом.
	fmt.Println(current)
}