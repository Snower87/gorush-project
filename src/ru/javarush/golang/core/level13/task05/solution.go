package main

/*
= Задача №5. Стереть участок =
Вы делаете мини-редактор "цифровой ленты": в слайсе хранятся показания датчиков, и иногда оператор просит "обнулить" подозрительный кусок, но длину записи менять нельзя — это важно для отчёта по минутам.
Программа читает n, затем n целых чисел в слайс readings. Потом читает два индекса from и to. Гарантируется, что 0 <= from <= to <= n. Обнулите элементы readings[from:to] с помощью clear (именно на под-слайсе), а затем выведите все n элементов в одной строке через пробел. Длина readings должна остаться равной n.
Требования:
• Программа должна считать из stdin число n, затем n целых чисел в слайс readings, затем два индекса from и to, причём гарантируется, что 0 <= from <= to <= n.
• Программа должна хранить показания в слайсе readings длины ровно n и не изменять длину readings в процессе решения (нельзя уменьшать слайс через reslice на меньшую длину).
• Программа должна обнулить элементы в диапазоне readings[from:to], вызывая встроенную функцию clear именно на под-слайсе readings[from:to].
• Решение не должно использовать циклы или другие ручные присваивания для зануления элементов по одному; обнуление должно быть выполнено через clear.
*/

import (
	"bufio"
	"fmt"
	"os"
)

func main() {
	in := bufio.NewReader(os.Stdin)

	var n int
	fmt.Fscan(in, &n)

	// Слайс фиксированной длины: len(readings) всегда == n.
	readings := make([]int, n)
	for i := 0; i < n; i++ {
		fmt.Fscan(in, &readings[i])
	}

	var from, to int
	fmt.Fscan(in, &from, &to)

	// TODO: Обнулите только указанный диапазон элементов через clear, применив clear к под-слайсу нужного участка.
	// Нельзя занулять элементы вручную по одному присваиванием.
	clear(readings[from:to])

	out := bufio.NewWriter(os.Stdout)
	for i := 0; i < n; i++ {
		// По условию допустим завершающий пробел.
		fmt.Fprint(out, readings[i], " ")
	}
	out.Flush()
}