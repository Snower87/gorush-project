package main

/*
= Задача №20. Упаковка склада =
На складе всё должно сходиться до последней детали. Общее количество деталей и вместимость одной коробки приходят как
строки: totalStr и boxSizeStr. Нужно понять, сколько коробок получится заполнить полностью, и сколько деталей останется “хвостом”.
Программа должна распарсить оба значения в int, вычислить fullBoxes и restItems, а затем вывести одну строку строго
в формате без пробелов: boxes=<число>;rest=<число>
Требования:
• Программа должна считать из stdin два значения и сохранить их в переменные типа string (например, totalStr и boxSizeStr).
• Программа должна преобразовать обе входные строки в int, используя strconv.Atoi (для каждого значения отдельно).
• После парсинга программа должна вычислить fullBoxes и restItems (или эквивалентные по смыслу переменные),
  используя целочисленные операции / и %.
• Программа должна собрать итоговую строку результата конкатенацией строк, преобразуя числа в строки через strconv.Itoa.
• Программа должна вывести ровно одну строку строго в формате boxes=<число>;rest=<число>, без пробелов и без любых
  дополнительных слов/символов.
• Код должен быть явно разделён комментариями // read, // parse, // compute, // print, отражающими последовательность
  read → parse → compute → print.
*/

import (
	"fmt"
	"strconv"
)

func main() {
	// read
	var totalStr string     // общее количество деталей
	var boxSizeStr string   // вместимость одной коробки
	fmt.Scan(&totalStr, &boxSizeStr)

	// parse
	// TODO: Распарсите totalStr и boxSizeStr в int через strconv.Atoi (каждое значение отдельно) и запишите результат в total и boxSize.
	total := 0
	boxSize := 0
	total, _ = strconv.Atoi(totalStr)
	boxSize, _ = strconv.Atoi(boxSizeStr)

	// compute
	// TODO: Вычислите количество полных коробок и остаток, используя целочисленные операции / и % (получите значения для fullBoxes и restItems).
	fullBoxes := total / boxSize
	restItems := total % boxSize

	// print
	// TODO: Сформируйте итоговую строку строго в формате boxes=<число>;rest=<число> через конкатенацию и strconv.Itoa, затем выведите ровно одну строку без пробелов.
	out := "boxes=" + strconv.Itoa(fullBoxes) + ";rest=" + strconv.Itoa(restItems)
	fmt.Print(out)
}