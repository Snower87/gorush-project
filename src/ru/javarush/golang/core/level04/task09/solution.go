package main

/*
= Задача №9. Следы в логе =
Вы отлаживаете простой текстовый протокол: программа получает "команду" одним словом, и вам нужно понять, какие позиции
(индексы) в строке реально проходит цикл range. Это полезно, когда вы впервые сталкиваетесь с тем, что range по строке
работает по рунам и может давать неожиданные шаги на не-ASCII символах.
Программа читает из stdin одну строку без пробелов command и обходит её циклом for i := range command. На каждой
итерации печатается значение i в отдельной строке. Вывод должен содержать столько строк, сколько итераций сделал range
по введённой строке.
Требования:
• Программа должна прочитать из stdin одну строку без пробелов в переменную command, используя fmt.Scan.
• Программа должна импортировать и использовать пакет fmt для ввода и вывода.
• Строка command должна обходиться циклом только вида for i := range command (только одно имя переменной в range, без value/_, без других вариантов обхода).
• На каждой итерации цикла программа должна печатать текущее значение i в отдельной строке, используя fmt.Println.
• Программа должна выводить только числа индексов (без пояснений, префиксов, суффиксов и прочего текста).
*/

import "fmt"

func main() {
	var command string
	fmt.Scan(&command)

	// TODO: Обойдите строку command циклом range только по индексу (одна переменная в range).
	// TODO: На каждой итерации печатайте текущее значение индекса i в отдельной строке через fmt.Println.
	// TODO: Убедитесь, что вывод соответствует реальным индексам, которые даёт range по строке в Go.
	for i := range command {
	    fmt.Println(i)
	}
}