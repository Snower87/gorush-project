package main

/*
= Задача №12. Флаги уведомлений =
Вы настраиваете уведомления в приложении: кто-то хочет письма на email, кто-то — SMS, кто-то — push. Внутри системы это хранится одной битовой маской: каждый включённый канал — это отдельный бит.
Программа читает маску как uint, проверяет, какие флаги включены, и печатает названия включённых каналов в стабильном порядке. Если маска нулевая, нужно вывести, что флагов нет.
Требования:
• Должен быть объявлен пользовательский тип NotifyFlag на базе uint.
• Флаги FlagEmail, FlagSMS, FlagPush должны быть объявлены в одном const-блоке с использованием шаблона 1 << iota.
• Программа должна прочитать одно число типа uint через fmt.Scan и после чтения привести его к типу NotifyFlag.
• Включённость каждого флага должна проверяться выражением вида mask&FlagX != 0 (для Email/SMS/Push).
• Если маска не равна 0, программа должна печатать названия включённых каналов строго в порядке email, затем sms, затем push, выводя каждое название на отдельной строке и пропуская отключённые флаги.
• Если маска равна 0, программа должна вывести ровно одну строку: no flags.
*/

import "fmt"

// NotifyFlag — набор флагов уведомлений, хранится как битовая маска.
type NotifyFlag uint

const (
	// TODO: объявите флаги FlagEmail, FlagSMS, FlagPush в одном const-блоке как битовые флаги через iota (паттерн 1 << iota).
	FlagEmail NotifyFlag = 1 << iota
	FlagSMS   NotifyFlag = 1 << iota
	FlagPush  NotifyFlag = 1 << iota
)

func main() {
	var raw uint
	fmt.Scan(&raw)

	mask := NotifyFlag(raw)

	if mask == 0 {
		fmt.Println("no flags")
		return
	}

	// TODO: реализуйте корректную проверку включённости флагов как битовой маски (поддержите комбинации флагов).
	// TODO: обеспечьте вывод включённых каналов строго в порядке: email, затем sms, затем push (каждый с новой строки).
	// Временная упрощённая логика: распознаёт только ровно один установленный флаг.
	if mask & FlagEmail != 0 {
		fmt.Println("email")
	}
	if mask & FlagSMS != 0 {
		fmt.Println("sms")
	}
	if mask & FlagPush != 0 {
		fmt.Println("push")
	}
	if mask == 0 {
		fmt.Println("no flags")
	}
}