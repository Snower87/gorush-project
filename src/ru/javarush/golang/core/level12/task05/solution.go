package main

/*
= Задача №5. Окно правок =
Вы ведёте простую табличку чисел (например, для отчёта или результатов теста): numbers := []int{10, 20, 30, 40, 50}. Для удобства вы открываете “окно” на середину — как будто вы в редакторе выделили кусок списка и правите его.
Программа должна создать под‑слайс view, который смотрит на элементы с индексами 1..3 исходного слайса (то есть 20, 30, 40). Затем вы меняете первый элемент этого окна: view[0] = 999. После этого нужно вывести и view, и весь numbers, чтобы увидеть эффект: правка в окне меняет исходник, потому что данные общие.
Требования:
• Программа должна находиться в package main и содержать функцию func main(), в которой выполняются все действия задачи.
• Переменная numbers должна быть создана литералом слайса []int{10, 20, 30, 40, 50}, без чтения данных извне.
• Переменная view должна быть создана выражением вида numbers[a:b] так, чтобы она включала элементы исходного слайса с индексами 1..3 (то есть значения 20, 30, 40).
• Программа должна изменить первый элемент под‑слайса присваиванием view[0] = 999 (без использования append и без пересоздания слайсов).
• Программа должна вывести ровно 2 строки: первая начинается с "view:" и содержит текущее значение view, вторая начинается с "numbers:" и содержит текущее значение numbers (чтобы было видно, что исходный слайс изменился).
*/

import "fmt"

func main() {
	numbers := []int{10, 20, 30, 40, 50}

	// Под-слайс — это "окно" в исходные данные (общий underlying array).
	view := numbers[1:4]
	// Меняем через view — изменится и исходный numbers.
	view[0] = 999

	fmt.Println("view:", view)
	fmt.Println("numbers:", numbers)
}