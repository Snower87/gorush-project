package main

/*
= Задача №7. Превью сообщения =
Вы делаете «превью» для ленты: нужно показать только первые N символов сообщения, но символы должны считаться именно как Unicode‑руны, а не как байты (иначе можно разрезать UTF‑8 посередине и получить «кракозябры»).
Программа читает целое число N и строку text без пробелов. Если N <= 0, нужно вывести пустую строку. Если в строке меньше N рун — вывести её целиком. Иначе нужно найти байтовую границу, на которой начинается (N+1)-я руна (через for range), и вывести срез text[:cut].
Требования:
• Программа должна считать из stdin целое число N (тип int) и строку text без пробелов, используя fmt.Scan.
• Если N меньше либо равно 0, программа должна вывести пустую строку (только перевод строки).
• Программа должна обходить строку text циклом for range и использовать индексы, возвращаемые range, чтобы определить позицию начала (N+1)-й руны (байтовый индекс cut).
• Если в строке как минимум N+1 руна, программа должна вывести префикс text[:cut], где cut — байтовый индекс, полученный из for range (без разрезания UTF-8 в середине руны).
• Если в строке меньше N рун (или ровно N рун), программа должна вывести исходную строку text целиком без изменений.
*/

import "fmt"

func main() {
	var n int
	var text string
	fmt.Scan(&n, &text)

	// По условию: при N <= 0 печатаем пустую строку.
	if n <= 0 {
		fmt.Println()
		return
	}

	// TODO: Реализуйте обрезку по первым N Unicode-рунам, а не по байтам.
	// TODO: Нельзя преобразовывать строку в []rune.
	// TODO: Используйте for range по строке text, чтобы найти байтовый индекс начала (N+1)-й руны.
	// TODO: Если в строке меньше либо ровно N рун — выведите строку целиком без изменений.
	cut := len(text)
	count := 0

	for i := range text {
		// Как только уже увидели N рун — текущий i и есть старт (N+1)-й.
		if count == n {
			cut = i
			break
		}
		count++
	}

	// Временная упрощённая реализация: режем по байтам (это неверно для UTF-8).
	//cut := len(text)
	//if n < len(text) {
//		cut = n
	//}

	fmt.Println(text[:cut])
}