package main

/*
= Задача №10. Охота за o =
В игре у вас есть ник игрока (одно слово, ASCII), и дизайнер просит статистику: сколько раз в нике встречается буква o (строчная латинская). Нужна максимально простая проверка — без готовых функций подсчёта.
Программа читает строку playerName и выводит одно число — сколько раз встречается символ o.
Требования:
• Программа должна импортировать пакет fmt и прочитать одну строку playerName из stdin с помощью fmt.Scan.
• Строка playerName должна обходиться ровно одним циклом вида for _, v := range playerName, при этом индекс обязательно игнорируется через _.
• Внутри цикла программа должна сравнивать текущий символ с 'o' (в одинарных кавычках) через условие v == 'o'.
• Для подсчёта должен использоваться счётчик типа int, который увеличивается на 1 при каждом совпадении с 'o'.
• После завершения цикла программа должна вывести только итоговое количество вхождений, без дополнительных слов, пояснений или форматирования.
*/

import "fmt"

func main() {
	var playerName string
	fmt.Scan(&playerName)

	var count int

	// TODO: Обойдите строку playerName ровно одним циклом for _, v := range playerName (индекс должен быть проигнорирован через _).
	// TODO: Внутри цикла сравнивайте v с символом 'o' и увеличивайте count на 1 при каждом совпадении.
	// TODO: Не используйте готовые функции подсчёта (например, strings.Count), не добавляйте дополнительные циклы и коллекции для символов.
	for _, v := range playerName {
	    if v == 'o' {
	        count++
	    }
	}
	fmt.Print(count)
}