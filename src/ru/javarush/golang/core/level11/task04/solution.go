package main

/*
= Задача №4. Проверка совпадений =
Вы пишете маленький модуль для игры: нужно сравнивать два списка бонусов и понимать, одинаковы ли они по содержимому. Но в Go слайсы нельзя сравнить оператором == целиком — придётся проверить длину и пройтись по элементам вручную.
В программе без ввода реализуйте функцию EqualInts(a, b []int) bool. Она должна вернуть true, только если длины равны и все элементы на одинаковых индексах совпадают. Иначе вернуть false.
В main создайте три слайса: x := []int{1, 2, 3}, y := []int{1, 2, 3}, z := []int{1, 2, 4}. Затем выведите результат EqualInts(x, y) и EqualInts(x, z) в двух строках так, чтобы получилось true, а затем false.
// Пример вывода:
true
false
Требования:
• В программе должна быть реализована функция EqualInts(a, b []int) bool, принимающая два слайса int и возвращающая bool.
• Внутри EqualInts должно выполняться сравнение длин слайсов через len(a) и len(b); при несовпадении длины функция должна возвращать false без дальнейшего сравнения элементов.
• Если длины равны, EqualInts должна пройтись по индексам (через for или range) и проверить, что элементы a[i] и b[i] совпадают на всех позициях; при первом несовпадении должно возвращаться false, иначе true.
• Для сравнения a и b нельзя использовать оператор == применительно ко всему слайсу (разрешено сравнивать только отдельные элементы типа int).
*/

import "fmt"

func EqualInts(a, b []int) bool {
	// Если длины разные — дальше сравнивать бессмысленно
	if len(a) != len(b) {
		return false
	}

	// TODO: Реализуйте поэлементное сравнение слайсов по индексам.
	// TODO: Нельзя сравнивать слайсы целиком через ==, нужно сравнивать только элементы.
	for i := range a {
	    if a[i] != b[i] {
	        return false
	    }
	}
	return true
}

func main() {
	x := []int{1, 2, 3}
	y := []int{1, 2, 3}
	z := []int{1, 2, 4}

	fmt.Println(EqualInts(x, y))
	fmt.Println(EqualInts(x, z))
}