package main

/*
= Задача №8. Превью без порчи =
Вы делаете консольный “превью-виджет” для списка задач: пользователь вводит n, затем n строк (названия задач без пробелов), а затем число k — сколько первых задач показать в коротком превью. Но есть важное требование продукта: превью не должно портить исходный список, потому что дальше он ещё будет использоваться.
Программа должна построить preview из первых k задач (или из всех n, если k > n) в формате строк с нумерацией: "1) <task>", "2) <task>" и так далее. При этом preview обязан быть независимым слайсом: нельзя брать tasks[:k]. После построения нужно вывести две строки: сначала preview, затем исходный tasks, чтобы было видно, что tasks не менялся.
Требования:
• Программа должна читать из stdin число n, затем n строк (названия задач без пробелов), затем число k, используя fmt.Fscan (допускается обёртка bufio.NewReader(os.Stdin)).
• Программа должна создать слайс tasks длины n и заполнить tasks[i] считанными названиями задач в том же порядке.
• Программа должна определить фактическое количество элементов превью как min(k, n), чтобы при k > n превью строилось по всем задачам.
• Программа должна создать preview как новый слайс (не под-слайс tasks[:...]) с помощью make и append, добавляя строки вида "1) <task>", "2) <task>" и т.д. (нумерация строго с 1 по порядку превью).
• В процессе построения preview программа не должна изменять tasks (никаких присваиваний вида tasks[i] = ...), и не должна использовать copy(), slices.Clone или другие аналогичные средства клонирования.
*/

import (
	"bufio"
	"fmt"
	"os"
)

func min(a, b int) int {
    if a < b {
        return a
    } else {
        return b
    }
}

func main() {
	in := bufio.NewReader(os.Stdin)

	var n int
	fmt.Fscan(in, &n)

	tasks := make([]string, n)
	for i := 0; i < n; i++ {
		fmt.Fscan(in, &tasks[i])
	}

	var k int
	fmt.Fscan(in, &k)

	m := 0
	// TODO: Вычислите фактический размер превью (m) как min(k, n).
	// TODO: Учтите, что k может быть больше n.
	m = min(k, n)

	preview := make([]string, 0, m)
	// TODO: Соберите независимый слайс preview (не под-слайс tasks[:...]).
	// TODO: Нельзя изменять tasks.
	// TODO: Нельзя использовать copy(), slices.Clone и аналогичные клонирующие функции.
	// TODO: Используйте make + append и добавляйте строки в формате "1) <task>", "2) <task>" и т.д.

	for i := 0; i < m; i++ {
		preview = append(preview, fmt.Sprintf("%d) %s", i + 1, tasks[i]))
	}

	// Должно быть выведено ровно две строки: сначала preview, затем tasks.
	fmt.Println(preview)
	fmt.Println(tasks)
}