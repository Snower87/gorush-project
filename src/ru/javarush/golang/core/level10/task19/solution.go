package main

/*
= Задача №19. Отчёт без потери =
Вы пишете короткий лог‑отчёт: по введённому числу нужно вывести одну строку, которая начинается с even: или odd: и дальше содержит само число. Это похоже на метку в журнале событий: быстро понять, с каким типом значения мы работаем.
Есть важная деталь для новичка в Go: строка отчёта должна собираться в переменной report, объявленной до if/else. Внутри веток нельзя использовать :=, иначе вы случайно создадите новую переменную и “потеряете” результат из-за области видимости. Число нужно превратить в строку через strconv.Itoa.
Требования:
• Программа должна считать одно целое число n из stdin.
• Программа должна импортировать пакеты fmt и strconv и не должна объявлять переменные (или другие идентификаторы) с именами fmt или strconv, чтобы не затенять импорты.
• Программа должна объявить переменную report один раз до конструкции if/else (например, через var report string).
• Внутри веток if/else программа должна формировать строку отчёта через обычное присваивание report = ..., не используя report := ... (чтобы не создавать новую переменную в области видимости ветки).
• Если n%2==0, строка report должна начинаться с even:, иначе — с odd:, и далее содержать само число n.
*/

import (
	"fmt"
	"strconv"
)

func main() {
	var n int
	fmt.Scan(&n)

	// report объявляем ДО if/else, чтобы не "потерять" значение из-за := в ветках
	var report string

	// TODO: Сформируйте строку отчёта в report:
	if n%2 == 0 {
		// TODO: Сформируйте корректный отчёт для чётного числа.
		report = "even:" + strconv.Itoa(n)
	} else {
		// TODO: Сформируйте корректный отчёт для нечётного числа.
		report = "odd:" + strconv.Itoa(n)
	}

	fmt.Print(report) // выводим ровно одну строку без лишних сообщений
}