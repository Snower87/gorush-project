package main

/*
= Задача №12. Разделение буфера =
Представьте, что у вас есть общий буфер данных s = [1 2 3 4 5 6]. Вы хотите разделить его на левую и правую части: справа данные должны оставаться как “вид” на исходник, а слева — тоже вид, но с защитой: чтобы любой append слева не мог тихо перезаписать правую часть в том же backing array.
Нужно написать функцию Split, которая принимает s []int и число k, и возвращает два слайса left и right: left — “безопасный” view на первые k элементов с ограниченной ёмкостью, right — обычный view на элементы от k до конца.
В main создайте s = [1 2 3 4 5 6], вызовите Split(s, 3), затем сделайте left = append(left, 100, 200) и выведите в трёх строках s, right, left.
s := []int{1, 2, 3, 4, 5, 6}
left, right := Split(s, 3)
left = append(left, 100, 200)
fmt.Println(s)
fmt.Println(right)
fmt.Println(left)
Требования:
• Нужно реализовать функцию Split(s []int, k int) (left []int, right []int), которая возвращает два слайса: левую и правую части исходного слайса.
• Внутри Split слайс left должен быть создан строго через полный срез с ограничением ёмкости: s[:k:k], чтобы cap(left) == k и append к left не мог перезаписать элементы правой части в том же backing array.
• Внутри Split слайс right должен быть создан как обычный view на оставшиеся элементы: s[k:].
• Split должен корректно работать при k=0 и при k=len(s), не падая по out of range (допускается использование if и/или аккуратное “подрезание” границ).
• В main нужно создать s = []int{1, 2, 3, 4, 5, 6}, вызвать Split(s, 3), затем обязательно выполнить left = append(left, 100, 200) (результат append должен быть присвоен обратно в left).
• Программа должна вывести ровно три строки и строго в порядке: сначала s, затем right, затем left; при этом после append исходный s должен остаться [1 2 3 4 5 6], а right должен остаться [4 5 6].
*/

import "fmt"

func Split(s []int, k int) (left []int, right []int) {
	if k < 0 {
		k = 0
	}
	if k > len(s) {
		k = len(s)
	}

	// TODO: Сделайте left "безопасным" view на первые k элементов: ограничьте ёмкость так,
	// чтобы append(left, ...) не мог перезаписать элементы правой части в том же backing array.
	left = s[:k:k]

	right = s[k:]
	return left, right
}

func main() {
	s := []int{1, 2, 3, 4, 5, 6}

	left, right := Split(s, 3)

	left = append(left, 100, 200)

	fmt.Println(s)
	fmt.Println(right)
	fmt.Println(left)
}