package main

/*
= Задача №7. Соседний слот =
Вы ведёте небольшой список задач на день: tasks := []string{"A","B","C","D"}. Чтобы быстрее работать с началом списка, вы берёте “голову” — первые две задачи — как под‑слайс head := tasks[:2]. На первый взгляд кажется, что это отдельный список… но на самом деле это лишь окно в тот же буфер.
Программа должна до изменения вывести tasks и head вместе с их len и cap. Затем выполнить head = append(head, "X") и снова вывести tasks и head с len и cap. Так вы увидите ловушку: при наличии запаса ёмкости append может перезаписать соседний элемент в исходном слайсе.
tasks := []string{"A","B","C","D"}
head := tasks[:2]
// Вывести tasks и head с их len и cap
head = append(head, "X")
// Снова вывести tasks и head с их len и cap
Требования:
• В программе должен быть создан слайс tasks строго литералом []string{"A","B","C","D"} (без чтения ввода и без иных способов построения).
• В программе должен быть создан слайс head строго как head := tasks[:2] (первые два элемента исходного слайса).
• До изменения head программа должна вывести ровно две строки: одну про tasks и одну про head; каждая из этих строк должна содержать метку (какой слайс печатается), само значение слайса, а также len и cap, вычисленные через len(...) и cap(...).
• Добавление нового элемента должно быть выполнено только выражением head = append(head, "X") (без дополнительных append к tasks и без других модификаций элементов).
• После выполнения append программа должна вывести ровно две строки: одну про tasks и одну про head; формат строк должен содержать метку, сам слайс, len и cap, вычисленные через len(...) и cap(...).
*/

import "fmt"

func main() {
	tasks := []string{"A", "B", "C", "D"}
	head := tasks[:2]

	fmt.Printf("tasks=%v len=%d cap=%d\n", tasks, len(tasks), cap(tasks))
	fmt.Printf("head=%v len=%d cap=%d\n", head, len(head), cap(head))

	// TODO: Добавьте к head элемент "X" через append и сохраните результат обратно в head.
	head = append(head, "X")

	// TODO: Выведите tasks и head после append тем же форматом (2 строки: tasks..., head...), что и до append.
	fmt.Printf("tasks=%v len=%d cap=%d\n", tasks, len(tasks), cap(tasks))
	fmt.Printf("head=%v len=%d cap=%d\n", head, len(head), cap(head))
}