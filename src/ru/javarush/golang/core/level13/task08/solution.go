package main

/*
= Задача №8. Вычеркнуть диапазон =
Вы делаете консольный "бан-лист" слов. Администратор задаёт диапазон [i:j), который нужно убрать одним куском. Индексы могут быть "кривыми", поэтому вы сперва приводите их к безопасным границам, а если после этого удалять нечего — ничего не меняете.
Реализуйте функцию deleteRange(words []string, i, j int) []string. Если i < 0, считайте i = 0. Если j > len(words), считайте j = len(words). Если после нормализации i >= j, верните исходный words без изменений. Иначе закройте дырку через copy(words[i:], words[j:]), вычислите новую длину, очистите освобождённый хвост через clear, и верните укороченный слайс.
Основная программа читает n, затем n строк, затем i и j, и выводит результат в одну строку через пробел.
Требования:
• Функция deleteRange должна приводить i и j к безопасным границам: если i < 0, считать i = 0; если j > len(words), считать j = len(words). Диапазон удаления трактуется как полуинтервал [i:j).
• Если после нормализации i >= j, функция должна вернуть исходный слайс words без изменений (и по длине, и по элементам), не выполняя копирование/очистку хвоста.
• Перед любыми операциями, которые меняют “смысловую длину” результата, функция должна сохранить oldLen := len(words) и использовать его для корректной очистки хвоста.
• При i < j функция должна закрыть удаляемый диапазон операцией moved := copy(words[i:], words[j:]) и вычислить новую длину строго как newLen := i + moved.
• После вычисления newLen функция должна очистить именно освобождённый хвост исходного массива: clear(words[newLen:oldLen]), не затрагивая элементы words[:newLen].
• Функция должна возвращать результат как words[:newLen] и не должна использовать append и пакет slices для удаления диапазона.
*/

import (
	"bufio"
	"fmt"
	"os"
)

func deleteRange(words []string, i, j int) []string {
	// Нормализуем границы в безопасный диапазон.
	if i < 0 {
		i = 0
	}
	if j > len(words) {
		j = len(words)
	}

	// Пустой диапазон — ничего не меняем.
	if i >= j {
		return words
	}

	oldLen := len(words)

	// TODO: Реализуйте удаление полуинтервала [i:j) "на месте":
	// TODO: закройте "дырку" через copy, вычислите новую длину, очистите освобождённый хвост через clear.
	// TODO: верните укороченный слайс без использования append и без пакета slices.
	moved := copy(words[i:], words[j:])
	newLen := i + moved
	clear(words[newLen:oldLen])

	// Временное поведение: диапазон НЕ удаляется (это нужно исправить).
	return words[:newLen]
}

func main() {
	in := bufio.NewReader(os.Stdin)
	out := bufio.NewWriter(os.Stdout)
	defer out.Flush()

	var n int
	fmt.Fscan(in, &n)

	words := make([]string, n)
	for k := 0; k < n; k++ {
		fmt.Fscan(in, &words[k])
	}

	var i, j int
	fmt.Fscan(in, &i, &j)

	words = deleteRange(words, i, j)

	for k, w := range words {
		if k > 0 {
			fmt.Fprint(out, " ")
		}
		fmt.Fprint(out, w)
	}
	fmt.Fprintln(out)
}