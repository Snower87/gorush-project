package main

/*
= Задача №18. Счётчик скидок =
В отчёте магазина вам нужно понять, сколько покупок прошло по “чётному” коду акции: акция считается чётной, если её код делится на 2 без остатка. Коды приходят потоком — хранить их все не нужно, достаточно сразу считать результат.
Программа читает целое число codesCount, а затем читает codesCount целых чисел и считает, сколько среди них чётных. Ввод: сначала codesCount, затем codesCount чисел. Программа должна вывести одно целое число — количество чётных среди введённых. Если codesCount равно 0, результатом является 0.
Требования:
• Программа должна прочитать целое число codesCount, затем прочитать ровно codesCount целых чисел, используя fmt.Scan.
• Программа должна использовать один цикл for, который выполняется codesCount раз, чтобы последовательно обработать каждый код акции.
• Внутри цикла программа должна определять чётность кода условием x%2 == 0.
• Программа должна хранить количество чётных кодов в переменной-счётчике (например, evenCodesCount), начинать с 0 и увеличивать на 1 (evenCodesCount++) при выполнении условия чётности.
• Программа не должна использовать слайсы/массивы или другие структуры для сохранения всех введённых кодов; обработка должна выполняться по мере чтения.
*/

import "fmt"

func main() {
	var codesCount int
	fmt.Scan(&codesCount)

	evenCodesCount := 0 // счётчик чётных кодов (zero value = 0)

	for i := 0; i < codesCount; i++ { // один цикл ровно на codesCount итераций
		var x int
		fmt.Scan(&x)

		// TODO: Определи, является ли код x чётным (через остаток от деления) и увеличь evenCodesCount на 1, если код чётный.
		if x % 2 == 0 {
		    evenCodesCount++
		}
	}

	fmt.Println(evenCodesCount)
}