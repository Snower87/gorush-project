package main

/*
= Задача №16. Байтовый протокол =
Вы подключились к устройству, которое присылает один байт настроек (значение x от 0 до 255). В этом байте упакованы два поля: level (3 младших бита) и mode (следующие 2 бита). Остальные биты — «служебные» и трогать их нельзя: при изменении level и mode они должны сохраниться.
Программа читает x, затем новые значения newLevel и newMode. Сначала печатает текущие level и mode, затем собирает новый байт x2, где заменены только эти поля, и выводит x2 в десятичном виде и в двоичном (ширина 8).
Требования:
• Программа должна считать три значения через fmt.Scan: x, newLevel, newMode, и хранить их в переменных типа uint.
• Программа должна вычислить текущие level и mode из x с помощью битовых операций: level = x & 0b0000_0111, mode = (x & 0b0001_1000) >> 3, и вывести оба значения (до формирования нового байта).
• Программа должна очистить в x только биты полей level и mode, используя оператор &^ с объединённой маской этих полей, чтобы остальные (служебные) биты сохранились без изменений.
• Программа должна принудительно ограничить newLevel до 3 бит (например, newLevel & 0b111) и newMode до 2 бит перед установкой их в байт.
• Программа должна собрать x2 из очищенного байта, установив новые поля через | и сдвиги, включая размещение mode как (newMode << 3), и сохранить тип результата как uint.
• Программа должна вывести x2 в десятичном виде и в двоичном виде шириной 8 бит, используя формат %08b для двоичного представления.
*/

import "fmt"

func main() {
	var x, newLevel, newMode uint
	fmt.Scan(&x, &newLevel, &newMode)

	// Достаём текущие поля из байта настроек.
	level := x & 0b0000_0111
	mode := (x & 0b0001_1000) >> 3
	fmt.Printf("%d %d\n", level, mode)

	// Маска всех битов полей level (0..2) и mode (3..4).
	fieldsMask := uint(0b0000_0111 | 0b0001_1000)

	// Очищаем только эти биты, служебные (остальные) не трогаем.
	cleared := x &^ fieldsMask

	// Принудительно ограничиваем новые значения шириной полей.
	newLevel &= 0b0000_0111 // 3 бита
	newMode &= 0b0000_0011  // 2 бита

	// Собираем новый байт: заменены только level и mode.
	x2 := cleared | newLevel | (newMode << 3)

	// Выводим в десятичном и двоичном виде (8 бит).
	fmt.Printf("%d %08b\n", x2, x2)
}