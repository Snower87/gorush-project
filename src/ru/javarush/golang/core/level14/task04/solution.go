package main

/*
= Задача №4. Точная подмена =
Вы поддерживаете систему «редактирования кода доступа» без лишних аллокаций. Вам приходит строка s, позиция i и строка repl. Нужно заменить ровно один байт в s — на первый байт строки repl. Но делать это надо по правилам Go: строка неизменяема, значит собираем новую строку из кусочков.
Программа читает три значения: строку s (без пробелов), целое число i и строку repl (без пробелов). Если i не в диапазоне [0, len(s)) или repl пустая, выведите OUT_OF_RANGE.
Если всё корректно, замените байт в позиции i на repl[0] и выведите получившуюся строку. Никаких преобразований в []byte: только срезы и конкатенация трёх частей s[:i] + string(repl[0]) + s[i+1:].
Требования:
• Программа должна считать из stdin ровно три значения в указанном порядке: строку s (без пробелов), целое число i и строку repl (без пробелов).
• До любых обращений к s[i] или до выполнения срезов s[:i] / s[i+1:] программа должна проверить, что i находится в диапазоне [0, len(s)).
• До обращения к repl[0] программа должна проверить, что len(repl) > 0.
• Если i вне диапазона [0, len(s)) или repl пустая, программа должна вывести ровно одну строку OUT_OF_RANGE и не выполнять замену.
• При корректных данных программа должна заменить байт в позиции i на первый байт repl (repl[0]) и собрать новую строку строго как s[:i] + string(repl[0]) + s[i+1:], используя байтовые индексы.
*/

import "fmt"

func main() {
	var s, repl string
	var i int

	// Читаем ровно три значения (s, i, repl).
	fmt.Scan(&s, &i, &repl)

	// Важно: сначала проверяем границы и repl, и только потом делаем срезы/индексацию.
	if i < 0 || i >= len(s) || len(repl) == 0 {
		fmt.Print("OUT_OF_RANGE")
		return
	}

	// TODO: Реализуйте замену ровно одного байта строки s в позиции i на первый байт строки repl.
	// TODO: Соберите результат через срезы и конкатенацию трех частей (без преобразования строк в []byte).
	// TODO: Нельзя использовать пакеты strings/bytes/strconv — допускается только fmt.
	s = s[:i] + string(repl[0]) + s[i+1:]

	// Временная заглушка: выводим исходную строку без изменений.
	fmt.Print(s)
}