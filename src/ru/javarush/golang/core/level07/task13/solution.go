package main

/*
= Задача №13. Сумма кассы =
Вы сводите отчёт в кассовой системе, где часть данных пришла как “обычный” int, а часть — как “расширенный” int64 (например, из базы). Go не позволит просто сложить их без явного решения, и это хорошо: меньше случайных ошибок.
Программа читает два целых числа: shortCount (тип int) и longCount (тип int64). Затем она явно переводит shortCount в int64, считает сумму в int64 и печатает две строки: сначала значение суммы, затем тип переменной суммы через "%T".
Требования:
• Программа должна прочитать два целых числа со stdin, используя fmt.Scan.
• Первое число должно храниться в переменной типа int (shortCount), второе — в переменной типа int64 (longCount).
• Сложение должно выполняться только после явного преобразования shortCount к int64 (например, int64(shortCount) + longCount), без попыток складывать int и int64 напрямую.
• Результат сложения должен сохраняться в отдельной переменной суммы типа int64.
*/

import "fmt"

func main() {
	var shortCount int
	var longCount int64

	fmt.Scan(&shortCount, &longCount)

	var sum int64
	// TODO: Посчитайте сумму в int64, явно приведя shortCount (int) к int64 перед сложением.
	sum = int64(shortCount) + longCount

	fmt.Println(sum)
	fmt.Printf("%T\n", sum)
}