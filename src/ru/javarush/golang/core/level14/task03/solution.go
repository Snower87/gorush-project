package main

/*
= Задача №3. Заглавный старт =
Вы делаете консольный "командный пульт" для мини-игры. Команды приходят как одно слово, и вам хочется, чтобы они выглядели аккуратно: если команда начинается с маленькой латинской буквы, сделайте её заглавной. Это не полноценный ToUpper, а простая "косметика" для ASCII.
Программа читает одну строку-команду (без пробелов) и выводит нормализованную версию. Если строка пустая — результат тоже пустой. Если строка не пустая и её первый байт лежит в диапазоне 'a'..'z', то первая буква становится заглавной ('a' → 'A', …, 'z' → 'Z'). Во всех остальных случаях строка выводится без изменений.
Результат нужно получить как новую строку (строки неизменяемы), собрав её из первого байта и хвоста исходной строки.
Требования:
• Программа должна прочитать одну строку-команду без пробелов через fmt.Scan или fmt.Fscan.
• Если введённая строка пустая (len(s) == 0), программа должна вывести пустую строку и не пытаться обращаться к s[0] или s[1:].
• Если строка не пустая, программа должна получить первый символ как первый байт через s[0] и сохранить его в переменной типа byte (или uint8).
• Если первый байт находится в диапазоне 'a'..'z', программа должна преобразовать его в 'A'..'Z' с помощью байтовой арифметики, без использования strings.ToUpper и аналогичных функций.
• Результат должен быть получен как новая строка, собранная выражением string(b) + s[1:], причём срез s[1:] должен использоваться только при len(s) > 0.
*/

import "fmt"

func main() {
	var s string
	fmt.Scan(&s) // по условию читаем одно "слово" (без пробелов)

	if len(s) == 0 {
		// Нельзя трогать s[0] и s[1:], если строка пустая.
		fmt.Print("")
		return
	}

	var b byte = s[0] // первый символ как первый байт

	// TODO: Если первый байт находится в диапазоне 'a'..'z', преобразуйте его в 'A'..'Z' с помощью байтовой арифметики (без strings.ToUpper и аналогов).
	// TODO: Соберите результат как новую строку из первого байта и хвоста исходной строки. Учтите, что s[1:] можно использовать только при len(s) > 0.
	if b >= 'a' && b <= 'z' {
	    b = b - 32
	    s = string(b) + s[1:]
	}

	// Временная реализация: выводит строку без нормализации.
	fmt.Print(s)
}