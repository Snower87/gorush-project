package main

/*
= Задача №3. Отчёт без тени =
Представьте, что вы делаете простой отчёт для панели мониторинга: оператор вводит одно число, а программа должна вывести его и “удвоенную версию” в одной строке — чтобы сразу видеть исходное значение и результат.
В этом задании есть маленькая ловушка из реальной разработки: вы хотите хранить текст отчёта в строковой переменной, но если назвать переменную так же, как импортированный пакет fmt, вы случайно “закроете” себе доступ к fmt.Printf и программа перестанет собираться. Поэтому имя строковой переменной должно быть нормальным и не конфликтовать с fmt.
Требования:
• Программа должна импортировать пакет fmt и использовать его для чтения входных данных и вывода результата.
• Программа должна считать из stdin ровно одно целое число типа int.
• Программа должна вычислить значение doubled как 2 * n на основе введённого числа.
• Программа должна вывести в stdout два значения (n и doubled), разделённые одним пробелом, и завершить вывод переводом строки, используя fmt.Printf или fmt.Sprintf.
• В коде должна быть строковая переменная, участвующая в формировании вывода (например, форматная строка или готовая строка), при этом её имя не должно быть fmt.
*/

import "fmt"

func main() {
	var n int
	fmt.Scan(&n)

	// TODO: Вычислите удвоенное значение числа n и запишите его в doubled.
	doubled := 2 * n

	// Важно: не называем строковую переменную "fmt", иначе затенится пакет fmt.
	outFormat := "%d %d\n"
	fmt.Printf(outFormat, n, doubled)
}