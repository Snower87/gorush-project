package main

/*
= Задача №14. Ёмкость не длина =
Вы готовите буфер под копирование данных, и кажется, что места достаточно: cap большой. Но copy смотрит не на cap, а на len. Это классическая ловушка: “память есть, а записывать некуда”, потому что длина нулевая.
Программа должна прочитать N, затем N целых чисел и сохранить их в src.
Далее программа должна создать dst так: dst := make([]int, 0, N). Затем обязательно сделать reslice dst до длины N, и только после этого выполнить n := copy(dst, src).
Программа должна вывести две строки: сначала число n, затем слайс dst через fmt.Println. При корректном решении n будет равно N, а dst будет содержать те же числа, что и src.
Требования:
• Программа должна прочитать целое число N, затем прочитать ровно N целых чисел и сохранить их в слайс src.
• Слайс src должен быть создан так, чтобы в него можно было записать N считанных чисел (например, make([]int, N) с последующим присваиванием по индексам).
• Программа должна создать dst строго как dst := make([]int, 0, N), то есть len(dst)=0 и cap(dst)=N.
• Перед вызовом copy программа должна обязательно изменить длину dst до N (например, dst = dst[:N]), чтобы copy копировал N элементов.
• Программа должна выполнить n := copy(dst, src) и сохранить возвращаемое значение в переменную n, которая далее используется в выводе.
*/

import (
	"bufio"
	"fmt"
	"os"
)

func main() {
	in := bufio.NewReader(os.Stdin)

	var N int
	fmt.Fscan(in, &N)

	src := make([]int, N)
	for i := 0; i < N; i++ {
		fmt.Fscan(in, &src[i])
	}

	dst := make([]int, 0, N)

	// TODO: Перед вызовом copy обеспечьте, чтобы длина dst была равна N (len(dst) == N).
	dst = dst[:N]

	n := copy(dst, src)

	fmt.Println(n)
	fmt.Println(dst)
}