package main

/*
= Задача №13. Дубликат списка =
Вы пишете маленькую утилиту для проверки “а точно ли копия — это копия?”. Пользователь вводит список чисел, а вы делаете второй список для экспериментов: в копии можно менять элементы, не боясь испортить оригинал.
Программа должна читать из stdin число N, а затем N целых чисел. Из них нужно собрать слайс src. Затем создать независимую копию dst: выделить для неё отдельный буфер через make и скопировать элементы через copy.
Если N > 0, программа должна изменить dst[0] на 999. После этого вывести две строки: сначала src, затем dst, используя fmt.Println, чтобы печать была в виде [1 2 3].
Требования:
• Программа должна прочитать из stdin число N, затем ровно N целых чисел.
• Программа должна собрать прочитанные N чисел в слайс src (при N=0 src должен быть корректным пустым слайсом).
• Программа должна создать слайс dst с len, равным len(src), выделив для него отдельный буфер через make, и скопировать элементы из src в dst функцией copy.
• Если N > 0, программа должна присвоить dst[0] значение 999, при этом значение src[0] не должно измениться.
• Программа должна вывести ровно две строки: сначала src, затем dst, используя fmt.Println так, чтобы формат вывода слайсов был вида [1 2 3].
• Программа не должна обращаться к dst[0] при N=0 и не должна падать (panic) при пустом вводе списка.
*/

import (
	"bufio"
	"fmt"
	"os"
)

func main() {
	in := bufio.NewReader(os.Stdin)

	var n int
	fmt.Fscan(in, &n)

	src := make([]int, n)
	for i := 0; i < n; i++ {
		fmt.Fscan(in, &src[i])
	}

	// TODO: Создайте независимую копию dst: выделите отдельный буфер через make и скопируйте элементы из src с помощью copy.
	dst := make([]int, len(src))
	copy(dst, src)

	if n > 0 {
		dst[0] = 999
	}

	fmt.Println(src)
	fmt.Println(dst)
}