package main

/*
= Задача №16. Сканер датчика =
Вы пишете консольный сканер датчиков: датчик шлёт числа потоком. 0 означает “стоп, сеанс закончен”. Отрицательные значения считаются помехами и должны быть проигнорированы. А положительные — это полезные измерения, их нужно посчитать и суммировать.
Программа читает целые числа из stdin, пока не встретится число 0 или пока ввод не закончится. Программа считает только положительные числа: если x > 0, оно учитывается в статистике; если x < 0, оно игнорируется с помощью continue; если x == 0, чтение завершается. После завершения программа должна вывести одну строку формата count=<количество_положительных> sum=<сумма_положительных>.
Требования:
• Программа должна читать входные числа в цикле вида `for { ... }`, без заранее известного количества итераций.
• В каждой итерации цикла программа должна читать ровно одно целое число через `fmt.Scan(&x)` и проверять ошибку чтения.
• Программа должна завершать цикл чтения в двух случаях: когда прочитано `x == 0` и когда `fmt.Scan` вернул ошибку (например, EOF).
• Если прочитано `x < 0`, программа должна пропустить обработку этого значения с помощью `continue`, не изменяя статистику и не создавая риска “залипания” цикла.
• Только числа `x > 0` должны увеличивать счётчик положительных и добавляться к сумме; значения `x <= 0` не должны попадать в статистику.
*/

import "fmt"

func main() {
	var (
		x     int
		count int
		sum   int
	)

	for { // потенциально бесконечный цикл чтения
		if _, err := fmt.Scan(&x); err != nil { // 1 число за итерацию; ошибка (например EOF) — выход
			break
		}

		if x == 0 {
			break
		}

		if x < 0 {
			// TODO: Отрицательные значения нужно игнорировать с помощью continue (не менять статистику).
			continue
		}

		if x > 0 {
			// TODO: Учитывайте только положительные значения: увеличьте count и добавьте x к sum.
			count++
			sum += x
		}
	}

	fmt.Printf("count=%d sum=%d", count, sum)
}