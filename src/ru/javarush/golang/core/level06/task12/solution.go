package main

/*
= Задача №12. Среднее плюсов =
Вы делаете отчёт для датчиков качества: нужно посчитать среднее значение только среди “хороших” показаний — строго больше 0. Показания вещественные (float64), и положительных может не оказаться вообще.
Программа читает N, затем N чисел float64. Считайте среднее только среди положительных.
Используйте переменные, объявленные через var без явной инициализации: sum (float64) — сумма положительных, count (float64) — количество положительных (счётчик тоже float64, чтобы пока не заниматься преобразованиями типов), hasAny (bool) — был ли найден хотя бы один положительный.
Если положительных чисел нет, выведите 0. Иначе выведите sum / count.
Требования:
• Программа должна прочитать через fmt.Scan сначала N типа int, затем ровно N значений типа float64.
• Переменные sum (float64), count (float64) и hasAny (bool) должны быть объявлены через var без явной инициализации.
• В расчёт должны попадать только значения строго больше 0; для каждого такого значения нужно прибавлять его к sum, увеличивать count на 1.0 и устанавливать hasAny в true.
• Деление sum / count должно выполняться только если count не равно 0 (то есть есть хотя бы одно положительное значение).
• Если положительных значений нет, программа должна вывести одной строкой значение 0.
*/

import "fmt"

func main() {
	var n int
	fmt.Scan(&n)

	// Важно: объявлены через var без явной инициализации (zero values).
	var sum float64   // сумма положительных
	var count float64 // количество положительных (тоже float64 по условию)
	var hasAny bool   // был ли хотя бы один положительный

	for i := 0; i < n; i++ {
		var x float64
		fmt.Scan(&x)

		// TODO: Реализуйте отбор только строго положительных значений (> 0).
		// TODO: Для положительных значений обновляйте sum, count (на 1.0) и hasAny.
		if x > 0 {
		    sum += x
		    count++
		    hasAny = true
		}
	}

	// TODO: Реализуйте вывод: если положительных нет — вывести 0, иначе вывести sum / count (делить только если count != 0).
	if !hasAny || count == 0 {
		fmt.Println(0)
		return
	}

	fmt.Println(sum / count)
}