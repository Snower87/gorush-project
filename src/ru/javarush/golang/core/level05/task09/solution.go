/*
= Задача №9. Индексы посылки =
Вы пишете утилиту для отладки: в систему приходит короткий "код посылки" — строка без пробелов, только ASCII. Вам нужно увидеть, какие индексы выдаёт обход строки через range, чтобы убедиться, что всё читается правильно и без сюрпризов.
Программа читает строку trackingCode и выводит все индексы, которые выдаёт for i := range trackingCode, каждый индекс — на отдельной строке. Никаких подписей: только числа в правильном порядке.
Требования:
• Программа должна прочитать одну строку trackingCode из stdin с помощью fmt.Scan (без использования bufio, os.Stdin и т.п.).
• Программа должна импортировать и использовать пакет fmt для ввода и вывода.
• Программа должна обходить строку trackingCode ровно циклом вида for i := range trackingCode { ... }.
• Внутри цикла программа должна выводить только индекс i, причём каждый индекс должен быть напечатан на отдельной строке и в том порядке, в котором range обходит строку.
• Нельзя использовать классический цикл for с i++ и нельзя задавать количество итераций через len(trackingCode) (например, for i := 0; i < len(trackingCode); i++ запрещён).
*/

import "fmt"

func main() {
	var trackingCode string
	fmt.Scan(&trackingCode)

	// Обходим строку только через range.
	for i := range trackingCode {
		// TODO: Выведите индекс i (каждый индекс на новой строке) без лишнего вывода.
		fmt.Println(i)
	}
}