package main

/*
= Задача №19. Охота на контроль =
Вы отлаживаете текст, который приходит из разных источников: кто-то вставил табы, кто-то прислал "\r\n", а кто-то добавил лишний перевод строки. Нужно пройтись по строке именно как по Unicode‑символам (рунам) и найти управляющие символы, не меняя исходный текст.
Программа читает одну строку целиком через ReadString('\n'), затем проходит по строке циклом for range (руны, не байты) и ищет символы '\t', '\r', '\n'. Для каждого найденного символа выведите отдельную строку:
pos=<байтовый индекс> rune=<символ в %q> code=<код руны как число>.
Если ни одного такого символа нет, выведите ровно одну строку clean.
Требования:
• Программа должна создать bufio.Reader (например, поверх os.Stdin) и прочитать одну строку целиком методом ReadString('\n').
• Если ReadString('\n') вернул ошибку, программа должна корректно обработать её и завершиться с понятным сообщением (не продолжать обработку “как будто всё нормально”).
• Программа должна обходить строку циклом for i, r := range s, где i используется как байтовый индекс позиции (pos), а r — текущая руна (Unicode-символ).
• В процессе анализа программа должна искать только руны '\t', '\r', '\n' и при этом не должна изменять исходную строку (нельзя использовать TrimSpace/Replace и т.п.).
• Для каждого найденного управляющего символа программа должна вывести отдельную строку строго в формате: pos=<i> rune=<r в %q> code=<код руны в %d>.
*/

import (
	"bufio"
	"fmt"
	"io"
	"os"
)

func main() {
	rd := bufio.NewReader(os.Stdin)

	// Читаем строку целиком до '\n' (если '\n' нет — возможен io.EOF, но строка может быть полезной).
	s, err := rd.ReadString('\n')
	if err != nil && err != io.EOF {
		fmt.Fprintln(os.Stderr, "read error:", err)
		os.Exit(1)
	}

	found := false

	// Важно: range по строке идёт по рунам, а i — байтовый индекс.
	for i, r := range s {
		// TODO: Найдите управляющие символы '\t', '\r', '\n' при обходе строки по рунам.
		// TODO: Для каждого найденного символа выведите отдельную строку в требуемом формате и отметьте found=true.
		if r == '\t' || r == '\r' || r == '\n' {
		    fmt.Printf("pos=%d rune=%q code=%d\n", i, r, r)
		    found = true
		}
	}

	if !found {
		fmt.Println("clean")
	}
}