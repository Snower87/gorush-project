package main

import "fmt"

/*
= Задача №4. План на день =
У вас есть список дел на день, и он уже “забит в приложение”:
tasks := []string{"learn Go", "write code", "drink water", "sleep", "repeat"}
Пользователь выбирает диапазон задач “по‑человечески”: номера начинаются с 1, а границы включительные (например, from=2 и to=4 — это задачи №2, №3 и №4).
Программа должна считать два целых числа from и to, перевести их в границы для среза как a := from - 1 и b := to, а затем при корректных границах вывести выбранные задачи по одной на строку в формате "<номер>: <текст>".
Выбор диапазона обязан быть сделан через tasks[a:b].
Если диапазон некорректный (from < 1, to < 1, from > to, to > len(tasks)), программа должна вывести [] одной строкой.
Требования:
• Программа должна использовать ровно один заранее заданный слайс tasks из 5 строковых элементов: []string{"learn Go", "write code", "drink water", "sleep", "repeat"}; добавлять/удалять/изменять элементы tasks нельзя (только чтение и вывод).
• Программа должна считать два целых числа from и to через fmt.Scan(&from, &to).
• Программа должна явно вычислить границы для среза через отдельные переменные a := from - 1 и b := to (перевод 1-based → 0-based).
• Перед выполнением tasks[a:b] программа должна проверить диапазон на корректность, исключающую panic: from >= 1, to >= 1, from <= to, to <= len(tasks) (в т.ч. должен быть допустим диапазон из одного элемента, from == to).
• При корректных границах выбор задач обязан выполняться именно через выражение tasks[a:b], без ручной фильтрации/подбора элементов без использования среза.
*/

func main() {
	// Заранее заданный список задач (менять нельзя).
	tasks := []string{"learn Go", "write code", "drink water", "sleep", "repeat"}

	var from, to int
	fmt.Scan(&from, &to)

	// Переводим "человеческие" границы (1-based, включительно) в границы среза.
	a := from - 1
	b := to

	// TODO: Реализуйте проверку корректности диапазона ДО нарезки среза, чтобы исключить panic.
	// TODO: Если диапазон некорректный — выведите ровно одну строку: [] и завершите программу.
	// TODO: Если диапазон корректный — выберите задачи через срез и выведите их построчно в формате "<номер>: <текст>".
	if from < 1 || to < 1 || from > to || to > len(tasks) {
	    fmt.Println("[]")
	    return
	}
	sel := tasks[a:b]
	for i, val := range sel {
	    fmt.Printf("%d: %s\n", i + a, val)
	}
}