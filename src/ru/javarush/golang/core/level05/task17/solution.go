package main

/*
= Задача №17. Кошелёк за день =
Вы делаете консольный "кошелёк" для учёта операций за день: сначала кассир вводит, сколько операций было (n), а затем вводит суммы каждой операции (они могут быть отрицательными — например, возвраты).
Программа должна быстро посчитать итоговый баланс дня, не сохраняя весь список целиком.
Программа читает целое operationsCount, затем читает ровно operationsCount целых чисел — значений операций. Нужно вывести сумму всех введённых чисел одним целым числом.
Требования:
• Код должен быть оформлен в package main и содержать функцию func main().
• Программа должна прочитать целое число operationsCount, а затем прочитать ровно operationsCount целых чисел (значений операций) с использованием fmt.Scan.
• Должен использоваться цикл for, который выполняется ровно operationsCount раз для чтения и обработки каждой операции.
• Переменная totalSum должна быть инициализирована нулём до начала цикла и на каждой итерации обновляться добавлением очередного введённого числа; при этом нельзя использовать массивы/слайсы для хранения всех операций.
• Программа должна корректно учитывать отрицательные целые числа во входных данных при подсчёте итоговой суммы.
*/

import "fmt"

func main() {
	var operationsCount int
	fmt.Scan(&operationsCount)

	totalSum := 0

	for i := 0; i < operationsCount; i++ {
		var op int
		fmt.Scan(&op)

		// TODO: Добавьте op к totalSum (накапливайте сумму операций без хранения списка).
		// TODO: Убедитесь, что отрицательные значения учитываются корректно.
		totalSum += op
	}

	fmt.Println(totalSum)
}