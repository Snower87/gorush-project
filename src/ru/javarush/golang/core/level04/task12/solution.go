package main

/*
= Задача №12. Ремонт карты =
В вашей игре карта уровня хранится строкой: символ # означает сломанную плитку, и движок требует заменить её на *, чтобы пометить как “временно закрыто”. При этом разработчикам важно знать, сколько плиток пришлось заменить, чтобы оценить масштаб проблемы.
Программа читает из stdin одну строку levelRow без пробелов и строит новую строку fixedRow, проходя по levelRow циклом for range. Каждый символ '#' заменяется на '*', остальные символы копируются без изменений. После обработки нужно вывести: в первой строке fixedRow, во второй строке — количество замен replacedCount.
Требования:
• Программа должна импортировать пакет fmt и прочитать из stdin одну строку levelRow без пробелов с помощью fmt.Scan.
• Программа должна обойти levelRow циклом вида for _, v := range levelRow, используя v как текущий символ (rune).
• При обходе, если v == '#', программа должна добавить в fixedRow символ "*" и увеличить счётчик replacedCount на 1; иначе программа должна добавить в fixedRow string(v) без изменений.
• Программа должна собрать новую строку fixedRow последовательно по мере обхода исходной строки, сохраняя порядок символов и выполняя только описанные замены.
• Программа должна вывести fixedRow в первой строке, а replacedCount — во второй строке.
*/

import "fmt"

func main() {
	var levelRow string
	fmt.Scan(&levelRow)

	var fixedRow string
	var replacedCount int

	for _, v := range levelRow {
		//_ = v
		// TODO: Реализуйте обработку каждого символа строки через range.
		// TODO: Если текущий символ равен '#', добавьте в fixedRow "*" и увеличьте replacedCount.
		// TODO: Иначе добавьте в fixedRow текущий символ без изменений (как строку).
		if v == '#' {
		    fixedRow += "*"
		    replacedCount++
		} else {
		    fixedRow += v
		}
	}

	fmt.Println(fixedRow)
	fmt.Println(replacedCount)
}