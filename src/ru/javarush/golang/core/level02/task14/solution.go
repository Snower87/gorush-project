package main

/*
= Задача №14.Метка заказа =
Вы печатаете “человеческий” код заказа для склада. На вход приходит обычное число orderNumber, а на наклейке оно должно
выглядеть как строка с префиксом: ORD-7, ORD-42 и так далее.
Программа должна прочитать одно целое число orderNumber, преобразовать его в строку через strconv.Itoa, склеить с "ORD-"
и вывести результат одной строкой.
Требования:
• Программа должна прочитать ровно одно целое число orderNumber типа int из stdin с помощью fmt.Scan.
• Программа должна преобразовать orderNumber в строку строго через strconv.Itoa; запрещено использовать string(orderNumber).
• Программа должна сформировать итоговую строку метки конкатенацией через оператор + в формате "ORD-" + <строковый номер> (например, ORD-7).
• Программа должна вывести ровно одну строку с готовой меткой без лишних слов и без дополнительного форматирования.
*/

import (
	"fmt"
	"strconv"
)

func main() {
	var orderNumber int
	fmt.Scan(&orderNumber)

	// TODO: Преобразуйте orderNumber в строку строго через strconv.Itoa и сохраните в переменную.
	orderStr := strconv.Itoa(orderNumber)
	_ = orderStr

	// TODO: Соберите итоговую метку заказа конкатенацией через + в формате "ORD-" + <строковый номер>.
	label := "ORD-" + orderStr

	// TODO: Выведите ровно одну строку с готовой меткой без лишних слов и форматирования.
	fmt.Println(label)
}