package main

/*
= Задача №6. Граница индекса =
Вы пишете защиту для робота-курьера: у него есть небольшой буфер команд, и обращаться можно только к реально существующим элементам. Даже если cap больше, это не значит, что можно лазить по индексам дальше len.
В программе без ввода создайте слайс nums через make([]int, 2, 5), затем присвойте nums[0]=7 и nums[1]=8. После этого выведите строку len=<...> cap=<...> data=<...>, где data — сам слайс.
Далее вычислите индекс i = 2. Если i не попадает в допустимый диапазон индексов (то есть i >= len(nums)), выведите отдельной строкой cannot access index 2. Если вдруг индекс допустим, выведите значение nums[i].
Требования:
• В программе без ввода должен быть создан слайс nums выражением make([]int, 2, 5).
• После создания слайса программа должна присвоить nums[0] = 7 и nums[1] = 8.
• Программа должна вывести первую строку формата len=<...> cap=<...> data=<...>, используя len(nums) и cap(nums); вывод должен выполняться через fmt.Printf и содержать data=[7 8].
• Программа должна вычислить i = 2 и выполнить ветвление через if/else, проверяя условие допустимости доступа только как i < len(nums).
• Если i >= len(nums), программа должна вывести отдельной строкой ровно: cannot access index 2.
*/

import "fmt"

func main() {
	nums := make([]int, 2, 5)

	// TODO: Заполните nums[0] и nums[1] значениями 7 и 8.
	nums[0] = 7
	nums[1] = 8

	fmt.Printf("len=%d cap=%d data=%v\n", len(nums), cap(nums), nums)

	i := 2
	// TODO: Реализуйте проверку доступа к nums[i] через if/else.
	// TODO: Условие допустимости должно быть строго i < len(nums) (не используйте cap).
	// TODO: Если индекс недопустим — выведите ровно "cannot access index 2", иначе — выведите nums[i].
	if i < len(nums) {
	    fmt.Println(nums[i])
	} else {
	    fmt.Println("cannot access index 2")
	}
}