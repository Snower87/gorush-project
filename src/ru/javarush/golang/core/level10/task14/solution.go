package main

/*
= Задача №14. Префикс в init =
У вас есть консольная утилита, которая печатает сообщения с “фирменным” префиксом. Команда решила подготовить этот префикс заранее, ещё до входа в main, чтобы в main не было лишней возни.
Программа читает одно слово и выводит его, но перед ним обязательно добавляет префикс, который задаётся в init() через глобальную переменную prefix.
Пример:
Ввод:
gopher
Вывод:
>> gopher
Требования:
• В программе должна быть объявлена глобальная переменная prefix типа string.
• Программа должна содержать функцию init(), которая присваивает prefix значение ">> " и при этом не читает данные из stdin.
• Функция init() не должна содержать условий (if/switch) и циклов (for/range).
• Функция main() должна прочитать ровно одно значение (одно слово) с помощью fmt.Scan или fmt.Fscan.
• Функция main() должна формировать вывод, используя значение prefix, установленное до входа в main() (то есть не задавать префикс локально и не вычислять его заново).
*/

import "fmt"

// Глобальный префикс, задаётся до входа в main().
var prefix string

func init() {
	// TODO: Инициализируйте глобальную переменную prefix в init() без чтения stdin.
	// TODO: В init() нельзя использовать условия (if/switch) и циклы (for/range).
	prefix = ">> "
}

func main() {
	var word string
	fmt.Scan(&word) // читаем ровно одно слово

	// TODO: Используйте подготовленный глобальный prefix (инициализированный до main()).
	fmt.Println(prefix + word)
}