package main

/*
= Задача №17. Общий буфер =
Вы ведёте маленький “планировщик задач” и хотите наглядно увидеть, почему под‑слайсы иногда ведут себя как “тот же список, просто с другой рамкой”. У вас есть общий список tasks, а today — это только его начало. Кажется, что вы добавляете элемент только в today, но внезапно меняется и tasks.
Программа должна создать слайс tasks длины 4 и ёмкости 4 через make и заполнить его строками: "A", "B", "C", "D". Затем программа должна создать под‑слайс today как tasks[:2] и добавить в today строку "X" через append, сохранив результат обратно в today.
Программа должна вывести 4 строки: состояние tasks и today до append и после append. Формат вывода: "<имя>: len=<...> cap=<...> <слайс>", чтобы было видно и содержимое, и размеры.
Требования:
• Программа должна импортировать пакет fmt и использовать его для вывода.
• Программа должна создать слайс base строго выражением make([]int, 2, 2) и записать в него значения 10 и 20 (чтобы base стал [10 20]).
• Программа должна создать под‑слайс sub строго как base[:] до выполнения append.
• Программа должна выполнить расширение под‑слайса строго операцией sub = append(sub, 30) (результат append обязательно присваивается обратно в sub).
*/

import "fmt"

// dump печатает слайс так, чтобы было видно len/cap и содержимое.
func dump(name string, s []string) {
	fmt.Printf("%s: len=%d cap=%d %v\n", name, len(s), cap(s), s)
}

func main() {
	// tasks: len=4, cap=4 (заполняем без литерала []string{...})
	tasks := make([]string, 4, 4)
	tasks[0] = "A"
	tasks[1] = "B"
	tasks[2] = "C"
	tasks[3] = "D"

	// TODO: Создайте под-слайс today ровно как tasks[:2], без копирования и без ограничения cap.
	today := tasks[:2]

	dump("tasks(before)", tasks)
	dump("today(before)", today)

	// TODO: Сохраните результат append обратно в today и добавьте строку "X".
	today = append(today, "X")

	dump("tasks(after)", tasks)
	dump("today(after)", today)
}