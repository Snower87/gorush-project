package main

/*
= Задача №17. Перестановка мест =
Вы пишете мини-игру "перемешай рычаги": на вход приходит два целых числа a и b — значения в двух слотах. По сценарию игрок нажимает кнопку, и значения должны поменяться местами мгновенно, без "третьего кармана" для временного хранения.
Программа должна считать из stdin два целых числа a и b, затем обменять их местами с помощью множественного присваивания в одной строке (идея вида a, b = b, a), не используя третью временную переменную.
После обмена программа должна вывести одну строку формата: a=<значение> b=<значение>
Требования:
• Программа должна импортировать пакет fmt и считать из stdin два целых числа a и b с помощью fmt.Scan.
• Программа должна поменять местами значения a и b, не используя третью временную переменную (tmp или аналог).
• Обмен значений должен быть выполнен одной операцией множественного присваивания в одной строке (например, a, b = b, a).
• После обмена программа должна вывести ровно одну строку строго в формате: a=<значение> b=<значение>, где между частями ровно один пробел.
• Вывод должен завершаться переводом строки, а значения должны выводиться как целые числа (int) через %d или эквивалентным способом.
*/

import "fmt"

func main() {
	var a, b int

	// Читаем два целых числа из stdin
	fmt.Scan(&a, &b)

	// TODO: Поменяйте местами значения a и b без третьей временной переменной.
	// TODO: Обмен должен быть выполнен в одной строке через множественное присваивание.
	a, b = b, a

	// Строгий формат вывода + перевод строки
	fmt.Printf("a=%d b=%d\n", a, b)
}