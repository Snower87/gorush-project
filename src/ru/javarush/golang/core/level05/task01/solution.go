package main

/*
= Задача №1. Журнал испытаний =
Вы пишете маленькую консольную утилиту для инженера-испытателя: он запускает тестовый стенд, а программа должна аккуратно записать в лог, сколько раз стенд "провернул цикл".
Инженер вводит число N, и вы печатаете ровно N строк — каждая строка показывает номер итерации, чтобы потом легко сверять отчёты.
На каждой строке должен быть строго текст вида Iteration <номер>, где нумерация начинается с 1.
Если инженер ввёл 0, значит испытание не запускалось — в этом случае программа вообще ничего не печатает.
Требования:
• Программа должна считать одно целое число N из стандартного ввода, используя fmt.Scan.
• Программа должна использовать цикл вида for init; cond; post, где счётчик объявлен прямо в init (например, i := 1), шаг — i++, а условие обеспечивает выполнение ровно N итераций.
• На первой напечатанной строке должен быть номер 1, далее номера должны увеличиваться на 1 на каждой итерации, пока не будет выведен номер N.
• На каждой итерации программа должна печатать ровно одну строку строго вида Iteration <номер> с переводом строки (без дополнительных символов, слов, пробелов или пустых строк).
• Если введено N = 0, программа не должна печатать вообще ничего (никаких строк, включая перевод строки).
*/

import "fmt"

func main() {
	var n int
	fmt.Scan(&n)

	// TODO: Выведите ровно N строк в stdout.
	// TODO: Каждая строка должна иметь строгий формат: "Iteration <номер>" + перевод строки.
	// TODO: Нумерация должна начинаться с 1 и увеличиваться на 1 на каждой итерации.
	// TODO: Используйте классический цикл for с init/cond/post (счётчик объявите в init).
	// TODO: При N = 0 не выводите вообще ничего.

	for i := 1; i <= n; i++ {
	    fmt.Printf("Iteration %d\n", i)
	}

}