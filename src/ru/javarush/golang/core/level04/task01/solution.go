package main

/*
= Задача №1. Лифт по этажам =
Вы пишете маленькую консольную панель для старого лифта в бизнес-центре. Охранник вводит число N — до какого этажа нужно
“пробежаться” в отчёте, чтобы проверить, что табло этажей работает без пропусков.
Программа читает одно целое число topFloor и печатает все целые числа от 1 до topFloor включительно, каждое с новой строки.
Если topFloor меньше 1, лифт “никуда не едет” — не нужно выводить вообще ничего.
Требования:
• Программа должна прочитать одно целое число topFloor из stdin с помощью fmt.Scan.
• Если topFloor меньше 1, программа не должна выводить ничего (вообще ни одной строки).
• Если topFloor больше либо равно 1, программа должна вывести все целые числа от 1 до topFloor включительно, по одному числу на строку, используя fmt.Println.
• Для обхода этажей должен использоваться классический цикл for с тремя частями (init; cond; post), без бесконечных циклов.
• Счётчик floor должен быть объявлен в init (например, floor := 1), а изменение счётчика должно быть выполнено через floor++.
*/

import "fmt"

func main() {
	var topFloor int
	fmt.Scan(&topFloor)

	if topFloor < 1 {
		return
	}

	// TODO: Настройте границы цикла так, чтобы обходить этажи от 1 до topFloor включительно.
	for floor := 1; floor <= topFloor; floor++ {
		// TODO: Выведите текущий этаж в отдельной строке с помощью fmt.Println.
		fmt.Println(floor)
	}
}