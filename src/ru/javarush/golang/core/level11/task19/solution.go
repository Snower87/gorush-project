package main

/*
= Задача №19. Счётчик роста =
Вы оптимизируете производительность: каждый раз, когда append увеличивает ёмкость слайса, Go может выделить новый backing array и скопировать элементы. Вы хотите увидеть, насколько часто это происходит в двух сценариях — без предвыделения и с предвыделением.
На вход подаётся целое число n. Программа должна два раза «нарастить» слайс int числами 0, 1, 2, …, n-1 через append и посчитать, сколько раз менялась ёмкость cap во время роста.
Первый сценарий: стартовать с var a []int (без предвыделения). Второй сценарий: стартовать с b := make([]int, 0, n) (с предвыделением). В обоих сценариях при каждом append сравнивайте текущий cap с предыдущим значением и увеличивайте счётчик, если cap изменился.
Программа должна вывести одну строку в формате no_prealloc=<x> prealloc=<y>.
Требования:
• Программа должна считать из stdin одно целое число n, определяющее количество добавляемых элементов (от 0 до n-1).
• Программа должна создать слайс как var a []int и добавить в него числа 0..n-1 в цикле строго через a = append(a, i), считая количество изменений ёмкости cap(a) по сравнению с prevCap.
• Программа должна создать слайс строго как b := make([]int, 0, n) и добавить в него числа 0..n-1 в цикле строго через b = append(b, i), считая количество изменений ёмкости cap(b) по сравнению с prevCap.
• В каждом сценарии программа должна использовать переменную prevCap и увеличивать счётчик ровно в момент, когда cap(s) != prevCap, при этом в качестве результата выводится число изменений cap, а не итоговое значение cap.
• Логика подсчёта должна опираться только на факт изменения cap (через сравнение cap(s) != prevCap) и не должна содержать предположений о том, как именно Go увеличивает ёмкость.
*/

import (
	"bufio"
	"fmt"
	"os"
)

func main() {
	in := bufio.NewReader(os.Stdin)
	out := bufio.NewWriter(os.Stdout)
	defer out.Flush()

	var n int
	fmt.Fscan(in, &n)

	// Сценарий без предвыделения: var a []int
	var a []int
	prevCapA := cap(a)
	noPrealloc := 0

	for i := 0; i < n; i++ {
		a = append(a, i)

		// TODO: Реализуйте подсчёт количества изменений ёмкости cap(a) относительно prevCapA.
		// TODO: Счётчик noPrealloc должен увеличиваться только когда cap(a) изменился.
		// TODO: Не забудьте обновлять prevCapA после фиксации изменения.
		if cap(a) != prevCapA {
		    noPrealloc++
		    prevCapA = cap(a)
		}
	}

	// Сценарий с предвыделением: b := make([]int, 0, n)
	b := make([]int, 0, n)
	prevCapB := cap(b)
	prealloc := 0

	for i := 0; i < n; i++ {
		b = append(b, i)

		// TODO: Реализуйте подсчёт количества изменений ёмкости cap(b) относительно prevCapB.
		// TODO: Счётчик prealloc должен увеличиваться только когда cap(b) изменился.
		// TODO: Не забудьте обновлять prevCapB после фиксации изменения.
		if cap(b) != prevCapB {
		    prealloc++
		    prevCapB = cap(b)
		}
	}

	fmt.Fprintf(out, "no_prealloc=%d prealloc=%d", noPrealloc, prealloc)
}