/*
= Задача №6. Касса до нуля =
Представьте маленькую кассу на мероприятии: волонтёр вводит суммы операций по одной (пополнения и возвраты могут быть и отрицательными), а когда смена заканчивается — вводит 0 как команду “стоп”. Ноль не является операцией, он только завершает ввод.
Программа читает целые числа по одному, накапливает сумму и останавливается, когда встречает 0. После остановки выводит итоговую сумму одной строкой.
Требования:
• Программа должна читать из stdin целые числа по одному (разделённые пробелами и/или переводами строк) до тех пор, пока не встретит число 0.
• Чтение каждого следующего числа должно выполняться внутри бесконечного цикла вида for { ... }.
• После чтения очередного числа программа должна проверить условие x == 0 и при выполнении завершить цикл через break, при этом значение 0 не должно добавляться к накопленной сумме.
• Программа должна накапливать сумму всех введённых чисел (включая отрицательные, если они введены) в переменной типа int.
• После завершения ввода программа должна вывести итоговую сумму одним числом и переводом строки.
*/

import "fmt"

func main() {
	sum := 0

	for { // бесконечный цикл ввода (по условию/требованию)
		var x int
		_, err := fmt.Scan(&x)
		if err != nil {
			// TODO: Обработайте завершение ввода: программа должна корректно завершиться и вывести текущую сумму.
			break
		}
		// TODO: Реализуйте логику остановки по команде 0 (0 не добавлять в сумму).
		// TODO: Реализуйте накопление суммы для всех остальных значений (включая отрицательные).
		if x == 0 {
		    break
		}
        sum += x
	}

	fmt.Println(sum)
}