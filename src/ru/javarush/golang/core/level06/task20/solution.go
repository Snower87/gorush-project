package main

/*
= Задача №20. Флаг отладки =
Вы собираете крошечное CLI-приложение, у которого есть имя и глобальный режим отладки. Название приложения — это строка appName, а флаг отладки — глобальная переменная debug. Внутри main пользователь вводит число flag (0 или 1), и программа должна включить или выключить режим, а затем красиво сообщить, в каком режиме она работает.
Объявите на уровне пакета (снаружи main) две переменные: appName (строка, сразу с заданным значением, например "MiniApp") и debug (bool, по умолчанию false). Затем в main считайте flag. Если flag == 1, глобальная debug становится true, а строка mode — "debug". Если flag == 0, глобальная debug становится false, а mode — "release".
После этого выведите одну строку: <appName> mode=<mode>
Отдельная важность задания — защита от shadowing: обновлять глобальную debug нужно через =, чтобы случайно не создать локальную переменную с тем же именем.
Требования:
• В пакете (вне main) должны быть объявлены две переменные через var: appName типа string с заданным значением (например, "MiniApp") и debug типа bool, оставленная со значением по умолчанию false.
• В main должна быть объявлена переменная flag (целого типа) и выполнено чтение значения через fmt.Scan(&flag); входное значение предполагается 0 или 1.
• В main должна быть объявлена переменная mode типа string; далее через if/else при flag == 1 нужно установить глобальную debug в true и mode в "debug", а при flag == 0 установить глобальную debug в false и mode в "release"; обновление debug должно выполняться через оператор = (и при желании допускается множественное присваивание debug, mode = ..., ...).
• Программа должна вывести ровно одну строку в stdout в формате "%s mode=%s\n", подставив appName и mode.
*/

import "fmt"

var appName string = "MiniApp"
var debug bool // по умолчанию false

func main() {
	var flag int
	fmt.Scan(&flag)

	var mode string

	// TODO: Установите глобальную переменную debug и строку mode в зависимости от значения flag (0 или 1).
	// TODO: Важно: не создавайте новую локальную переменную debug через ":=" — нужно обновлять глобальную через "=".
	if flag == 1 {
	    debug, mode = true, "debug"
	}
	if flag == 0 {
	    debug = false
	    mode = "release"
	}

	fmt.Printf("%s mode=%s\n", appName, mode)
}