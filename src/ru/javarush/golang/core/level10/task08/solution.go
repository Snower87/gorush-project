package main

/*
= Задача №8. Имя по пути =
Вы делаете учебный “сканер зависимостей”: вам дают список import path’ов, а вы печатаете, каким именем пакета обычно будут пользоваться в коде.
По правилу “последнего сегмента” всё просто: берём кусок строки после последнего символа /. Если / нет — имя совпадает со всей строкой.
Программа читает число n, затем n строк с путями, и для каждого пути печатает ожидаемое имя пакета с новой строки.
Требования:
• Программа должна читать через fmt.Scan сначала целое число n, затем ровно n строк import path’ов.
• Программа должна использовать цикл for, чтобы обработать каждый из n путей без пропусков и без чтения лишних строк.
• Для каждого пути программа должна найти индекс последнего символа “/” с помощью strings.LastIndex и вывести подстроку path[i+1:], а если “/” отсутствует — вывести весь путь как имя пакета.
• Программа должна печатать результат для каждого пути отдельной строкой (с переводом строки после каждого имени пакета).
• Программа должна импортировать только пакеты fmt и strings, без использования любых других пакетов.
*/

import (
	"fmt"
	"strings"
)

func main() {
	var n int
	fmt.Scan(&n)

	for i := 0; i < n; i++ {
		var path string
		fmt.Scan(&path)

		lastSlash := strings.LastIndex(path, "/")

		// TODO: По правилу "последнего сегмента" выведите ожидаемое имя пакета.
		// TODO: Используйте strings.LastIndex для поиска последнего символа "/".
		// TODO: Если "/" нет, нужно вывести весь путь; если есть — вывести только последний сегмент.
		if lastSlash == -1 {
		    fmt.Println(path)
		} else {
		    fmt.Println(path[lastSlash+1:])
		}
	}
}