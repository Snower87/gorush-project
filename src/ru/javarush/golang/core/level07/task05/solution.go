package main

/*
= Задача №5. Два датчика =
Вы подключаете к проекту два виртуальных датчика, которые оба показывают 0.1, но один работает в стандартной точности, а второй — в облегчённом режиме и хранит данные как float32. Ваша задача — вывести показания и типы так, чтобы сразу было видно: одинаковая запись в коде ещё не означает одинаковый тип.
Программа не читает ввод. Она создаёт переменную defaultReading через := со значением 0.1 и переменную liteReading с явным типом float32 и тем же значением. Затем печатает две строки одинакового формата: значение и тип.
Требования:
• Программа должна быть объявлена в пакете main и содержать функцию func main().
• В main нужно создать переменную defaultReading с помощью короткого объявления (:=) и присвоить ей значение 0.1 (без явного указания типа).
• В main нужно создать переменную liteReading с явным типом float32 и присвоить ей значение 0.1.
• Для печати результата программа должна использовать fmt.Printf (а не Print/Println) и импортировать только пакет fmt без дополнительных импортов.
• Каждая из двух выводимых строк должна содержать одновременно значение переменной и её тип, причём тип должен выводиться через плейсхолдер %T; формат обеих строк должен быть одинаковым.
*/

import "fmt"

func main() {
	// TODO: Создайте переменную defaultReading через := и присвойте ей значение 0.1 (без явного указания типа).
	defaultReading := 0.1

	// TODO: Создайте переменную liteReading с явным типом float32 и присвойте ей значение 0.1.
	var liteReading float32 = 0.1

	// TODO: Выведите ровно две строки через fmt.Printf: по одной строке для каждой переменной.
	// TODO: Формат обеих строк должен быть одинаковым и включать одновременно значение переменной и её тип (тип выводите через %T), каждая строка должна завершаться переводом строки.
	fmt.Printf("%v %T\n", defaultReading, defaultReading)
	fmt.Printf("%v %T\n", liteReading, liteReading)
}