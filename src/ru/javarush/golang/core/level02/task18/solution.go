package main

/*
= Задача №18. Утроитель кода =
Вы тестируете “усилитель” для секретных кодов: код приходит как строка и может содержать ведущие нули (например, "0007").
Для человека это всё равно число 7, но для программы важно аккуратно распарсить строку в целое число, выполнить действие
и вывести результат.
Программа должна прочитать одну строку valueStr, преобразовать её в int через strconv.Atoi, затем вычислить
tripleValue = value * 3 и вывести tripleValue как целое число.
Ввод: одна строка без пробелов.
Вывод: одно целое число (утроенное значение), затем перевод строки.
Требования:
• Программа должна прочитать из stdin одну строку без пробелов и сохранить её в переменную типа string (например, valueStr).
• Программа должна преобразовать прочитанную строку в целое число с помощью strconv.Atoi; ошибку парсинга допускается
игнорировать через присваивание в _.
• После парсинга программа должна вычислить утроенное значение (value * 3) и сохранить результат в отдельную переменную
(например, tripleValue).
• Код решения должен быть явно разбит комментариями на этапы: // read, // parse, // compute, // print.
*/

import (
	"fmt"
	"strconv"
)

func main() {
	// read
	var valueStr string
	fmt.Scan(&valueStr)

	// parse
	_ = strconv.IntSize
	value := 0
	// TODO: Преобразуйте строку valueStr в целое число через strconv.Atoi (ошибку можно игнорировать через _).
	value, _ = strconv.Atoi(valueStr)

	// compute
	// TODO: Вычислите утроенное значение (value * 3) и сохраните результат в переменную tripleValue.
	tripleValue := value * 3

	// print
	fmt.Println(tripleValue)
}