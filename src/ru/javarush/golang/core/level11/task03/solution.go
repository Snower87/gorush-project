package main

/*
= Задача №3. Санитарный контроль =
Вы автоматизируете "санитарный контроль" показаний датчиков: датчик может отдавать отрицательные значения (ошибка), а в отчёте такие значения нужно заменять на 0. Но есть важное правило: если вы передали массив в функцию, он не «починится сам по себе» — нужно явно взять результат обратно.
Программа читает из stdin пять целых чисел в массив in типа [5]int. Затем реализуется функция Normalize, которая принимает массив [5]int, заменяет все отрицательные элементы на 0 и возвращает новый массив.
В main вы печатаете исходный массив и результат нормализации двумя строками с префиксами in: и out: , чтобы было видно: исходник не менялся «магически», а результат получен через возвращаемое значение.
Требования:
• Программа должна прочитать из stdin ровно пять целых чисел в массив `in` типа `[5]int`, используя `fmt.Scan` в цикле `for` по индексам от 0 до 4 включительно.
• В программе должна быть реализована функция `Normalize` с сигнатурой `func Normalize(a [5]int) [5]int`, которая принимает и возвращает именно массив типа `[5]int` (не слайс).
• Функция `Normalize` должна заменить все элементы массива, которые меньше 0, на 0, при этом все неотрицательные элементы должны быть сохранены без изменений.
• Внутри функции `Normalize` обработка элементов массива должна выполняться с использованием цикла `for` (по индексам или через `range`).
• Результат работы `Normalize` должен быть сохранён в отдельную переменную (например, `out := Normalize(in)`), а исходный массив `in` должен использоваться отдельно, чтобы показать отсутствие “магического” изменения при передаче массива в функцию.
*/

import "fmt"

// Normalize принимает МАССИВ (копию), заменяет отрицательные на 0 и возвращает новый массив.
func Normalize(a [5]int) [5]int {
	// TODO: Реализуйте нормализацию массива: все элементы меньше 0 замените на 0.
	for i := range a {
		// TODO: Добавьте проверку значения a[i] и при необходимости замените его.
		a[i] = a[i]
		if a[i] < 0 {
		    a[i] = 0
		}
	}
	return a
}

func main() {
	var in [5]int

	// Читаем ровно 5 чисел в массив через fmt.Scan в цикле по индексам 0..4.
	for i := 0; i < 5; i++ {
		fmt.Scan(&in[i])
	}

	// Важно: массив не меняется "сам", результат нужно явно взять из return.
	out := Normalize(in)

	fmt.Printf("in: %v\n", in)
	fmt.Printf("out: %v\n", out)
}