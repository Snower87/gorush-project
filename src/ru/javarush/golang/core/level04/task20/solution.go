/*
= Задача №20. Поиск маркера =
Вы обрабатываете поток событий: первым числом приходит “маркер” target, а дальше — длинная лента значений до конца файла. Вам нужно найти, на какой позиции после маркера впервые встретилось значение, равное target. Как только нашли — дальше читать уже не нужно: вы и так знаете ответ.
Программа читает одно целое число targetValue, а затем читает целые числа до конца ввода (пока чтение успешно). Нужно найти позицию первого числа, равного targetValue. Позиция считается среди чисел, которые идут после targetValue, начиная с 1.
Программа должна вывести одно целое число: позицию первого совпадения, если targetValue встретился, или -1, если совпадений нет или после targetValue не было ни одного числа. Чтение должно выполняться в цикле, который завершается при ошибке ввода (EOF): стандартный паттерн “читаем, пока читается”, после чего цикл завершается через break.
Требования:
• Программа должна прочитать первое целое число targetValue с помощью fmt.Scan до начала обработки остального ввода.
• После чтения targetValue программа должна читать последующие целые числа в бесконечном цикле for { ... }, на каждой итерации проверять результат fmt.Scan и завершать цикл через break при любой ошибке чтения (в т.ч. EOF).
• Программа должна вести счётчик позиции (начиная с 1 для первого числа после targetValue) и увеличивать его на 1 для каждого успешно прочитанного числа.
• При первом прочитанном числе, равном targetValue, программа должна зафиксировать ответ (позицию) и немедленно прекратить дальнейшее чтение ввода (ранний выход из цикла через break), используя шаблон поиска с флагом found (bool).
*/

import "fmt"

func main() {
	var targetValue int
	fmt.Scan(&targetValue) // маркер читаем один раз до обработки ленты

	pos := 1
	answer := -1
	found := false

	for {
		var x int
		if _, err := fmt.Scan(&x); err != nil {
			break // читаем, пока читается (EOF/ошибка завершает цикл)
		}

		//_ = x // технически используем переменную, пока логика не реализована

		// TODO: Реализуйте проверку: если текущее число равно targetValue — это первое совпадение.
		// TODO: При первом совпадении сохраните позицию (pos) в answer, установите found = true и выйдите из цикла (break).
		// TODO: Корректно обновляйте pos: позиция считается среди чисел ПОСЛЕ targetValue, начиная с 1.
		if x == targetValue {
		    answer = pos
		    found = true
		    break
		}

        // Если текущее число не равно targetValue, увеличиваем счетчик позиции.
        // Это происходит для КАЖДОГО успешно прочитанного числа после targetValue,
        // независимо от его значения, пока совпадение не найдено.
        pos++
	}

	if !found {
		fmt.Println(-1)
		return
	}
	fmt.Println(answer)
}