package main

/*
= Сумма переводов =
Вы пишете микросервисную “заглушку” для переводов: два значения приходят как текстовые токены aStr и bStr, и каждое из
них нужно попытаться распарсить отдельно. Даже если где-то ошибка, программа всё равно должна посчитать сумму (в Go при
ошибке Atoi число будет 0) и честно вывести обе ошибки для диагностики.
Программа должна прочитать две строки, распарсить их двумя независимыми вызовами strconv.Atoi в (a, errA) и (b, errB),
посчитать sum = a + b, собрать строку "sum=" + <sum> через strconv.Itoa, и вывести три строки:
- sum=<число>
- errA=<ошибка или <nil>>
- errB=<ошибка или <nil>>
Требования:
• Программа должна прочитать ровно две строки (aStr и bStr) из stdin с помощью fmt.Scan, без чтения дополнительных значений.
• Каждая из двух строк должна быть распарсена отдельным вызовом strconv.Atoi с получением пары (число, ошибка): (a, errA) для aStr и (b, errB) для bStr.
• Ошибки парсинга должны сохраняться в две разные переменные с именами errA и errB (без переиспользования одной переменной ошибки для обоих разборов).
• Программа должна вычислять sum = a + b независимо от наличия ошибок парсинга; при ошибке Atoi соответствующее значение (a или b) должно учитываться как 0.
• Строка для вывода суммы должна быть собрана как "sum=" + strconv.Itoa(sum), то есть с использованием strconv.Itoa и оператора конкатенации строк.
*/

import (
	"fmt"
	"strconv"
)

func main() {
	var aStr, bStr string
	fmt.Scan(&aStr, &bStr)

	a, errA := 0, error(nil)
	b, errB := 0, error(nil)

	// TODO: Распарсите aStr отдельным вызовом strconv.Atoi и сохраните результат в переменные a и errA (ошибка должна быть в errA).
	a, errA = strconv.Atoi(aStr)
	// TODO: Распарсите bStr отдельным вызовом strconv.Atoi и сохраните результат в переменные b и errB (ошибка должна быть в errB).
	b, errB = strconv.Atoi(bStr)

	sum := a + b
	sumLine := "sum=" + strconv.Itoa(sum)

	errALine := "errA="
	errBLine := "errB="

	// TODO: Сформируйте errALine строго в формате `errA=<ошибка или <nil>>` (без пробелов вокруг `=`),
	errALine += errA
	// TODO: Сформируйте errBLine строго в формате `errB=<ошибка или <nil>>` (без пробелов вокруг `=`), .
	errBLine += errB

	fmt.Println(sumLine)
	fmt.Println(errALine)
	fmt.Println(errBLine)
}