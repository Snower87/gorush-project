package main

/*
= Задача №20. Чётный фильтр =
Вы делаете простую обработку данных: из потока чисел нужно оставить только чётные — например, это валидные события, а нечётные считаются шумом. Сколько чётных будет — заранее неизвестно, но вы можете выделить ёмкость «с запасом», равную входному размеру.
На вход подаётся целое число n, затем n целых чисел. Сформируйте новый слайс evenValues, содержащий только чётные числа из входа, в исходном порядке. Результирующий слайс собирайте через append. Ёмкость предвыделите как n: make([]int, 0, n).
После обработки выведите две строки: первая — even_count=<len> cap=<cap>, вторая — все чётные числа в одну строку через пробел (если чётных нет — пустая строка). При n=0 должны печататься две строки, и программа не должна падать.
Требования:
• Программа должна создать результирующий слайс evenValues строго через make([]int, 0, n), где n — количество входных чисел.
• Программа должна проходить по n входным целым числам и добавлять в evenValues только те x, для которых x%2==0, используя только форму evenValues = append(evenValues, x).
• Программа должна сохранять порядок чётных чисел в evenValues ровно таким же, как во входных данных (стабильная фильтрация).
• Программа должна считывать n и последующие n чисел с использованием fmt.Fscan (допускается чтение из bufio.Reader).
*/

import (
	"bufio"
	"fmt"
	"os"
)

func main() {
	in := bufio.NewReader(os.Stdin)
	out := bufio.NewWriter(os.Stdout)
	defer out.Flush()

	var n int
	fmt.Fscan(in, &n)

	// TODO: Создайте результирующий слайс evenValues длиной 0 и ёмкостью n (предвыделение памяти).
	evenValues := make([]int, 0, n)

	for i := 0; i < n; i++ {
		var x int
		fmt.Fscan(in, &x)

		// TODO: Добавляйте в evenValues только чётные числа через append, сохраняя исходный порядок.
		if x % 2 == 0 {
		    evenValues = append(evenValues, x)
		}
	}

	fmt.Fprintf(out, "even_count=%d cap=%d\n", len(evenValues), cap(evenValues))

	for i, v := range evenValues {
		if i > 0 {
			fmt.Fprint(out, " ")
		}
		fmt.Fprint(out, v)
	}
	fmt.Fprint(out, "\n")
}