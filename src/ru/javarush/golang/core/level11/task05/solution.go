/*
= Задача №5. Паспорт слайса =
Вы готовите мини-отчёт для отладчика: хотите быстро увидеть «паспорт» небольшого набора чисел — сколько элементов он хранит и какая у него ёмкость, чтобы понять, как Go хранит слайсы.
В программе без ввода создайте слайс nums со значениями [10, 20, 30] через литерал []int{...}. Сначала выведите в одной строке его len и cap в формате len=<длина> cap=<ёмкость>. Затем во второй строке выведите сам слайс обычной печатью через fmt.Println(nums).
Требования:
• В программе должен быть создан слайс nums со значениями 10, 20, 30 с помощью слайс-литерала вида []int{10, 20, 30}.
• Программа должна вывести ровно две строки в stdout: первую со значениями len и cap, вторую — сам слайс.
• Первая строка должна выводиться через fmt.Printf и иметь точный формат: len=3 cap=3 (между “len=3” и “cap=3” один пробел, затем перевод строки).
• Вторая строка должна выводить слайс nums обычной печатью через fmt.Println(nums), чтобы результат был в виде: [10 20 30].
*/

import "fmt"

func main() {
	// TODO: Создайте слайс nums через литерал с нужными значениями из условия.
	nums := []int{10, 20, 30}

	// TODO: Выведите len и cap в точном формате, который требуется в условии (используйте fmt.Printf).
	fmt.Printf("len=%d cap=%d\n", len(nums), cap(nums))

	// Вторая строка печатается обычным способом.
	fmt.Println(nums)
}