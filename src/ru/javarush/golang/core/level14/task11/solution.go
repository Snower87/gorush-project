package main

/*
= Задача №11. Первая руна =
Вы оформляете «бейдж» пользователя: нужно заменить первый символ в нике на новый (например, поставить эмодзи или специальный знак), но сделать это корректно для Unicode. Если действовать байтами, можно сломать UTF‑8, поэтому работаем рунами.
Программа читает две строки (по одному токену): s и repl. Нужно вернуть новую строку, где первый Unicode‑символ строки s заменён на первый Unicode‑символ строки repl. Для этого преобразуйте s в []rune, замените runes[0], затем соберите строку обратно через string(runes).
Если s пустая — результат пустая строка. Если repl пустая — результатом должна быть исходная s без изменений.
Требования:
• Программа должна считать две строки (два токена) `s` и `repl` через `fmt.Scan` или `fmt.Scanln`.
• Если `s` пустая, программа должна вывести пустую строку (один перевод строки) и завершиться.
• Если `repl` пустая, программа должна вывести исходную строку `s` без изменений (один перевод строки).
• Для замены первого символа `s` программа должна преобразовать `s` в `[]rune(s)`, заменить только `runes[0]` на первый Unicode-символ строки `repl` (то есть первый элемент `[]rune(repl)`), не выполняя никаких других замен.
• После замены программа должна собрать итоговую строку строго через `string(runes)` и вывести её.
*/

import "fmt"

func main() {
	var s, repl string
	fmt.Scan(&s, &repl)

	// Если s пустая — сразу печатаем пустую строку.
	if s == "" {
		fmt.Println("")
		return
	}

	// Если repl пустая — строку s не меняем.
	if repl == "" {
		fmt.Println(s)
		return
	}

	// TODO: Замените первый Unicode-символ строки s на первый Unicode-символ строки repl через преобразование в []rune.
	// TODO: Соберите результат строго через string(runes) и выведите ровно одну строку.
	runes := []rune(s)
	runes[0] = []rune(repl)[0]
	s = string(runes)
	fmt.Println(s)
}