package main

/*
= Задача №12. Старт и паспорт =
Вы делаете консольный “стартёр” задачи: программа должна сразу написать, что процесс начался, а затем вывести “паспорт” — id и имя задачи. Но требования специально подбрасывают два типичных случая из реальных проектов: конфликт имён и blank import.
В main должна быть переменная с именем strings, в которую читается ввод. Поэтому пакет "strings" придётся импортировать с алиасом (например, str "strings"). А ещё в import‑блоке должен быть blank import _ "math" — он нужен “для регистрации побочных эффектов” (в этом задании напрямую мы math не используем).
TASK берётся как строка после str.TrimSpace, ID — это strconv.Itoa(len(task)). Вывод — две строки.
Требования:
• В программе должен быть import-блок вида import ( ... ), в котором присутствуют импорты: "fmt", "strconv", str "strings", _ "math".
• Функция TrimSpace должна вызываться через алиас импортированного пакета (например, str.TrimSpace), а не через имя strings.
• В функции main должна быть переменная с именем strings, в которую считывается ввод пользователя (введённая строка).
• Импорт _ "math" должен присутствовать, при этом пакет math не должен использоваться по имени в коде (никаких обращений вида math.X).
• Значение TASK должно быть получено как результат str.TrimSpace от введённой строки, а ID должен вычисляться ровно как strconv.Itoa(len(task)).
*/

import (
	"fmt"
	_ "math" // blank import по условию (без прямого использования)
	"strconv"
	str "strings" // алиас нужен, потому что в main есть переменная strings
)

func main() {
	var strings string
	fmt.Scanf("%[^\n]", &strings)

	// TODO: Получите значение task из введённой строки. Используйте TrimSpace через алиас str.
	task := str.TrimSpace(strings)

	// TODO: Вычислите id по формуле из условия (в строковом виде). Используйте strconv.Itoa.
	id := strconv.Itoa(len(task))

	// TODO: Проверьте, что вывод ровно из двух строк и строго в нужном формате.
	fmt.Println("started.")
	fmt.Printf("id:%s task:%s\n", id, task)
}