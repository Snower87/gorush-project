package main

/*
= Задача №11. Первый дефис =
Вы принимаете “системный идентификатор” в виде строки без пробелов (ASCII). Иногда в него вставляют дефис `-`, чтобы отделить префикс. Вам нужно быстро найти, где впервые появился дефис, чтобы потом разрезать строку (в следующих задачах), но пока — просто вывести позицию.
Программа читает строку systemTag и выводит индекс первого символа -. Если дефиса нет — выводит -1.
Требования:
• Программа должна прочитать одну строку systemTag из stdin через fmt.Scan (строка без пробелов, ASCII).
• Программа должна использовать переменную answer, которая до начала поиска инициализируется значением -1.
• Поиск первого дефиса должен выполняться одним проходом по строке с циклом вида for i, v := range systemTag, где i — индекс символа, v — текущий символ.
• При первом обнаружении дефиса сравнение должно выполняться как v == '-', после чего answer должен получить значение i, и цикл должен завершиться через break (чтобы не искать дальше).
• Программа должна вывести в stdout ровно одно число (answer) и перевод строки.
*/

import "fmt"

func main() {
	var systemTag string
	fmt.Scan(&systemTag)

	// Ответ по умолчанию: "не найдено" (паттерн index-or-minus-one)
	answer := -1

	for i, v := range systemTag {
		// TODO: Найдите индекс первого символа '-' одним проходом по строке через range.
		// TODO: При первом обнаружении: присвойте answer индекс i и завершите цикл через break.
		if v == '-' {
		    answer = i
		    break
		}
	}

	fmt.Println(answer)
}