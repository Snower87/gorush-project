package main

/*
= Задача №17. Печатный разделитель =
Вы собираете консольный отчёт для админки: между блоками отчёта нужен аккуратный разделитель из символов =, длина которого задаётся числом. Но вы не хотите размазывать цикл по всему main — вместо этого вы делаете маленькую утилиту, которая умеет повторять любое действие n раз.
Программа читает одно целое число separatorLength и печатает строку из separatorLength символов =, а затем один перевод строки. Логику повторения вынесите в отдельную функцию repeat(n int, action func()): она должна выполнить переданное действие ровно n раз, используя цикл внутри repeat. В main должен остаться только ввод, вызов repeat и финальный вывод перевода строки. Если separatorLength <= 0, программа должна вывести только пустую строку (то есть один перевод строки), не печатая ни одного =.
Требования:
• Программа должна прочитать одно целое число separatorLength (n) из stdin с помощью fmt.Scan.
• В программе должна быть реализована функция repeat(n int, action func()), принимающая количество повторений и действие (функцию без аргументов).
• Функция repeat должна выполнять переданное действие ровно n раз, используя цикл for внутри repeat; отдельный цикл for для печати в main использовать нельзя.
• В repeat должна передаваться анонимная функция (function literal), которая печатает один символ '='.
• Если separatorLength (n) меньше либо равно 0, программа не должна печатать ни одного символа '=', а должна перейти к печати перевода строки.
• После печати (или непечати) символов '=' программа должна вывести ровно один перевод строки (один '\n').
*/

import "fmt"

// repeat выполняет action ровно n раз; цикл должен быть именно здесь, а не в main.
func repeat(n int, action func()) {
	for i := 0; i < n; i++ {
		// TODO: Вызовите action() внутри цикла, чтобы действие выполнилось ровно n раз.
		action()
	}
}

func main() {
	var separatorLength int
	fmt.Scan(&separatorLength)

	// Печатаем по одному символу '=' через анонимную функцию.
	repeat(separatorLength, func() {
		fmt.Print("=")
	})

	// Ровно один перевод строки в конце, даже если separatorLength <= 0.
	fmt.Print("\n")
}