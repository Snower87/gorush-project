package main

/*
= Задача №3. Сумма и лидер =
Вы пишете утилиту для ведущего викторины: он вводит, сколько ответов пришло, а затем сами результаты, чтобы быстро увидеть общую сумму баллов и самый сильный результат.
Программа должна считать из stdin целое число n, а затем прочитать n целых чисел. После этого выведите в одной строке два числа через пробел: сумму всех введённых чисел и максимальное из них. Если n равно 0, выведите 0 0.
Здесь важно, чтобы “накопители” жили дольше цикла: переменные sum и max должны быть объявлены снаружи, а переменная для текущего числа — внутри тела цикла.
Требования:
• Программа должна считать из stdin целое число n, затем (если n > 0) считать ровно n целых чисел, используя fmt.Scan для всех операций ввода.
• Если n равно 0, программа должна вывести строку "0 0" (два числа через пробел и перевод строки) и не выполнять чтение последующих чисел.
• Программа должна объявить переменную sum до цикла и на каждой итерации увеличивать её через обычное присваивание (оператор =), добавляя очередное введённое число.
• Программа должна объявить переменную max до цикла и корректно инициализировать её без использования заведомо маленьких “магических” констант (например, через присваивание первому введённому числу при n > 0), затем обновлять max при необходимости в цикле.
• Программа должна использовать цикл for с индексом/счётчиком от 0 до n-1, при этом переменная для текущего числа (например, x) должна объявляться внутри тела цикла через `var x int`.
*/

import "fmt"

func main() {
	var n int
	fmt.Scan(&n)

	if n == 0 {
		fmt.Println("0 0")
		return
	}

	// "Накопители" живут дольше цикла: объявляем снаружи.
	var sum int
	var max int
	var initFirst bool = false

	for i := 0; i < n; i++ {
		// Текущее число — внутри тела цикла, чтобы показать область видимости.
		var x int
		fmt.Scan(&x)

		// TODO: увеличьте сумму на x через обычное присваивание (оператор =) без использования слайсов/массивов
		// TODO: найдите максимум среди введённых чисел; корректно инициализируйте max без "магических" значений
		sum += x
		if initFirst == false {
		    max = x
		    initFirst = true
		}
		if x > max {
		    max = x
		}
	}

	fmt.Println(sum, max)
}