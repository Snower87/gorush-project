package main

/*
= Задача №2. Счётчик наград =
Вы пишете простую механику для игры: игрок получает награды за каждый уровень с 1 до N, и вам нужно посчитать общее количество наград.
Но есть строгие правила: если кто-то прислал отрицательное N, это явно ошибка в данных, и игру надо остановить сообщением error.
Программа читает одно целое число levelsCount (это и есть N). Если число отрицательное — печатает error. Если неотрицательное — в цикле складывает 1 + 2 + … + N в переменную totalRewards и выводит сумму вместе с её типом, чтобы вы убедились: всё считается именно в int.
// пример кода для подсчёта наград
Требования:
• Программа должна прочитать одно целое число levelsCount из stdin с помощью fmt.Scan.
• Переменная levelsCount должна быть объявлена с типом int (например, var levelsCount int).
• Если levelsCount < 0, программа должна вывести ровно error (без дополнительных символов/строк/значений) и больше ничего не выводить.
• Если levelsCount неотрицательное, программа должна вычислить сумму 1 + 2 + … + levelsCount, используя цикл for (любой корректный вариант for).
• Сумма должна накапливаться в переменной totalRewards типа int.
*/

import "fmt"

func main() {
	var levelsCount int
	fmt.Scan(&levelsCount)

	// Отрицательное значение — ошибка входных данных.
	if levelsCount < 0 {
		fmt.Print("error")
		return
	}

	var totalRewards int
	for level := 1; level <= levelsCount; level++ {
		// TODO: Реализуйте накопление суммы 1 + 2 + … + levelsCount в переменной totalRewards.
		totalRewards += level
	}

	// Печатаем сумму и тип переменной (должно быть int).
	fmt.Printf("%d (%T)", totalRewards, totalRewards)
}