package main

/*
= Задача №13. Печать диапазона =
Вы пишете консольный инструмент для проверки датчика: он выдаёт два целых числа A и B, и оператор просит “показать весь диапазон” — чтобы глазами увидеть, что границы корректные. Числа должны идти подряд, без пропусков.
Программа читает A и B (гарантируется A <= B) и выводит все целые числа от A до B включительно в одну строку через пробел. После последнего числа должен быть перевод строки, а лишнего пробела в конце быть не должно.
Требования:
• Программа должна считать два целых числа A и B из stdin (допускается ввод через fmt.Scan), при этом можно полагаться на условие, что A <= B.
• Программа должна вывести все целые числа от A до B включительно в одной строке, разделяя числа одним пробелом.
• Для перебора чисел программа должна использовать цикл for со счётчиком, который начинается с A и увеличивается до B включительно.
• Программа не должна печатать пробел после последнего числа и должна завершать вывод переводом строки; в выводе не должно быть никаких дополнительных слов или символов кроме чисел, пробелов между ними и финального '\n'.
• Если A равно B, программа должна вывести ровно одно число (A) и перевод строки.
*/

import "fmt"

func main() {
	var a, b int
	fmt.Scan(&a, &b)

	// TODO: Выведите все целые числа от a до b включительно в одну строку через пробел.
	// TODO: После последнего числа НЕ должно быть пробела, но должен быть перевод строки.
	// TODO: Для перебора используйте цикл for со счётчиком от a до b включительно.
	// TODO: Не используйте массивы или слайсы для накопления/формирования вывода.
	for i := a; i <= b; i++ {
	    if i == b {
	        fmt.Println(i)
	        break
	    }
        fmt.Printf("%d ", i)
	}
}