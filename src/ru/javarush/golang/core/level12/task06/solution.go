package main

/*
= Задача №6. Два ярлыка =
Представьте, что вы храните палитру цветов для интерфейса: original := []string{"red", "green", "blue"}. В какой-то момент вы не копируете палитру, а просто добавляете второй “ярлык” на тот же набор: alias := original. И тут начинается магия ссылок: вы правите “ярлык”, а меняется оригинал.
Программа должна сделать alias := original, затем заменить alias[1] на "YELLOW". После этого нужно вывести alias, original, а затем честно проверить, действительно ли они смотрят на одну и ту же память: сравнить адреса первых элементов выражением &original[0] == &alias[0].
// Пример кода
original := []string{"red", "green", "blue"}
alias := original
alias[1] = "YELLOW"
fmt.Println(alias)
fmt.Println(original)
fmt.Println(&original[0] == &alias[0])
Требования:
• В программе должен быть создан слайс original через литерал вида []string{"red", "green", "blue"}.
• В программе должен быть создан alias только присваиванием alias := original; нельзя использовать копирование, append, copy или любые другие способы создания отдельного массива.
• Программа должна заменить второй элемент палитры только операцией alias[1] = "YELLOW" (без прямого изменения original).
• Программа должна вывести alias, затем original, как две первые строки вывода, чтобы было видно, что изменение через alias отразилось в original.
• Третья строка вывода должна быть булевым значением, полученным строго из выражения &original[0] == &alias[0].
*/

import "fmt"

func main() {
	original := []string{"red", "green", "blue"}

	alias := original

	// TODO: Замените второй элемент палитры только через alias (без прямого изменения original)
	alias[1] = "YELLOW"

	fmt.Println(alias)
	fmt.Println(original)

	// TODO: Выведите булево значение строго из выражения сравнения адресов первых элементов
	fmt.Println(&original[0] == &alias[0])
}