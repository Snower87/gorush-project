package main

/*
= Задача №20. Потоковая статистика =
Вы подключились к потоку данных: числа идут одно за другим, и заранее неизвестно, сколько их будет. В конце поток просто заканчивается (EOF). Вам нужно собрать простую статистику “на лету”: общую сумму, максимальное значение и количество строго положительных чисел. Это похоже на мониторинг датчика, который может оборваться в любой момент.
Программа читает целые числа из stdin до конца ввода. Затем выводит три строки: (1) сумму всех чисел, (2) максимум среди прочитанных, (3) количество чисел строго больше 0. Если во входе нет ни одного числа, нужно вывести три строки с нулями: 0, 0, 0.
Требования:
• Программа должна читать целые числа из stdin до конца ввода, выполняя чтение в бесконечном цикле for { ... } через fmt.Scan(&x) и завершая цикл по ошибке чтения (например, EOF) с помощью break.
• Программа должна накапливать сумму всех прочитанных чисел в аккумуляторе, который инициализирован значением 0.
• Программа должна увеличивать счётчик только для чисел, которые строго больше 0 (условие x > 0).
*/

import "fmt"

func main() {
	var (
		x        int
		sum      int
		max      int
		posCount int
		hasAny   bool
	)

	for {
		if _, err := fmt.Scan(&x); err != nil {
			break
		}

		sum += x

		// TODO: Увеличивать счётчик только для чисел, которые строго больше 0 (x > 0).
		if x > 0 {
			posCount++
		}

		// TODO: Реализовать корректное вычисление максимума, включая случай,
		// когда все числа отрицательные (нельзя полагаться на max=0).
		if x > max || hasAny == false {
			max = x
		}

		hasAny = true
	}

	if !hasAny {
		fmt.Println(0)
		fmt.Println(0)
		fmt.Println(0)
		return
	}

	fmt.Println(sum)
	fmt.Println(max)
	fmt.Println(posCount)
}