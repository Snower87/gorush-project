package main

/*
= Задача №7. Ответ сервера =
Вы делаете консольный помощник для тестирования API. Оператор вводит HTTP‑метод и код статуса, а программа должна красиво вывести “как это будет выглядеть в логах”: метод в верхнем регистре и текст статуса (например, POST Created).
Метод нужно нормализовать через strings.ToUpper, а текст статуса взять из стандартной библиотеки через http.StatusText. Если текст статуса пустой (код неизвестен), выведите "UNKNOWN".
Требования:
• Программа должна прочитать из стандартного ввода ровно два значения через fmt.Scan: HTTP-метод (string) и код статуса (int).
• В программе должны быть импортированы пакеты fmt, strings и net/http, без alias-импортов и без неиспользуемых импортов.
• Введённый метод должен быть приведён к верхнему регистру с помощью strings.ToUpper.
• Текст статуса должен быть получен вызовом http.StatusText(code) для введённого кода.
• Если http.StatusText(code) вернул пустую строку, вместо текста статуса должно быть использовано значение UNKNOWN.
*/

import (
	"fmt"
	"net/http"
	"strings"
)

func main() {
	var method string
	var code int

	fmt.Scan(&method, &code)
	// TODO: Нормализуйте HTTP-метод через strings.ToUpper.
	method = strings.ToUpper(method)
	// В шаблоне метод намеренно оставлен без нормализации.

	// TODO: Получите текст статуса через http.StatusText(code).
	// TODO: Если http.StatusText(code) вернул пустую строку — используйте "UNKNOWN".
	// В шаблоне намеренно используется фиксированное значение, чтобы задача не была решена.
	status := http.StatusText(code)
	if status == "" {
	    status = "UNKNOWN"
	}

	fmt.Printf("%s %s\n", method, status)
	// code пока не используется в шаблоне.
}