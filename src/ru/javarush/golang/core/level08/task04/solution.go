	blocks8 := blocks

	if blocks8 > int64(MaxBlocks) {
		fmt.Println("TOO MANY")
		return
	}

    blocks8 = uint8(blocks)

	// TODO: Делайте конвертацию к uint8 только в ветке, где blocks гарантированно не превышает MaxBlocks.
