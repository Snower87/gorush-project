package main

/*
= Задача №6. Первый байт =
В поддержку прилетел баг: «ник начинается странно». Вы хотите сразу увидеть две разные правды про начало строки: первый байт (как хранится в памяти) и первый символ (как воспринимается в Unicode). Это особенно наглядно на кириллице и других многобайтных символах.
Программа читает одну строку без пробелов и печатает одну строку отчёта формата byte=<число> rune=<символ> code=<число>. Первое число — это s[0]. Первый символ и его код нужно достать через самую первую итерацию for range по строке (взяли — и сразу остановились).
Требования:
• Программа должна прочитать одну строку без пробелов из stdin с помощью fmt.Scan.
• Программа должна получить первый байт строки выражением s[0] и использовать его значение как число для поля byte.
• Программа должна получить первый символ строки через самую первую итерацию цикла for range по строке; после получения первого rune и его кода цикл должен быть немедленно остановлен (например, через break).
• Программа должна вывести ровно одну строку в формате byte=<число> rune=<символ> code=<число> с переводом строки в конце (\n).
• Символ (rune) должен выводиться как %c, а код символа (значение rune) — как число.
*/

import "fmt"

func main() {
	var s string
	fmt.Scan(&s)

	// TODO: Получите первый байт строки выражением s[0] и сохраните в переменную b (тип byte).
	b := s[0]

	// TODO: Получите первый Unicode-символ (rune) через самую первую итерацию for range по строке.
	// TODO: После получения rune и его кода немедленно остановите цикл (например, через break).
	var r rune

	for _, val := range s {
		// TODO: Реализуйте извлечение первого rune из строки через range и остановку цикла.
		r = val
		break
	}

	fmt.Printf("byte=%d rune=%c code=%d\n", b, r, r)
}