package main

/*
= Задача №15. Чистые теги =
Вы готовите строку тегов для публикации: автор вводит всё как попало — лишние пробелы, двойные запятые, запятая в конце. А вам нужно получить аккуратный список тегов без пустых значений и собрать красивую строку для вывода: Tags: ....
В программе есть строка rawTags со значением " go, strings,, utf-8 , ". Нормализация такая: обрезать пробелы по краям всей строки (strings.TrimSpace), разбить по запятой (strings.Split), у каждого кусочка обрезать пробелы по краям, удалить пустые теги, затем собрать итоговую строку вида "Tags: go, strings, utf-8" с помощью strings.Builder и вывести её одной строкой.
Требования:
• В программе должна быть переменная rawTags со значением " go, strings,, utf-8 , " (как в условии), от которой строится весь дальнейший результат.
• Программа должна обрезать пробелы по краям всей строки через strings.TrimSpace, затем разбить строку по запятой через strings.Split, затем у каждого полученного фрагмента обрезать пробелы по краям (также через strings.TrimSpace).
• Программа должна пройтись циклом по результату strings.Split, собрать новый слайс тегов через append и не добавлять в него пустые значения (пустые строки после обрезки пробелов).
• Итоговый список тегов должен быть собран в одну строку с помощью strings.Join, используя разделитель ", " (запятая и пробел).
• Финальный вывод вида "Tags: go, strings, utf-8" должен быть собран через strings.Builder с использованием WriteString и получением результата через String, без конкатенации вида result +=.
*/

import (
	"fmt"
	"strings"
)

func main() {
	// Исходная "грязная" строка тегов (по условию)
	rawTags := "  go,  strings,,  utf-8  ,  "

	// 1) Обрезаем пробелы по краям всей строки
	clean := strings.TrimSpace(rawTags)

	// 2) Разбиваем по запятой
	parts := strings.Split(clean, ",")

	// 3) Собираем теги (пока упрощённо)
	var tags []string
	for _, p := range parts {
		// TODO: Обрежьте пробелы у каждого тега через strings.TrimSpace (нельзя оставлять лишние пробелы внутри списка).
		// TODO: Отфильтруйте пустые теги (пустая строка после обрезки пробелов не должна попадать в итоговый список).
		p = strings.TrimSpace(p)

		// Временная упрощённая фильтрация: убирает только строго пустые фрагменты,
		// но не учитывает фрагменты из одних пробелов и не чистит пробелы вокруг тегов.
		if p == "" {
			continue
		}

		tags = append(tags, p)
	}

	// 4) Склеиваем теги через ", "
	list := strings.Join(tags, ", ")

	// 5) Собираем финальную строку через strings.Builder (без конкатенации)
	var b strings.Builder
	b.WriteString("Tags: ")
	b.WriteString(list)

	// Ровно одна строка вывода
	fmt.Println(b.String())
}