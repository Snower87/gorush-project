package main

/*
= Задача №3. Касса и сдача =
Вы делаете простую “кассу” для ярмарки: продавец вводит, сколько покупок было в очереди, а затем по очереди вводит суммы
(в целых единицах). Нужно мгновенно получить общий итог, не сохраняя весь список покупок.
Программа читает целое число itemsCount, затем читает itemsCount целых чисел и выводит их сумму. Числа могут быть введены
в одну или несколько строк — как удобно. Если itemsCount равно 0, сумма считается равной 0, и программа должна вывести 0.
Требования:
• Программа должна прочитать из stdin целое число itemsCount с помощью fmt.Scan до чтения сумм покупок.
• Программа должна использовать классический цикл for со счётчиком (0..itemsCount-1 или 1..itemsCount) и выполнить ровно itemsCount итераций без использования break/continue.
• На каждой итерации цикла программа должна читать ровно одно целое число через fmt.Scan и сразу учитывать его в сумме, не сохраняя список значений.
• Сумма всех введённых чисел должна накапливаться в отдельной переменной типа int (например, totalSum).
*/

import "fmt"

func main() {
	var itemsCount int
	fmt.Scan(&itemsCount)

	totalSum := 0

	// TODO: Реализуйте суммирование покупок без сохранения списка значений.
	// Подсказка по структуре: в цикле нужно itemsCount раз считать одно число и сразу учесть его в totalSum.
	for i := 1; i <= itemsCount; i++ {
		var price int
		// TODO: Прочитайте цену покупки в переменную price через fmt.Scan(&price).
		// TODO: Сразу добавьте price к totalSum (накапливайте сумму в int-переменной).
		fmt.Scan(&price)
		totalSum += price
	}

	// Выводим только итоговую сумму (без текста).
	fmt.Print(totalSum)
}