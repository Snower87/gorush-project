package main

/*
= Задача №5. Разгон бустера =
Вы пишете симулятор для маленького "бустера" в игре: у него есть текущая мощность x. Пока мощность меньше 1000, бустер удваивается — будто игрок нажимает кнопку "ускорить". Вам нужно не только получить итоговую мощность, но и посчитать, сколько раз сработало удваивание — чтобы баланс игры можно было проверять.
Программа читает целое currentPower и, пока оно меньше 1000, умножает его на 2. После завершения печатает в одной строке через пробел: итоговую мощность и количество удваиваний. Если исходная мощность уже не меньше 1000, удваиваний нет и счётчик равен 0.
Требования:
• Программа должна считать одно целое число N из стандартного ввода, используя fmt.Scan.
• Программа должна использовать цикл вида for init; cond; post, где счётчик объявлен прямо в init (например, i := 1), шаг — i++, а условие обеспечивает выполнение ровно N итераций.
• На первой напечатанной строке должен быть номер 1, далее номера должны увеличиваться на 1 на каждой итерации, пока не будет выведен номер N.
• На каждой итерации программа должна печатать ровно одну строку строго вида Iteration <номер> с переводом строки (без дополнительных символов, слов, пробелов или пустых строк).
• Если введено N = 0, программа не должна печатать вообще ничего (никаких строк, включая перевод строки).
*/

import "fmt"

func main() {
	var currentPower int
	fmt.Scan(&currentPower)

	doubles := 0

	// for <cond> { ... } — это "for как while" (цикл с одним условием)
	for currentPower < 1000 {
		currentPower *= 2
		doubles++
	}

	// Формат: итоговое_значение количество_удваиваний
	fmt.Printf("%d %d", currentPower, doubles)
}