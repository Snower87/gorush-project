package main

/*
= Задача №3. Фейс контроль =
Представьте, что вы делаете «фейс‑контроль» для закрытого чата. Пользователь вводит ник и возраст. Пройти можно только если возраст достаточный, а ник не слишком длинный (иначе он ломает верстку и таблички в админке).
Минимальный возраст вы задаёте untyped-константой, а максимально допустимую длину ника — typed-константой маленького беззнакового типа. Из-за этого сравнение длины строки придётся сделать аккуратно по типам.
Требования:
• Программа должна прочитать из stdin два значения: строку `nickname` (одно слово без пробелов) и целое число `age`.
• В коде должна быть объявлена константа минимального возраста без явного типа: `const minAge = 18`.
• В коде должна быть объявлена typed-константа максимальной длины ника ровно с типом `uint8`: `const maxLen uint8 = 12`.
• В проверке должна использоваться `len(nickname)`, а сравнение результата `len(nickname)` с `maxLen` должно выполняться с явным приведением типов (чтобы сравнение было корректным по типам).
• Решение должно допускать пользователя только при одновременном выполнении двух условий (возраст достаточный и ник не слишком длинный), используя операцию `&&` в логике.
• Программа должна вывести в stdout ровно одно слово с переводом строки: `ALLOWED` (если допущен) или `DENIED` (если не допущен).
*/

import "fmt"

const minAge = 18       // untyped-константа
const maxLen uint8 = 12 // typed-константа

func main() {
	var nickname string
	var age int
	fmt.Scan(&nickname, &age)

	// TODO: Реализуйте условие допуска: возраст должен быть не меньше minAge, а длина ника (len) не больше maxLen.
	// TODO: В сравнении len(nickname) и maxLen сделайте явное приведение типов.
	// TODO: В логике допуска используйте && (должны выполниться оба условия одновременно).

	allowed := age >= minAge && uint8(len(nickname)) <= maxLen

	if allowed {
		fmt.Println("ALLOWED")
		return
	}
	fmt.Println("DENIED")
}